                <h2>Wie stellt man eine Mikrozement-Arbeitsplatte her?</h2>

                <p>
                    Der Prozess des Auftragens einer Mikrozement-Arbeitsplatte hängt von der Art der gewünschten
                    Oberfläche und dem als Basis der Oberfläche verwendeten Material ab, ob es sich um eine saugfähige
                    Beschichtung wie Gipskarton, Gips, Zementputz und Sand oder DM handelt, oder ob es sich um eine
                    nicht saugfähige Beschichtung wie Marmor, Terrazzo, Fliesen oder Granit handelt.

                </p>
                <p>
                    Wir beschreiben die Anwendung von Mikrozement auf Arbeitsplatten Schritt für Schritt, je nach
                    gewünschtem Ergebnis:

                </p>
                <h3>1. Legen Sie das Gewebe auf die zukünftige Mikrozement-Arbeitsplatte.</h3>

                <p>
                    Der erste Schritt besteht darin, das Gewebe auf die Oberfläche zu legen, die die Basis der
                    Mikrozement-Arbeitsplatte bilden wird: Marmor, Granit, Gipskarton... Dies ist sehr wichtig, weil
                    dadurch verhindert wird, dass mit der Zeit Risse entstehen.

                </p>
                <h3>2. Grundierung der Mikrozement-Arbeitsplatte</h3>

                <p>
                    Der zweite Schritt bei der Anwendung von Mikrozement auf einer Arbeitsplatte besteht aus einer
                    Grundierung, die die Verbindung der vorhandenen saugfähigen oder nicht saugfähigen Beschichtung mit
                    der neuen Mikrozement-Arbeitsplatte erleichtert. Im ersten Fall sollte der PRIMACEM ABS
                    Haftvermittler und im zweiten Fall der PRIMACEM PLUS Primer aufgetragen werden.

                </p>
                <h3>3. Tragen Sie zwei Schichten Microbase-Mikrozement auf die Arbeitsplatte auf</h3>

                <p>
                    Tragen Sie dann zwei Schichten Microbase-Mikrozement auf. Die erste Schicht sollte ohne Pigment
                    sein, und nach dem Schleifen ist eine zweite Schicht Microbase an der Reihe, die bereits pigmentiert
                    ist, je nach der für die Arbeitsplatte gewählten Farbe des Mikrozements. Danach muss nochmals
                    geschliffen werden. Wenn Sie ein Stein- oder Rustikal-Finish wünschen, sollten Sie nach den zwei
                    Schichten Microbase-Mikrozement die Mikrozement-Arbeitsplatte zum Schutz zuerst mit
                    Presealer-Acryllack und anschließend mit Topsealer-Polyurethanlack versiegeln.

                </p>
                <h3>4. Tragen Sie zwei Schichten Microfino Mikrozement auf die Arbeitsplatte auf</h3>

                <p>
                    Soll die Mikrozement-Arbeitsplatte hingegen eine ausgeprägte Struktur oder einen Stuckeffekt haben,
                    sind nach den zwei Schichten Microbase-Mikrozement zwei Schichten Microfino-Mikrozement, beide
                    pigmentiert, an der Reihe. Nach jeder Schicht muss ausnahmslos geschliffen werden. Nachdem die
                    beiden Schichten Microfino aufgetragen wurden, sollte der Untergrund zunächst mit Presealer und dann
                    mit Topsealer geschützt werden.

                </p>
                <h3>5. Tragen Sie zwei Schichten Microdeck Mikrozement auf die Arbeitsplatte auf </h3>

                <p>
                    Wenn Sie es bevorzugen, dass sich die Mikrozement-Arbeitsplatte durch ihr natürliches Aussehen
                    auszeichnet, wird empfohlen, nach den zwei Schichten Microbase zwei Schichten pigmentierten
                    Microdeck-Mikrozements aufzutragen und zum Schluss zu schleifen. Zum Abschluss sollten, wie bei den
                    beiden vorherigen Anwendungen, Presealer-Lack und Topsealer-Mikrozementversiegelung auf den
                    Untergrund aufgetragen werden.

                </p>
                <h3>6. Schützen und versiegeln Sie die Mikrozement-Arbeitsplatte</h3>

                <p>
                    Nach dem Auftragen der zwei Schichten Microbase, Microfino oder Microdeck, je nach gewünschtem
                    Finish, muss die Mikrozement-Arbeitsplatte geschützt werden, um Probleme zu vermeiden. Um die Möbel
                    richtig zu versiegeln, sind zwei Anstriche mit Presealer und zwei Anstriche mit Topsealer
                    erforderlich, wie oben erklärt.

                </p>

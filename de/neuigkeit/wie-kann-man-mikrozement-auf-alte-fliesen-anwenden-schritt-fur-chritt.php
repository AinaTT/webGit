                    <h4>1. die Trocknungszeiten einhalten</h4>

                    <p>
                        Bei der Verwendung einer Grundierung für nicht saugende Oberflächen wie Primacem PLUS ist es
                        sehr wichtig, die Trocknungszeit zu beachten, wenn Fliesen mit Mikrozement belegt werden. Wenn
                        die in den technischen Datenblättern angegebenen Angaben und Trocknungszeiten nicht strikt
                        befolgt werden, kommt es zu Feuchtigkeitseinschlüssen zwischen den Anstrichen und damit zur
                        Markierung der Fliesenfugen.

                    </p>
                    <p>
                        Mit diesen Tips vermeiden Sie den häufigsten Fehler beim Auftragen von Mikrozement auf Fliesen.
                        Ein möglicher Fehler, der durch die Verwendung von Primacem Impoxy A+B Primer vermieden wird,
                        bei dem man es mit den Trocknungszeiten nicht so genau nehmen muss, da er die kapillare
                        Feuchtigkeit blockiert.

                    </p>
                    <h4>2. Lassen Sie 24 Stunden trocknen, bevor Sie die zweite Schicht Mikrozement auftragen</h4>

                    <p>
                        Eine weitere Empfehlung der Topciment-Experten, auch im Falle der Verwendung von Primacem PLUS,
                        ist es, zwischen der ersten und zweiten Schicht der Mikrozementbasis einen Zeitraum von 24
                        Stunden zum Trocknen zu lassen.

                    </p>
                    <p>
                        Ein Prozess, den nicht jeder macht, da es nicht zwingend erforderlich ist, aber mit dem der
                        Profi sicherstellt, dass es viel unwahrscheinlicher ist, die Fugen durch einen Fehler bei der
                        Anwendung von Mikrozement auf Fliesen zu markieren. Mit Primacem Impoxy A+B wird es mit dem
                        normalen System gemacht. Das heißt, die zweite Schicht Microbase wird direkt nach der ersten
                        aufgetragen.
                    </p>

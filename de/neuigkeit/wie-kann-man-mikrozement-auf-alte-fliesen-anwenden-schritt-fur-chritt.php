                        </figcaption>
                    </figure>
                    <h4>5. Verlegung von Fasergewebe auf der ersten Schicht Mikrozement auf Fliesen</h4>

                    <p>
                        Nach der entsprechenden Trocknungszeit (siehe technische Datenblätter) legen wir vor dem
                        Auftragen des Mikrozements das Fasergewebe auf die Fliesen, um die zukünftige Beschichtung zu
                        schützen und Risse, Sprünge etc. zu vermeiden. Ein Schritt, den nicht jeder beachtet und der
                        später zu großem Kopfzerbrechen führt.

                    </p>
                    <p>
                        Tragen Sie anschließend die erste Schicht Mikrozementbasis (Microbase) ohne Pigment auf die
                        Fliesen auf. Sobald die Fliesen gut mit dem Mikrozement bedeckt sind, ohne Unregelmäßigkeiten,
                        wird der Untergrund geschliffen.

                    </p>
                    <h4>6. Tragen Sie die zweite Schicht Mikrozement-Basis auf die Fliesen auf </h4>

                    <p>
                        Tragen Sie die zweite Schicht der Mikrozementbasis (Microbase) auf. Diesmal muss sie mit der
                        Farbe des Mikrozements pigmentiert werden, die für die Abdeckung der alten Fliesen gewählt
                        wurde. Auf die gleiche Weise wie im vorherigen Schritt muss die Oberfläche geschliffen werden.

                    </p>
                    <h4>7. Auftragen von zwei Schichten Microdeck oder Microfino Mikrozement auf Fliesen</h4>

                    <p>
                        Nach der Mikrozementbasis tragen Sie zwei Schichten Microdeck Mikrozement oder zwei Schichten
                        Microfino Mikrozement auf Fliesen auf. Ersteres wird normalerweise für Böden und letzteres für
                        Wände verwendet. Allerdings sind auch Kombinationen möglich, je nach Geschmack, Vorlieben und
                        gewünschter Ausführung. Beide Schichten werden bereits pigmentiert sein. Anschließend schleifen.

                    </p>
                    <h4>8. Schützen und versiegeln Sie den Mikrozement auf Fliesen</h4>

                    <p>
                        Der letzte Schritt ist einer der wichtigsten. Es dient zum Schutz der Mikrozementbeschichtung
                        auf Fliesen. Hierfür haben wir alle Arten von Lacken und Versiegelungen. Allerdings empfehlen
                        wir bei dieser Gelegenheit zwei <a href="../mikrozement/lack-polyurethan-acrylat-Wasserbasis-topsealer#presealer">Schichten
                            Presealer</a> und zwei Schichten
                        <strong><a class="links" href="../mikrozement/lack-polyurethan-acrylat-Wasserbasis-topsealer#topsealer-one-coat">Topsealer WT
                                One Coat </a> </strong> zu verwenden.

                    </p>
                    <figure class="figure mt-4">
                        <img src="https://www.topciment.com/imagenes/news/azulejos-microcemento-piedraparis-suelo-pared-cocina.jpg"

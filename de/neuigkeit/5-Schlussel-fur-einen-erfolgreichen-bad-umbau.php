                        <h2>4.Auswahl der Möbel</h2> <p><br>Toiletten, Möbel und Accessoires sind bei der Einrichtung genauso wichtig wie die Wände und Böden des Badezimmers. Bei den Waschbecken zum Beispiel gibt es viele Typen: freistehend, eingebaut, Aufsatzwaschbecken, kompakt... jeweils in einer Vielzahl von Materialien: Naturstein, Harz, Zement, Porzellan, Keramik, Glas... <b>Alles hängt von der Umgebung ab, in der es platziert werden soll</b>: die klassischen Sanitärobjekte bringen einen romantischen Hauch, perfekt für eine Vintage-Dekoration, während die wandmontierten in jenen Projekten nützlich sein werden, die nach einem modernen und zeitgenössischen Touch suchen.<br>&nbsp;</p>

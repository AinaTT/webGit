                    <h2>Wie man einen Mikrozementboden verlegt</h2>
                    <p>Nun, da Sie alle Vorzüge von Mikrozementböden kennen, ist es an der Zeit, den Anwendungsprozess
                        zu erklären. Dies sind die <strong>sechs Schritte</strong>, die Sie berücksichtigen müssen, um
                        einen Mikrozementboden erfolgreich zu verlegen:</p>
                    <h3> 1. Reinigen Sie den zukünftigen Mikrozementboden</h3>
                    <p>Der Belag, der mit Mikrozement bedeckt werden soll, muss in perfektem Zustand sein. Daher ist der
                        erste Schritt die Reinigung des zukünftigen Mikrozementbodens von <strong>Staub, Flecken,
                            Fett</strong> oder jeder anderen Art von Partikeln; sowie die Sicherstellung, dass der
                        Untergrund in perfektem Zustand ist. Es ist sehr wichtig, den Feuchtigkeitsgrad des Bodens zu
                        prüfen, zu warten, bis er getrocknet ist, oder direkt eine Impoxid-Sperre aufzutragen, um
                        aufsteigende Feuchtigkeit zu blockieren oder auch als Dampfsperre.</p>
                    <h3>2. Grundierung</h3>
                    <p>Nachdem die Oberfläche sauber ist, tragen Sie eine Schicht Grundierung auf den Boden auf, um die
                        <strong>Haftung zwischen dem vorhandenen Untergrund und dem neuen Mikrozementboden</strong> zu
                        verbessern. </strong> Wenn der Untergrund saugfähig ist, wie z. B. Beton oder
                        selbstnivellierend, verwenden Sie <a class="links"
                            href="https://www.topciment.com/de/mikrozement/grundierungs-und-adhasionsprimer-primacem#primacem-abs">PRIMACEM
                            ABS</a>; wenn er nicht saugfähig ist, wie z. B. Terrazzo, Fliesen oder Marmor, verwenden Sie
                        <a class="links"
                            href="https://www.topciment.com/de/mikrozement/grundierungs-und-adhasionsprimer-primacem#primacem-plus">PRIMACEM
                            PLUS</a>.</p>
                    <h3>3. Erste Schicht Mikrozementbasis</h3>
                    <p>Nach der Grundierung tragen Sie die erste Schicht unpigmentierter Mikrozementbasis auf.
                        <strong>Es ist ratsam, ein Glasfasernetz</strong> zu verwenden, da es dem Fachmann hilft, die
                        Wahrscheinlichkeit zu minimieren, dass mit der Zeit Risse entstehen. Nach dem Auftragen
                        schleifen Sie die Oberfläche. </p>



                    <h3>4. Zweite Schicht Mikrozementbasis </h3>
                    <p>Wenn die Oberfläche vollständig trocken ist, tragen Sie eine zweite Schicht pigmentierter
                        Mikrozementbasis auf. In diesem Schritt <strong>wählen Sie die gewünschte Farbe des Mikrozements
                        </strong>zur Abdeckung des durchgehenden Bodens und, sobald die Oberfläche perfekt abgedeckt
                        ist, schleifen Sie den Mikrozementboden. </p>
                    <h3>5. Zwei Schichten Feinstzement</h3>
                    <p>Der fünfte Schritt besteht aus dem Auftragen von <strong>zwei Schichten Feinstzement</strong>
                        oder Pigmentfinish. Je nach Ausführung und gewünschtem Grad der Glätte können Sie mittleren oder
                        dicken Mikrozement verwenden. Wir als Spezialisten empfehlen zwei Schichten
                        <strong>Microdeck</strong> , unseren mittleren Mikrozement optimal für die Anwendung von
                        Mikrozement auf Böden. </p>
                    <h3>6. Schutz microcemento Boden </h3>
                    <p>Der letzte Schritt der Anwendung von microcemento Boden bildet eine der wichtigsten. Der
                        Mikrozementboden ist fertig, jetzt muss er geschützt werden. Dazu werden <strong>zwei Anstriche
                            <a class="links"
                                href="https://www.topciment.com/de/mikrozement/topselaer-lack#presealer">PRESEALER</a></strong>
                        mit der Rolle aufgetragen und anschließend ein weiterer <strong>zwei Anstriche mit <a
                                class="links"
                                href="https://www.topciment.com/de/mikrozement/topselaer-lack">TOPSEALER
                                WT</a></strong>. Zusätzlich zum Schutz des Mikrozements des Fußbodens erhalten Sie eine
                        Abdichtung und sogar eine rutschfeste Oberfläche. </p>
                    <p> <a class="links" href="../mikrozement/anwendungsmethoden-mikrozement">Auf dieser Seite</a>
                        können Sie sich auch über die Anwendung von Mikrozement an Wänden und Schwimmbecken informieren.
                    </p>

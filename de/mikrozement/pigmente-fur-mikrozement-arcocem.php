                            <h1 class="en2">Pigmente und Farben microcemento | Arcocem
</h1>
                      
<p>
    Arcocem ist die exklusive Linie der Mikrozementpigmente und -farben von Topciment. Eine breite Palette von Pigmentpasten, die aus mehr als 30 Farbtönen besteht, mit denen Sie unbegrenzte dekorative Oberflächen in Innen- und Außenräumen erzielen können.

</p>    
<p>
    Innerhalb dieser Reihe von hochkonzentrierten Flüssigfarbstoffen, die während der Zubereitung des Mikrozements zugegeben werden, gibt es zwei Linien: Arcocem Basic und Arcocem Plus. Beide sind mit allen Arten von Oberflächen kompatibel: Wände, Böden, Terrassen... Mit Arcocem ist jede Kombination möglich, um Räume zu renovieren.

</p>    
<p>
    Einerseits haben wir die Arcocem Basic Mikrozementfarben: blanco, negro, azul, rojo anaranjado, verde und amarillo. Diese Farben dienen als Basis für das breite Spektrum an Mikrozementpigmenten, die Arcocem Plus bilden. Auf der anderen Seite gibt es Arcocem Plus. Dies sind die Einzeldosis-Pigmentpasten, aus denen der Rest der Mikrozementfarben in der breiten Farbpalette von Topciment hergestellt wird. Darunter befindet sich eine satte Graustufe, eine der von Profis am meisten gefragten Farben.
       

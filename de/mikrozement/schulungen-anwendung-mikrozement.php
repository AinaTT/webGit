                <h2 class="mt-5">Preis</h2>
                <p>Der Preis der Topciment-Schulungen hängt von mehreren Faktoren ab, wie z. B. dem
                    <strong>Spezialisierungsgrad</strong>. </p>
                <p>Die Kosten sind im Einsteigerkurs, in dem lediglich eine Einführung in die kontinuierliche
                    Verkleidung gegeben wird, niedriger und in den Fortgeschrittenenkursen höher. </p>

                <p> <strong>Die Kosten für das Material,</strong> das Topciment jedem Fachmann für den Abschluss des

                    <h2>Comment faire un comptoir en béton ciré ?</h2>

                    <p>
                        Le processus d'application d'un comptoir en béton ciré  dépendra du type de finition à poursuivre
                        et du matériau utilisé comme base de la surface, qu'il s'agisse d'un revêtement absorbant tel
                        que des plaques de plâtre, du plâtre, du plâtre ciment-sable ou du DM, ou si en raison de
                        l'opposé est un revêtement non absorbant tel que le marbre, le terrazzo, le carrelage ou le
                        granit.

                    </p>
                    <p>
                        Nous détaillons l'application du béton ciré  sur le comptoir, étape par étape, en fonction du
                        résultat souhaité:

                    </p> 
                    <h3>Placez le treillis sur le futur comptoir en béton ciré .</h3>

                    <p>
                        La première étape consiste à placer le treillis sur la surface qui servira de base au comptoir
                        en béton ciré : marbre, granit, plaque de plâtre ... C'est super important car cela évitera
                        l'apparition de fissures avec le temps.

                    </p>
                    <h3>Apprêt de comptoir en béton ciré  </h3>

                    <p>
                        La deuxième étape de l'application du béton ciré  sur un comptoir consiste en l'apprêt, qui
                        facilitera la liaison du revêtement absorbant ou non absorbant existant avec le nouveau comptoir
                        en béton ciré . Pour le premier cas, le promoteur d'adhérence PRIMACEM ABS sera appliqué et, pour
                        le second, l'apprêt PRIMACEM PLUS.

                    </p>
                    <h3>3. Appliquez deux couches de béton ciré  Microbase sur le comptoir</h3>

                    <p>
                        Ensuite, deux couches de béton ciré  Microbase doivent être appliquées. La première couche devra
                        être sans pigment et, après ponçage, ce sera au tour d'une deuxième couche de Microbase déjà
                        pigmentée selon la couleur de béton ciré  choisie pour le comptoir. Ensuite, il sera nécessaire
                        de poncer à nouveau. Si vous recherchez une finition en pierre ou rustique, après les deux
                        couches de béton ciré  Microbase, il doit être scellé pour protéger le comptoir de béton ciré 
                        d'abord avec le vernis acrylique Presealer, puis avec le vernis polyuréthane Topsealer.

                    </p>
                    <h3>4. Appliquez deux couches de béton ciré  Microfino sur le plan de travail</h3>

                    <p>
                        Si à la place vous voulez que le comptoir en béton ciré  ait un aspect marqué d'eau ou de stuc,
                        après les deux couches de béton ciré  Microbase, ce sera au tour de deux couches de béton ciré 
                        Microfino, toutes deux pigmentées. Un ponçage sera nécessaire après chaque couche, sans
                        exception. Une fois les deux couches de Microfino appliquées, le support sera d'abord protégé
                        avec Presealer et plus tard avec Topsealer.

                    </p>
                    <h3>5. Appliquez deux couches de béton ciré  Microdeck sur le comptoir</h3>

                    <p>
                        Si vous préférez que le comptoir en béton ciré  se démarque par son aspect naturel, après les
                        deux couches de Microbase, il est recommandé d'appliquer deux couches de béton ciré  Microdeck
                        pigmenté, ponçage en dernier. Pour finir, comme dans les deux applications précédentes, le
                        vernis Presealer et le scellant à béton ciré  Topsealer devront être appliqués sur le support.

                    </p>

                    <h3>6. Protégez et scellez le comptoir en béton ciré  </h3>

                    <p>
                        Une fois les deux couches de Microbase, Microfino ou Microdeck appliquées, selon la finition
                        souhaitée, le comptoir en béton ciré  doit être protégé pour éviter les problèmes. Pour sceller
                        correctement les meubles, deux mains de Presealer et deux autres de Topsealer sont nécessaires,
                        comme expliqué précédemment.

                    </p>  <!--  -->

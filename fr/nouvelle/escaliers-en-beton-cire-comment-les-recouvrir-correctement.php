                            va dans le prolongement du sol également recouvert de béton ciré Topciment.
                        </figcaption>
                    </figure>
                    <h2>Types d'escaliers en béton ciré</h2>

                    <p>
                        Les possibilités de conception que le béton ciré permet pour le revêtement des escaliers sont
                        illimitées. Dans la catégorisation que nous allons faire, nous avons pris en compte deux
                        facteurs. Tout d'abord, si les marches de l'escalier en béton ciré sont entre les murs ou non et
                        le type de contremarches de celui-ci.

                    </p>
                    <p>
                        Avec cette différenciation, nous trouvons les escaliers en béton ciré suivants: escaliers en
                        béton ciré fermés avec deux murs, escaliers en béton ciré fermés avec un mur, escaliers en béton
                        ciré fermés sans murs, escaliers en béton ciré ouverts avec deux murs, escaliers en béton ciré
                        ouverts avec un mur, escaliers en béton ciré ouverts sans murs , escaliers en béton ciré
                        encastrés dans un mur, escaliers en béton ciré encastrés sans murs et escaliers en béton ciré
                        avec support central.

                    </p>

                    <h2>Quel bétonciré et quelle couleur utiliser pour les escaliers</h2>

                    <p>
                        Bien que le gris soit la couleur la plus demandée pour recouvrir les escaliers de béton ciré,
                        toutes sortes de couleurs peuvent être appliquées pour fournir un élément de différenciation à
                        la décoration de votre maison ou de votre entreprise. Chez Topciment, nous avons un large
                        catalogue de couleurs de béton ciré pour escaliers. Des couleurs neutres pour un design plus
                        minimaliste et industriel aux couleurs plus vives si vous recherchez une décoration plus
                        risquée.

                    </p>
                    <p>
                        Concernant le type de béton ciré, nos experts recommandent le béton ciré de finition Microdeck
                        pour le revêtement des escaliers. Chez Topciment, nous avons développé trois types de Microdeck:
                        Standard Microdeck (béton ciré traditionnel à deux composants), Unlimitted Microdeck (béton ciré
                        à deux composants qui améliore la maniabilité) et Evoluttion Microdeck (béton ciré à un
                        composant).
                    </p>
                    <h2>Comment faire des escaliers en béton ciré:</h2>

                    <p>
                        La première chose à garder à l'esprit lors de l'application de béton ciré sur un escalier est
                        que ce processus ne peut être effectué que par un professionnel expérimenté. Étant donné que le
                        revêtement d'un escalier avec du béton ciré nécessite une technique d'application avancée pour
                        obtenir le résultat souhaité. Une série de particularités qu'il faut prendre en compte et que
                        seul un applicateur formé pourra garantir.

                    </p>
                    <h3>Appliquer le béton ciré sur les escaliers en 8 étapes</h3>

                    <p>
                        Prenez note des 8 étapes à suivre dans les escaliers recouverts de béton ciré.

                    </p>
                    <h4>Évaluer l'état du support</h4>

                    <p>
                        L'état du support existant sur l'escalier doit être analysé avant l'application du béton ciré.
                        Car si le support qui va constituer la base de l'échelle de béton ciré n'est pas dans un état
                        optimal, toutes sortes de fissures ou de défauts pourraient finir par apparaître. C'est pourquoi
                        il est si important d'avoir un bon professionnel, qui saura évaluer correctement la qualité du
                        support.

                    </p>
                    <h4> 2. Amorce de l'échelle de béton ciré</h4>

                    <p>
                        Une fois que nous nous sommes assurés que le support est en bon état, la deuxième étape pour
                        réaliser une échelle de béton ciré consiste à placer l'apprêt ou le promoteur d'adhérence.

                    </p>
                    <h4>3. Nivelez les marches de l'échelle de béton ciré</h4>

                    <p>
                        La troisième étape de la fabrication d'une échelle en béton ciré consiste à niveler et à
                        protéger les marches. De cette manière, les futures ruptures et usures sont évitées en éliminant
                        toutes les irrégularités pouvant exister.

                    </p>
                    <h4>4. Appliquer la première couche de béton ciré Microbase sur l'escalier</h4>

                    <p>
                        La quatrième étape d'application du béton ciré dans les escaliers consiste à appliquer une
                        première couche de Microbase sans pigment. Une fois terminé, il serait poncé.

                    </p>

                    <figure class="figure mt-4">
                        <img src="https://www.topciment.com/imagenes/news/escalera-microcemento-interior.jpg"
                            class="figure-img img-fluid " title="escalier intérieur en béton ciré avec support central" alt="escalier intérieur en béton ciré avec support central " width="auto" height="auto">
                             <figcaption class="figure-caption">Escalier intérieur en béton ciré avec support central.
                        </figcaption>
                    </figure>
                    <h4>5. Appliquer une deuxième couche de béton ciré Microbase sur l'escalier</h4>

                    <p>
                        La cinquième étape d'application dans les escaliers en béton ciré consiste à appliquer une
                        deuxième couche de Microbase, dans ce cas avec du pigment. La procédure et le ponçage de l'étape
                        précédente doivent être répétés.

                    </p>
                    <h4>6. Appliquer la première couche de béton ciré Microdeck sur l'escalier</h4>

                    <p>
                        La sixième étape de l'application du béton ciré sur les escaliers est une première couche mince
                        de Microdeck avec de la couleur. Une fois la couche sèche, la surface devra être poncée.

                    </p>
                    <h4>7. Appliquer une deuxième couche de béton ciré Microdeck sur l'escalier</h4>

                    <p>
                        La sixième étape de la fabrication d'escaliers en béton ciré consiste à appliquer la deuxième
                        couche de Microdeck. Dès qu'il sèche, il sera nécessaire de poncer la surface.

                    </p>
                    <h4>8. Sceller et protéger l'échelle de microciment</h4>

                    <p>
                        La dernière étape de l'application du béton ciré dans les escaliers est l'une des plus
                        importantes, car il s'agit de protéger le bardage et de s'assurer que son état optimal se
                        prolonge dans le temps.

                    </p>
                    <p>
                        Pour ce faire, chez Topciment nous vous recommandons d'appliquer deux couches de
                        <strong> <a class="links" href="../beton-cire/vernis-polyurethane-acrylique-aleau-topsealer#presealer"> vernis acrylique Presealer</a> </strong> et deux autres couches d'un de nos vernis polyuréthane, en particulier <strong><a class="links"  href="../beton-cire/vernis-polyurethane-acrylique-aleau-topsealer#topsealer-one-coat">Topsealer WT One Coat</a></strong>
                    </p>

                    <h2>3 conseils pour appliquer du béton ciré dans les escaliers</h2>
                         
                    <p>
                        Comme mentionné précédemment, tout le monde n'est pas formé pour fabriquer des escaliers en
                        béton ciré, mais uniquement des applicateurs professionnels avec des années d'expérience en
                        raison de la complexité impliquée. 
                    </p>
                    <p>
                        Voici les trois principaux conseils à garder à l'esprit lors de la réalisation d'escaliers en
                        béton ciré: 
                    </p>
                    <figure class="figure mt-4">
                        <img src="https://www.topciment.com/imagenes/news/escalera-microcemento-exterior-acero.jpg"
                            class="figure-img img-fluid " title="escalier extérieur en béton ciré Acier"
                            alt=" Escalier extérieur en béton ciré couleur Acier " width="auto" height="auto">
                        <figcaption class="figure-caption">Escalier en béton ciré couleur Acier par Topciment à
                            l'extérieur d'une maison.</figcaption>
                    </figure>
                    <h3>1. Protéger les bords de l'escalier en béton ciré avec un profilé en aluminium</h3>

                    <p>
                        Les coins ou les bords des marches des escaliers en béton ciré sont, sans aucun doute, la zone
                        la plus exposée à toutes sortes de rayures et de bosses. Pour cette raison, en tant que
                        fabricants, nous recommandons d'appliquer des pièces d'angle ou des<strong> <a class="links"
                                href="../beton-cire/outils-beton-cire#perfil">profilés en aluminium </a></strong>.
                        Compléments qui peuvent être cachés de la vue (même si nous recommandons de les
                        laisser visibles car ils apportent une touche d'élégance) et qui protégeront mieux et plus
                        longtemps les escaliers en microciment, évitant ainsi la casse ou l'usure.

                    </p>
                    <h3>2. Bords d'escalier en béton ciré minimalement arrondis</h3>

                    <p>
                        Bien que la marche de l'échelle de béton ciré puisse être laissée à 90 degrés, il est recommandé
                        que les bords soient arrondis même légèrement. En suivant ces conseils et en "coupant" les bords
                        du futur escalier en béton ciré, on évite les éclats ou les bosses puisque l'impact sur
                        l'escalier en béton ciré est moindre.

                    </p>
                    <h3>3. Maille de fibre oui ou oui dans une échelle de béton ciré</h3>

                    <p>
                        Bien que certains applicateurs ne placent pas toujours un filet de fibrium sur la couche de base
                        du béton ciré, chez Topciment nous sommes en faveur de le faire toujours. Grâce au treillis,
                        nous avons pu renforcer l'escalier en béton ciré.

                    </p>

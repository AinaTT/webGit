                    <h2>Rénover de vieux carreaux avec du béton ciré: application et astuces</h2>

                    <p>
                        Pour placer du béton ciré sur des carreaux, il est essentiel d'avoir un applicateur
                        professionnel et qualifié qui fait du bon travail, comme les installateurs de béton ciré qui
                        travaillent avec nos bétons cirés.

                    </p>
                    <p>
                        Ce n'est qu'alors que vous n'aurez pas à regretter certains des
                        <strong> <a class="links"
                                href="../beton-cire/comment-eviter-les-taches-ou-marques-indesirables-en-appliquant-le-beton-cire">
                                problèmes les plus fréquents</a></strong>
                        qui sont la conséquence d'erreurs dans l'application du béton ciré.

                    </p>
                    <h3>8 étapes pour appliquer le béton ciré sur les carreaux</h3>

                    <h4>1. Évaluer l'état des carreaux</h4>

                    <p>
                        La première chose à faire avant d'appliquer le béton ciré directement sur les vieux carreaux est
                        de vérifier l'état des carreaux. Puisque si les carreaux de sol ou de mur présentent des
                        imperfections, ils ne peuvent pas être recouverts de béton ciré comme si rien d'autre. Le
                        substrat devra être réparé avant de placer notre béton ciré lissé. D'où l'importance de la
                        rénovation effectuée par un professionnel, car il saura localiser ces failles.

                    </p>
                    <h4>2. Nettoyez les carreaux avant d'appliquer le béton ciré</h4>

                    <p>
                        Après avoir réparé les carreaux, si nécessaire, les carreaux doivent être soigneusement nettoyés
                        avant d'appliquer le béton ciré lissé afin qu'il n'y ait ni graisse ni poussière.

                    </p>
                    <h4>3. Remplissez les joints des carreaux de béton ciré</h4>

                    <p>
                        Une fois que le mur ou le sol sur lequel sont posés les carreaux sont propres, il est temps de
                        passer aux choses sérieuses. La toute première étape de l'application du béton ciré sur les
                        carreaux consiste à remplir les joints. Étant donné que le béton ciré lissé est un revêtement
                        continu et, par conséquent, si les joints de carreaux ne sont pas remplis, le support qui
                        servira de base ne sera pas stable et il y aura des irrégularités qui marquent les joints.

                    </p>
                    <h4>4. Apprêter les carreaux à recouvrir de béton ciré</h4>

                    <p>
                        Lorsque la surface est de niveau et que les joints des carreaux sont parfaitement remplis, ce
                        sera au tour d'appliquer le primaire ou le promoteur d'adhérence correspondant. Un produit
                        fondamental puisqu'il sert de pont entre les dalles et le béton ciré, pour que ce dernier adhère
                        mieux.

                    </p>
                    <p>
                        Chez Topciment, nous avons fabriqué une large gamme d'apprêts ( primaire) en fonction de la
                        surface sur laquelle le béton ciré est appliqué. Dans ce cas, qui concerne les carreaux, nos
                        experts conseillent d'utiliser l'un de ces deux: Primacem Impoxy A + B ou Primacem Plus. Nous
                        allons développer les deux pour que vous sachiez lequel utiliser à chaque fois.

                    </p>
                    <p>
                        <strong> <a class="links" href="../beton-cire/promoteurs-dadherence-primacem#primacem-impoxy">
                                Primacem Impoxy A + B </a></strong>est notre apprêt époxy anti-humidité qui bloque
                        l'humidité
                        par capillarité et agit comme un pare-vapeur. Un promoteur d'adhérence à deux composants, 100%
                        solide, parfait pour les carreaux de mur de douche ou de sol de salle de bain. Un produit
                        innovant qui évite également de tracer les joints dans le béton ciré.

                    </p>
                    <p>
                        D'autre part, vous pouvez également utiliser<strong> <a class="links"
                                href="../beton-cire/promoteurs-dadherence-primacem#primacem-plus">Primacem Plus
                            </a></strong>, notre apprêt
                        spécifique pour les surfaces non absorbantes comme les carreaux et qui s'applique en une seule
                        couche.

                    </p>
                    <figure class="figure mt-4">
                        <img src="https://www.topciment.com/imagenes/news/azulejos-microcemento-calabaza-pared-banyo.jpg"
                            class="figure-img img-fluid "
                            title="Carreaux de salle de bain en microciment, couleur citrouille"
                            alt="Carreaux de salle de bain en microciment, couleur citrouille" width="auto"
                            height="auto">
                        <figcaption class="figure-caption">L'avant et l'après d'une salle de bain dans laquelle nous
                            avons utilisé notre béton ciré couleur citrouille pour recouvrir les carreaux du mur de la
                            salle de bain.
                        </figcaption>
                    </figure>
                    <h4>5. Placer un treillis en fibre dans la première couche de béton ciré sur les carreaux</h4>

                    <p>
                        Après avoir quitté les heures de séchage appropriées (voir fiches techniques), nous placerons la
                        maille de fibre de verre sur les carreaux avant d'appliquer le béton ciré pour protéger le futur
                        revêtement et éviter les fissures, fissures, etc. Une étape que tout le monde ne prend pas en
                        compte et qui devient plus tard un casse-tête majeur.

                    </p>
                    <p>
                        Ensuite, la première couche de base de béton ciré (Microbase) sans pigment sera appliquée sur
                        les carreaux. Une fois les carreaux bien recouverts par le béton ciré, sans irrégularités, le
                        support sera poncé.

                    </p>
                    <h4>6. Appliquer une deuxième couche de base de béton ciré sur les carreaux</h4>

                    <p>
                        Appliquer la deuxième couche de base de béton ciré(Microbase). A cette occasion, il devra être
                        pigmenté avec la couleur de béton ciré qui a été choisie pour recouvrir les anciens carreaux. De
                        la même manière qu'à l'étape précédente, la surface devra être poncée.

                    </p>
                    <h4>7. Appliquer deux couches de béton ciré Microdeck ou Microfino sur les carreaux</h4>

                    <p>
                        Après le béton ciré de base, deux couches de béton ciré Microdeck ou deux couches de Microfino
                        doivent être appliquées sur les carreaux. Le premier est généralement utilisé pour les sols et
                        le second pour les murs. Bien qu'en fonction des goûts, des préférences et des finitions
                        recherchées, des combinaisons peuvent être faites. Les deux mains seront déjà pigmentées. Puis
                        poncez.

                    </p>
                    <h4>8. Protéger et sceller le béton ciré sur les carreaux</h4>

                    <p>
                        La dernière étape est l'une des plus importantes. Il s'agit de protéger le revêtement de béton
                        ciré sur les carreaux. Pour cela, nous avons toutes sortes de vernis et de scellants. Bien qu'à
                        cette occasion, nous vous recommandons d'utiliser deux couches de<strong> <a class="links"
                                href="../beton-cire/vernis-polyurethane-acrylique-aleau-topsealer#presealer">Presealer</a> </strong> et
                        deux autres couches de <strong><a class="links"
                                href="../beton-cire/vernis-polyurethane-acrylique-aleau-topsealer#topsealer-one-coat">Topsealer WT One Coat
                            </a></strong>.

                    </p>
                    <figure class="figure mt-4">
                        <img src="https://www.topciment.com/imagenes/news/azulejos-microcemento-piedraparis-suelo-pared-cocina.jpg"
                            class="figure-img img-fluid " title="Cuisine avec carreaux de sol et de mur en microcemento color Piedra Paris" alt="Cuisine avec carreaux de sol et de mur recouverts de microcemento color Piedra Paris"
                            width="auto" height="auto">
                        <figcaption class="figure-caption">Rénovation impressionnante d'une cuisine après l'utilisation de la couleur béton ciré Piedra Paris Topciment sur les carreaux de sol et de mur.
                        </figcaption>
                    </figure>
                    <h3>Conseils et recommandations pour rêvetir les carreaux avec du béton ciré</h3>

                    <h4>1. Respectez les temps de séchage </h4>

                    <p>
                        Dans le cas de l'utilisation d'un apprêt pour surfaces non absorbantes comme Primacem PLUS, il
                        est très important que lorsque les carreaux sont enduits de béton ciré, ils respectent le temps
                        de séchage. Si les indications et les temps de séchage spécifiés dans les fiches techniques ne
                        sont pas strictement respectés, l'humidité restera emprisonnée entre les mains et donc les
                        joints des carreaux seront marqués.

                    </p>
                    <p>
                        Avec ces conseils, vous éviterez de produire l'échec le plus courant lors de l'application de
                        béton ciré sur des carreaux. Une erreur possible qui peut être évitée lors de l'utilisation de
                        l'apprêt Primacem Impoxy A + B, pour lequel il n'est pas nécessaire d'être si méticuleux avec
                        les heures de séchage en raison de sa capacité à bloquer l'humidité par capillarité.

                    </p>
                    <h4>2. Laisser sécher 24 heures avant d'appliquer la deuxième couche de béton ciré</h4>

                    <p>
                        Une autre recommandation des experts de Topciment, dans le cas de l'utilisation de Primacem PLUS
                        également, est de laisser une période de séchage de 24 heures entre la première et la deuxième
                        couche de base de béton ciré.

                    </p>
                    <p>
                        Un procédé que tout le monde ne fait pas puisqu'il n'est pas obligatoire, mais avec lequel le
                        professionnel s'assure qu'il y a beaucoup moins de probabilité que les joints soient marqués en
                        raison d'une erreur dans l'application du béton ciré sur les carreaux. Avec Primacem Impoxy A +
                        B, cela se fera avec le système normal. Autrement dit, appliquer la deuxième couche de Microbase
                        immédiatement après la première.

                    </p>
                    <h4>3. Apprêter les carreaux avant d'appliquer la deuxième couche de béton ciré</h4>

                    <p>
                        Nous recommandons également que 24 heures après avoir appliqué la première couche de béton ciré,
                        soit plus de temps qu'il ne faut pour qu'elle sèche au toucher, le béton ciré qui recouvre les
                        carreaux soit apprêté avec Primacem PLUS.

                    </p>


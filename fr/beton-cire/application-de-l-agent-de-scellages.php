                        de la dernière couche de béton ciré. En plus de faciliter le travail de nettoyage,
                        le scellage rendra la surface imperméable.<br>
                    </p>
                    <p style="text-align:left; ;" data-animate-scroll='{"x": "-100", "y": "0", "alpha": "0", "duration": "1"}'>
                        L'utilisation du primer Presealer est recommandée avant le scellage avec Topsealer
                        WT, le premier pénètre dans le support et sèche pour former un film plastique
                        recouvrant tous les pores, évitant ainsi une consommation élevée de scellant. Il est
                        appliqué en deux couches avec un intervalle de 4 heures. Pour éviter des problèmes
                        d'adhérence avec la couche protectrice et l'apparition de glaçures et de bulles, la
                        teneur en humidité du revêtement ne doit pas dépasser 5%.<br>
                    </p>
                    <p style="text-align:left; ;" data-animate-scroll='{"x": "-100", "y": "0", "alpha": "0", "duration": "1"}'>
                        4 heures sont nécessaires pour le séchage, avant l'application de la première couche
                        de polyuréthane Topsealer WT sur le primer. Appliquer en deux couches avec un
                        rouleau microfibre à poils courts. La deuxième couche est appliquée après un minimum
                        de 12 heures si la couche Topsealer WT One est utilisée. Si Topsealer WT Quick Dry
                        est utilisé (les basses températures et l'humidité ambiante retardent le séchage),
                        il peut être appliqué après seulement 4 heures. La première couche est poncée avec
                        du papier de verre grain 400, la couche finale ne nécessite aucun ponçage. Si elle
                        est appliquée sur un vieux vernis, la surface doit être soigneusement poncée pour
                        ouvrir les pores et éliminer toute trace de taches, de graisse, de poussière, etc.
                        Laissez la surface sécher pendant sept jours et deux semaines sans utiliser de
                        produits de nettoyage et sans couvrir.<br>

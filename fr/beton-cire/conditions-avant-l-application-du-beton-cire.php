                    <div id="mitadTextoIzquierda">
                        <h2>Conditions préalables à l’application </h2>
                        <h3>Nettoyage.</h3>
                        <p>Le support doit être propre, exempt de poussière et de graisses.</p>
                        <h3>Planéité.</h3>
                        <p>Le <a href="../information-beton-cire">béton ciré </a> est appliqué par couches non supérieures
                            à 1 mm, et leur somme ne dépassera jamais quatre millimètres, c’est pourquoi nous ne pourrons pas
                            résoudre des questions de planéité avec ce matériaux ; nous devrons arranger les irrégularités du
                            support avant d’initier l’application. Le béton ciré  peut être appliqué sur des planchers, murs et
                            même des plafonds, le résultat final dépendra du support et du travail de l’applicateur.</p>
                        <h3>Consistance.</h3>
                        <p>La base d’application doit être stable. Les supports avec des mortiers pulvérulents ou des
                            carrelages détachés ne sont pas appropriés, en fonction de leur état il faudra les retirer ou els
                            consolider avec des mortiers à prise rapide, à rétraction compensée. </p>
                        <h3>Sans humidité.</h3>
                        <p>Nous ne pourrons pas appliquer le béton ciré  s’il y a de l’humidité sur le support. Il faudra
                            déterminer d’abord son origine et l’éliminer, nous recommandons d’utiliser un mesureur d’humidité
                            afin de vérifier que l’humidité ne dépasse jamais 5 %.</p>
                        <p>Le support devra avoir subi un processus de prise suffisant. Nous ne devons pas oublier que la
                            température et l’humidité relative de l’ambiance avec l’aération de l’espace de travail ont un effet
                            sur le processus de séchage des mortiers et accélère ou retarde le processus de prise.</p>
                        <h3>Protection de la zone de travail</h3>
                        <p>Nous devons protéger la zone de travail de manière appropriée et les surfaces que nous ne
                            revêtirons pas, le béton ciré  adhère très fortement et il est difficile à nettoyer quand il sèche.
                            Nous emploierons un matériel de protection commun pour les travaux de peinture, comme des rubans de
                            peintre, papier pour protéger, plastiques couvre-tout... </p>
                        <h3>Support recommandé dans le système de piscines</h3>
                        <p>L’application est réalisée sur du béton projeté ou gunité, avec le support propre, consolidé et en
                            bon état de planimétrie. <strong>L’application ne doit pas être faite sur de la pâte de verre.
                            </strong> </p>
                    </div>
                    <div id="mitadTextoDerecha">
                        <h2>Préparation du support</h2>
                        <p>L’application du béton ciré  commencera par poser l’apprêt sur le support en utilisant des
                            promoteurs d’adhérence et avec l’installation de la maille de fibre de verre. </p>
                        <h3>Apprêt avec des promoteurs d’adhérence</h3>
                        <p>Les promoteurs d’adhérence ont plusieurs fonctions ; ils garantissent une meilleure adhérence sur
                            n’importe quel type de support, ils augmentent sa cohésion, ils améliorent la vitesse d’application
                            de la première couche du béton ciré , et ils évitent la perte prématurée d’humidité du micro-mortier.
                        </p>
                        <p>Les supports ont différentes capacités d’absorption, par conséquent nous devons employer des
                            solutions pour chacun d’entre eux, promoteurs pour surfaces non absorbantes, pour surfaces
                            absorbantes, pour surfaces qui ont besoin d’un certain degré de consolidation ou promoteurs pour
                            surfaces qui peuvent présenter des problèmes d’humidité. </p>
                        <h3>béton ciré  sur du grès, granito, dalle hydraulique ou marbre</h3>
                        <p>Pour revêtir ce type de supports peu ou presque pas du tout poreux, nous devons utiliser Primacem
                            Plus, promoteur pour surfaces non absorbantes ; le processus sera le suivant :</p>
                        <ul>
                            <li>Remplir les joints avec Microbase et laisser sécher 24 h. Si les joints sont très fins, nous
                                pouvons employer Microdeck ou même Microfino. <br>
                            </li>
                            <li>Appliquer une couche de Primacem PLUS au moyen d’un rouleau avec la maille Builtex. <br>
                            </li>
                            <li>Laisser sécher au moins 30 minutes et appliquer la première couche de Microbase. </li>
                            <li>Laisser sécher 24 h, passer une couche de Primacem PLUS et appliquer la seconde couche de
                                Microbase.</li>
                        </ul>
                        <h2>Nous voyons ici un système d’application différent avec Impoxi</h2>
                        <h3>béton ciré  sur plâtre, placoplâtre, panneaux de DM</h3>
                        <p>Sur des supports absorbants, nous utiliserons Primacem Abs, en suivant ces pas :</p>
                        <ul>
                            <li> Appliquer l’apprêt avec un rouleau commun de peintre.<br>
                            </li>
                            <li> Laisser agir le pont d’union au moins 30 minutes et 24 heures maximum avant de placer la maille
                                avec la première couche de béton ciré .</li>
                        </ul>
                        <h3>béton ciré  sur mortier de ciment, béton projeté ou gunité.</h3>
                        <p>Pour des surfaces absorbantes qui peuvent exiger un certain degré de consolidation, nous devons
                            procéder de la manière suivante :</p>
                        <ul>
                            <li> Appliquer l’apprêt Acricem avec un rouleau commun de peintre.<br>
                            </li>
                            <li> Laisser agir le pont d’union au moins 30 minutes et 24 heures maximum avant de placer la maille
                                avec la première couche de béton ciré . </li>
                        </ul>
                        <h3>béton ciré  sur des supports qui peuvent présenter de l’humidité</h3>
                        <p>Sur des supports consolidés sur lesquels peuvent apparaître de l’humidité, nous devrons appliquer
                            une barrière à base de résines époxy pour éviter que l’humidité affecte le <a href="../information-beton-cire">béton ciré </a>. Impoxy résout les problèmes d’humidité en
                            permettant l’application sur le support de revêtements de béton ciré ..</p>
                        <ul>
                            <li> Apprêt au rouleau avec Impoxy à 0,35 kg /m²<br>
                            </li>
                            <li> Saupoudrer du sable de silicium sec de 0,3-0,8 mm à 1 kg /m²<br>
                            </li>
                            <li> Laisser sécher 24 heures</li>
                            <li> Retirer le sable de silicium en trop en balayant et aspirant.<br>
                            </li>
                            <li> Application d’Impoxy avec une truelle à 3 kg /m²</li>
                        </ul>
                        <h3>Installation de la maille</h3>
                        <p>Le béton ciré  Topciment ne se fissure pas, tant que la base sur laquelle il est placé ne le fasse
                            pas. La maille Topciment est une assurance supplémentaire dans les applications de béton ciré , c’est
                            une maille de grandes prestations, flexible, résistante et très facile à travailler, elle est
                            recouverte facilement avec Microbase, sans créer de poches. La maille permet de désolidariser le
                            revêtement du support d’application.</p>
                        <p>Fixation de la maille :</p>
                        <p>La maille peut être fixée avec le promoteur d’adhérence Primacem Plus, ou quand est utilisé comme
                            promoteur Primacem ABS ou la résine Acricem, avec la première couche de béton ciré  Base. Il faudra
                            superposer les bandes de maille un petit peu et nous appliquerons toujours deux couches de Microbase
                            pour couvrir complètement le treillis.</p>

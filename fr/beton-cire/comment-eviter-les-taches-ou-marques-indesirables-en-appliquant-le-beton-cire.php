                                <h2>Marques de joint de carrelage lors de l'application du béton ciré</h2>
                                <p>
                                    Voulez-vous appliquer le béton ciré  sur des carreaux? Ce sont les trois points à
                                    prendre en compte pour éviter de marquer les joints des carreaux.

                                </p>
                                <h3>1. Faites attention à la différence d'absorption entre les carreaux et les joints
                                </h3>
                                <p>
                                    La résine contenue dans le béton ciré est absorbée différemment. Si aucune
                                    précaution n'est prise, nous courons le risque de les voir apparaître sous la forme
                                    d'un spectre visuel lors du séchage (schéma visuel). L'humidité dans les
                                    articulations provoque également ce défaut.

                                </p>

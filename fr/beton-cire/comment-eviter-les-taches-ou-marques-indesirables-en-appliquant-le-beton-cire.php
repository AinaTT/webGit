                                        <div id="mitadTextoIzquierda" >
                                            <h2>Taches de l’excès de polissage</h2>
                                            <p>Quand nous effectuons le polissage de la dernière couche de béton ciré, nous devons nous assurer de travailler doucement, avec une main nous prendrons le polissoir ou el gant de polissage, et avec l’autre nous vérifierons au toucher si le polissage est correct. Si nous travaillons de manière correcte, nous éviterons trois types de taches caractéristiques : cercles, courbes de niveau et marques noires.</p>
                                            <p>Le polissoir devra être du type roto-orbital pour éviter de laisser des cercles. Nous essaierons de la maintenir en mouvement de forme constante sans rester de forme prolongée sur le même endroit. Si le polissage est excessif, nous verrions apparaître le béton ciré de la couche précédente et avec des « courbes de niveau » caractéristiques qui indiqueraient que nous avons atteint la couche inférieure. Dès que la meulette est usée, nous devrons la changer, sinon la meulette cesse de polir pour lustrer en laissant des marques noires sur le béton ciré.</p>
                                        </div>
                                        <div id="mitadTextoDerecha" >
                                            <h2>Excès de brunissage sur le béton ciré</h2>
                                            <p>Les retouches quand le béton ciré a déjà commencé à sécher laisseront des marques évidentes et peu esthétiques, le mieux quand la finition n'est pas celle souhaitée est d’appliquer une couche supplémentaire.</p>
                                            <p>Travailler en excès une zone avec la truelle laissera des marques foncées. Ceci peut se produire par exemple, quand du matériau s’accumule au sol à la fin du parcours de l’application, nous obligeant à ramasser le béton ciré en trop et à le travailler davantage avec la truelle.</p>
                                            <p>Si on travaille en excès une zone, on force le séchage. Ce problème se reflète sur le blanchissement de la zone. Par exemple : un mur noir pourrait avoir des taches grises beaucoup plus claires que la couleur choisie. </p>
                                            <p>Si l’applicateur travaille trop toute la surface ou exerce une pression trop forte, alors toute la surface aura une couleur plus claire. Il est très habituel de voir un mur noir qui semble gris.</p>

                                <div id="mitadTextoDerecha" >
                                    <h2>Marques de la truelle, rehaussant les irrégularités de la couche précédente</h2>
                                    <p>Nous devrons appliquer le béton ciré sans laisser d’ébarbures ni de reliefs qui mettent en évidence le passage de la truelle, en polissant ces imperfections lors de chaque couche. Si nous appliquons sur une couche avec des ébarbures, ces reliefs seront copiés. </p>
                                    <p>Avec la truelle en acier et un excès de pression, ces imperfections provoquent l’effet « brûlé », des taches foncées qui reflètent le manque de polissage de la couche inférieure.</p>
                                    <p>&nbsp;</p>
                                    <h2>Polissage des irrégularités</h2>
                                    <p>Nous devrons polir le béton ciré après chaque couche pour éviter l’effet décrit au volet précédent. Nous effectuerons un léger dégrossissage avec un polissoir roto-orbital à faible révolution ou avec un gant de polissage, et des <strong>meulettes de carbure de silicium. </strong> </p>
                                    <p> Environ 3 heures après l’application, nous observons un changement de ton du béton ciré qui devient plus clair, ce qui indique qu’on peut marcher sur le béton ciré et qu’il est suffisamment dur pour procéder au polissage, la température, l’humidité ambiante et l’aération de la pièce ont une influence sur ce délai de temps.</p>
                                    <p>Nous recommandons de polir dans les 12 heures après l’application pour que le revêtement n’ait pas encore atteint une dureté excessive et que ce pas soit plus compliqué.</p>

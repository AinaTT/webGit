                                <h2>Irrégularités de surface du béton ciré</h2>

                                <p>
                                    Après l'application de chaque couche de béton ciré, la surface doit être poncée. Une
                                    étape non négociable pour éviter l'effet décrit dans la section précédente. Nous
                                    réaliserons une ébauche lisse avec une ponceuse roto-orbitale à basse vitesse ou
                                    avec un gant de ponçage et du papier de verre au carbure de silicium.

                                </p>
                                <p>
                                    Environ 3 heures après l'application du béton ciré lissé, on observe un changement
                                    de tonalité du revêtement, devenant plus léger. Un changement de ton qui indique que
                                    le béton ciré est passable et suffisamment dur pour procéder au ponçage. Il est à
                                    noter que la température, l'humidité ambiante et la ventilation de la pièce
                                    influencent cette durée.

                                </p>
                                <p>
                                    Nous recommandons de poncer dans les 12 heures suivant l'application du béton ciré
                                    afin que le revêtement n'ait pas atteint une dureté excessive et cette étape est
                                    plus compliquée.

                                </p>
                                <h2>Taches de bétons cirés dues au ponçage excessif</h2>

                                <p>
                                    Lorsque nous ponçons la dernière couche de béton ciré, nous devons nous assurer de
                                    travailler le matériau en douceur. D'une main nous prendrons la ponceuse ou le gant
                                    de ponçage et de l'autre nous vérifierons au toucher si le ponçage est correct. Si
                                    nous travaillons correctement la surface, nous éviterons trois types de taches
                                    caractéristiques: les cercles, les lignes de contour et les marques noires.

                                </p>
                                <p>
                                    La ponceuse devra être de type roto-orbitale pour éviter de laisser des cercles.
                                    Nous essaierons de le maintenir constamment en mouvement sans affecter le même site
                                    pendant longtemps. Si le ponçage était excessif, nous finirions par voir le béton
                                    ciré lissé de la couche précédente et laisser des "lignes de contour"
                                    caractéristiques qui indiqueraient que nous sommes passés à la couche inférieure.
                                    Dès que le papier de verre s'use, nous devrons le changer, si nous ne faisons pas le
                                    changement, le papier de verre passe du ponçage au polissage, laissant des traces
                                    noires sur le béton ciré.

                                </p>
                                <h2>Marques de bétons cirés dues à un polissage excessif</h2>

                                <p>
                                    Si la finition en béton ciré n'est pas celle souhaitée, il est préférable
                                    d'appliquer une couche de béton ciré lissée supplémentaire. Puisque faire des
                                    retouches sur la surface déjà sèche laissera des marques visibles et très
                                    disgracieuses.

                                </p>
                                <p>
                                    Passer sur une zone avec la taloche laissera des marques sombres. Cela peut arriver,
                                    par exemple, lorsque le matériau s'accumule sur les sols à la fin du chemin
                                    d'application, nous obligeant à collecter l'excès de béton ciré lissé et à le
                                    travailler avec la taloche.

                                </p>
                                <p>
                                    Si vous dépassez une zone, vous devez la forcer à sécher. Ce problème se reflète
                                    dans le blanchiment de la zone. Par exemple: un mur noir peut avoir des taches
                                    grises beaucoup plus claires que la couleur de béton ciré choisie.

                                </p>
                                <p>
                                    Si l'applicateur passe trop sur toute la surface ou serre trop le support en béton
                                    ciré lissé, il peut s'avérer être une couleur plus claire. Il est très typique de
                                    voir un mur noir qui apparaît gris.

                                </p>






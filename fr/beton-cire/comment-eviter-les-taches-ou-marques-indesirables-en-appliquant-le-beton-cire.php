                                    <h2>Marques des joints des carrelages</h2>
                                    <p>Certains points doivent être considérés pour éviter de marquer les joints des carrelages :</p>
                                    <ul class="marginLeft50px">
                                        <li> Nous devons faire attention à la différence d’absorption entre les dalles et les joints : la résine contenue dans le béton ciré est absorbée de manière différente, si aucune précaution n’est prise, nous risquons qu’elles apparaissent en forme de spectre visuel en séchant (modèle visuel). L’humidité sur les joints provoque également ce défaut.<br>
                                        </li>
                                        <li>Les joints sont généralement creux, ils ne sont pas sur le même plan que le carrelage, la rétraction du béton ciré après séchage crée un relief sur la zone des joints (modèle en relief).</li>
                                    </ul>
                                    <p>C’est pourquoi, avec notre système, nous créons une couche qui isole et nivelle.</p>
                                    <img src="https://www.topciment.com/imagenes/marcas-de-las-juntas-de-los-azulejos-por-humedad.jpg" width="100%" alt="marcas de las juntas de los azulejos por humedad" title="marcas de las juntas de los azulejos por humedad">



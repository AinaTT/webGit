                                <h3>2. Les joints des carreaux sont généralement creux</h3>
                                <p>
                                    Les joints sont généralement creux, ils ne sont pas dans le même plan que le
                                    carreau. De même, la rétraction du béton ciré une fois sec crée un relief dans la
                                    zone articulaire (motif en relief). Pour tout cela, avec nos systèmes de béton ciré,
                                    nous créons une couche qui isole et nivelle.

                                </p>
                                <h3>3. Laisser sécher 24 heures avant d'appliquer le béton ciré</h3>
                                <p>
                                    Il est très important qu'après avoir rempli les joints de carreaux, on laisse sécher
                                    pendant 24 heures avant de commencer l'application de la base de béton ciré.

                                </p>
                                <h2>Marques de taloche sur le béton ciré</h2>

                                <p>
                                    Il va falloir appliquer le béton ciré sans laisser de bavures ni de reliefs qui
                                    montrent le passage de la taloche, en ponçant ces imperfections dans chaque main. Si
                                    nous appliquons du béton ciré sur une couche avec des bavures, ces reliefs seront
                                    copiés.

                                </p>
                                <p>
                                    Avec la taloche en acier et une pression excessive, ces imperfections provoquent
                                    l'effet "brûlé", des taches sombres qui reflètent l'absence de ponçage de la couche
                                    inférieure de béton ciré.

                                </p>

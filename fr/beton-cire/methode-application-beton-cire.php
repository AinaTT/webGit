                            <h2>Comment appliquer le béton ciré</h2>

                         <p>   1. Conditionnez la surface de travail, placez le treillis et appliquez l'apprêt ou le promoteur d'adhérence.</p>

                        <p>    2. Appliquer deux couches de préparation ou de béton ciré de base, sur un treillis, qui servira de support au béton ciré de finition.</p>

                        <p>    3. Ensuite, appliquez deux couches de béton ciré de finition. C'est celui qui combine la texture et la couleur désirées, donnant l'aspect final à la surface.</p>

                         <p>   4. Enfin, pour protéger le béton ciré, appliquez deux couches de Presealer  et deux autres de Topsealer .</p>


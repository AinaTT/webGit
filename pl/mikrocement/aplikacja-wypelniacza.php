jąc pierwszą warstwę. Drugiej warstwy nie szlifuje się, musi ona schnąć przez co najmniej siedem dni przed oddaniem powierzchni do użytku.</p--> <p style="text-align:left;"> Baseny wykonane przy użyciu Sttandard Aquaciment należy po prostu uszczelnić przy użyciu Presealer, nakładając dwie warstwy w odstępie 6 godzin, pokrywając całą powierzchnię całkowicie i pozostawiając pięć dni do wyschnięcia przed napełnieniem basenu. Unikać przepływu wody podczas procesu uszczelniania i wstrzymać aplikację, jeśli zapowiadany jest deszcz.</p> <p style="text-align:left;"> Produkty do pielęgnacji wody muszą być najpierw rozpuszczone w pojemniku i umieszczone w skimmerach, skąd będą wlewane do basenu. Produkty te nie powinny być wylewane bezpośrednio, ponieważ mogą powodować białe plamy i przebarwienia na dnie basenu. Zaleca się odnawianie ostatniej warstwy preparatu Presealer co dwa lata, uprzednio szlifując powierzchnię.




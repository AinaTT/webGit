                <div class="headline fadeInRight styleNormal16justified">
                    <p>Mikrocement jest <strong>jedną z najbardziej rozpowszechnionych nawierzchni ciągłych na
                            świecie</strong> ze względu na możliwości dekoracyjne, jakie oferuje zarówno wewnątrz, jak i
                        na zewnątrz budynków. Powłoka, która może być stosowana na podłogach kuchennych, łazienkowych,
                        tarasowych itp.</p>
                    <p>Materiał budowlany, który choć ma wieloletnią historię, w ostatnich latach stał się znacznie
                        bardziej <strong>popularny</strong> , szczególnie do pokrywania wszelkiego rodzaju podłóg.
                        Poniżej odkrywamy wszystko, co musisz wiedzieć o posadzkach z mikrocementu: jak się je stosuje,
                        ich główne zalety, rodzaje mikrocementu do podłóg, wskazówki... i wiele więcej. </p>
                    <h2>Korzyści z zastosowania posadzki mikrocementowej</h2>
                    <p>Zastosowanie posadzki mikrocementowej oferuje <strong>niezliczone korzyści. </strong> Oto
                        podsumowanie niektórych z najważniejszych korzyści, jakimi ta innowacyjna powłoka dekoracyjna
                        może się pochwalić jako podłoga:</p>
                    <ul>
                        <ul>
                            <li>Nie podnosi zbytnio poziomu podłogi dzięki minimalnej grubości wynoszącej od 2 do 3 mm.
                            </li>
                            <li>Jest to podłoga ciągła, co oznacza, że nie ma żadnych szczelin dylatacyjnych ani szwów.
                                Cała renowacja podłogi wykonywana jest w jednym kawałku.</li>
                            <li>Przylega do każdego rodzaju materiału bez konieczności usuwania istniejącego podparcia.
                            </li>
                            <li>Jest niezmienny na promienie UV i ma bardzo dobrą odporność termiczną.</li>
                            <li>Ma godną pozazdroszczenia odporność na ruch, zużycie ścierne i domowe środki czyszczące.
                            </li>
                            <li>Żadne dwie podłogi z mikrocementu nie są takie same dzięki różnorodności kolorów i

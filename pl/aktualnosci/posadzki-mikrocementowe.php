                                <h2>Jak zainstalować podłogę mikrocementową</h2>
                                <p>Jak już znasz wszystkie zalety podłogi mikrocementowej, nadszedł czas, aby wyjaśnić
                                    proces aplikacji. Oto <strong>sześć kroków</strong>, które należy wziąć pod uwagę,
                                    aby pomyślnie zainstalować podłogę z mikrocementu:</p>
                                <h3> 1. Oczyść przyszłą podłogę z mikrocementu</h3>
                                <p>Chodnik, który ma być pokryty mikrocementem musi być w idealnym stanie. Dlatego też
                                    pierwszym krokiem jest oczyszczenie przyszłej posadzki mikrocementowej z
                                    <strong>kurzu, plam, tłuszczu</strong> lub innego rodzaju cząstek, jak również
                                    upewnienie się, że podłoże jest w idealnym stanie. Bardzo ważne jest, aby sprawdzić
                                    stopień wilgotności podłogi, poczekać aż wyschnie lub bezpośrednio zastosować
                                    barierę impoxy w celu zablokowania wilgoci podciąganej lub jako barierę
                                    paroszczelną.</p>
                                <h3>2. Podkład</h3>
                                <p>Po oczyszczeniu powierzchni należy nałożyć na podłogę warstwę podkładu w celu
                                    <strong>zwiększenia przyczepności pomiędzy istniejącym podłożem a nową posadzką
                                        mikrocementową</strong>. </strong>Jeżeli podłoże jest chłonne, takie jak beton
                                    lub samopoziomujące, stosować <a class="links"
                                        href="https://www.topciment.com/pl/mikrocement/Grunty-i-przygotowanie-podloza#primacem-abs">
                                        PRIMACEM ABS</a>; jeżeli jest niechłonne, takie jak lastryko, płytki ceramiczne
                                    lub marmur, stosować <a class="links"
                                        href="https://www.topciment.com/pl/mikrocement/Grunty-i-przygotowanie-podloza#primacem-plus">PRIMACEM
                                        PLUS</a>.</p>
                                <h3>3. Pierwsza warstwa podkładu mikrocementowego</h3>
                                <p>Po zagruntowaniu należy położyć pierwszą warstwę niepigmentowanego podkładu
                                    mikrocementowego. <strong>Wskazane jest użycie siatki z włókna szklanego</strong> ,
                                    ponieważ pomoże to profesjonaliście zminimalizować szanse na pojawienie się pęknięć
                                    z czasem. Po nałożeniu przeszlifować powierzchnię. </p>
                                <h3>4. Druga warstwa bazy mikrocementowej </h3>
                                <p>Po całkowitym wyschnięciu powierzchni, nałożyć drugą warstwę pigmentowanej bazy
                                    mikrocementowej. W tym kroku, <strong>wybierz pożądany kolor mikrocementu
                                    </strong>do pokrycia ciągłej podłogi i, gdy powierzchnia jest idealnie pokryta,
                                    szlifować podłogę mikrocementową. </p>
                                <h3>5. Dwie warstwy mikrocementu wykończeniowego</h3>
                                <p>Piąty etap polega na nałożeniu <strong>dwóch warstw mikrocementu
                                        wykończeniowego</strong> lub wykończenia pigmentowego. W zależności od
                                    wykończenia i oczekiwanego stopnia śliskości, można użyć średniego lub grubego
                                    mikrocementu. Jako specjaliści zalecamy dwie warstwy <strong>Microdeck</strong>,
                                    naszego średniego mikrocementu optymalnego do nakładania mikrocementu na podłogi.
                                </p>
                                <h3>6. Ochrona podłóg microcemento </h3>
                                <p>Ostatni etap stosowania podłóg microcemento stanowi jeden z najważniejszych. Posadzka
                                    mikrocementowa jest gotowa, teraz trzeba ją zabezpieczyć.


                                    W tym celu należy nałożyć wałkiem <strong>dwie warstwy <a class="links"
                                            href="https://www.topciment.com/pl/mikrocement/lakier-topsealer#presealer">PRESEALER</a></strong>,
                                    a następnie kolejne <strong>dwie warstwy <a class="links"
                                            href="https://www.topciment.com/pl/mikrocement/lakier-topsealer">TOPSEALER
                                            WT</a></strong>. Oprócz ochrony mikrocementu podłogi, będziesz w stanie
                                    wodoodporne, a nawet uzyskać antypoślizgowe wykończenie. </p>
                                <p> <a class="links" href="../mikrocement/metody-aplikacji-mikrocementu">Na tej
                                        stronie</a>, można również dowiedzieć się o zastosowaniu mikrocementu na
                                    ścianach i basenach.</p>

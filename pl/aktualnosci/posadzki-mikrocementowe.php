                                        href="../mikrocement/mikrocement-dwuskladnikowy-unlimitted">Unlimitted</a>,
                                    nową gamę, która zapewnia gładsze wykończenie i większą odporność mechaniczną. </p>
                                <p class="h3">Mikrocement monokomponentowy</p>
                                <p>Na bazie wody i cementu opracowano <strong><a class="links"

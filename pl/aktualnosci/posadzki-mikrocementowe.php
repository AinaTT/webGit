                                <h2>Typy mikrocementu do podłóg</h2>
                                <p>Jeśli istnieje jedna cecha, która doskonale definiuje mikrocement, jest to jego
                                    wszechstronność, ponieważ istnieją różne rodzaje w zależności od tekstury i
                                    wykończenia, które chce się uzyskać: jednoskładnikowe, dwuskładnikowe i gotowe do
                                    użycia. <strong>W Topciment mamy aż cztery różne systemy mikrocementowe do
                                        posadzek</strong>: </p>
                                <p class="h3">Mikrocement dwuskładnikowy</p>
                                <p>Mikrocement dwuskładnikowy (cement + żywica) jest bez wątpienia jednym z najczęściej
                                    stosowanych w posadzkach mikrocementowych. Z tego powodu stworzyliśmy dwie linie: <a

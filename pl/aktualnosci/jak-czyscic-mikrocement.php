                    <h2>Specjalistyczne produkty do czyszczenia mikrocementu </h2>
                    <p>Pomimo, że do czyszczenia mikrocementu, zarówno podłóg jak i ścian, wystarczy woda i neutralne
                        mydło, w Topciment proponujemy dwa specjalistyczne produkty do czyszczenia mikrocementu. Dwa
                        środki czyszczące, które jeszcze bardziej zwiększają, jeśli to możliwe, trwałość optymalnego
                        stanu tej innowacyjnej powłoki. </p>



                    <h3>Czyszczenie mikrocementu za pomocą Ecoclean Basic</h3>
                    <p> <a class="links"
                            href="../mikrocement/cleaning-and-care-of-microcement#ecoclean-basic">Ecoclean Basic</a>
                        to ekologiczny środek czyszczący z detergentem przeznaczony do użytku domowego, idealny do
                        czyszczenia mikrocementu na ścianach i podłogach w domach prywatnych. Dzięki dużej sile
                        odtłuszczania i odkamieniania szybko usuwa nagromadzony tłuszcz, resztki jedzenia, kamień czy
                        ślady oleju. </p>
                    <p>Ale jak nakładać go na posadzkę z mikrocementu lub ścianę z mikrocementu? Bardzo łatwe. Wystarczy
                        rozcieńczyć go z wodą i nanieść na powierzchnię za pomocą mopa, spryskiwacza, piłki lub gąbki. W
                        celu dogłębnego oczyszczenia mikrocementu, produkt należy pozostawić na 5-15 minut. </p>
                    <h3>Czyszczenie mikrocementu za pomocą Ecoclean Pro</h3>
                    <p>W Topciment posiadamy również<a class="links"
                            href="../mikrocement/cleaning-and-care-of-microcement#ecoclean-pro">Ecoclean Pro</a>,
                        przyjazny dla środowiska, alkaliczny środek czyszczący z detergentem. Czynnik, który prowadzi do
                        konieczności stosowania ochrony do czyszczenia mikrocementu na ścianach i podłogach, stąd
                        przeznaczony jest wyłącznie do użytku profesjonalnego. </p>
                    <p> Ecolean Pro jest wysoce zalecany do czyszczenia garaży lub warsztatów, hoteli, kuchni
                        restauracyjnych, szpitali, hoteli, lotnisk, centrów handlowych i innych dużych powierzchni. W
                        przeciwieństwie do Ecoclean Basic, jego siła odtłuszczania jest znacznie większa, dzięki czemu
                        jest w stanie usunąć praktycznie wszystkie plamy i ślady. Nawet te najtrudniejsze i najbardziej
                        uporczywe, jak ślady opon, środki antyadhezyjne, zanieczyszczenia, sadza, kamień, olej, resztki
                        jedzenia, tłuszcz, krew itp. </p>
                    <p>Jeśli chodzi o zastosowanie na ścianach z mikrocementu lub podłogach z mikrocementu, najpierw
                        rozcieńcza się go wodą, a następnie natryskuje na powierzchnię, która ma być poddana obróbce.
                        Preparat należy pozostawić na około 30 sekund, po upływie tego czasu przetrzeć szmatką lub
                        wacikiem, a następnie spłukać wodą. </p>




                    <p>Pokrycie to posiada dobrą odporność termiczną, a także dobrą odporność na domowe środki
                        czystości. Dwa czynniki różnicujące, które sprawiły, że mikrocementowe podłogi i ściany w
                        kuchniach oraz blaty kuchenne pokryte mikrocementem stały się bardzo modne. </p>

                    <p> </p>
                    <h2>3.Elegir entre ducha y bañera</h2>
                    <p>A la hora de escoger <b>pensando en el medio ambiente</b>, la ducha ha ganado
                        terreno a la bañera por su funcionalidad y un gasto tres veces menor en agua. No
                        pasa esto en baños de grandes dimensiones, donde se puede apostar por el
                        confort: bañeras de dimensiones generosas o columnas de hidromasaje. Si dudamos
                        por el espacio, a partir de cinco metros cuadrados se puede instalar una bañera
                        sin problemas.<br>&nbsp;</p>
                    <p> </p>
                    <img class="img-fluid" src="https://www.topciment.com/imagenes/banos-microcemento/revestimiento-cemento-pulido-banos.jpg" width="100%" alt="revestimiento cemento pulido baños">
                    <p> </p>
                    <h2>4.Escoger el mobiliario</h2>
                    <p><br>Los sanitarios, los muebles y los complementos son tan importantes en la
                        decoración como las paredes y los suelos del baño. Dentro de los lavabos, por
                        ejemplo, existen muchas tipologías: con pie, encastrados, sobre la encimera,
                        compactos... cada uno en multitud de materiales: piedra natural, resina,
                        cemento, porcelana, cerámica, cristal... <b>Todo depende del entorno en el que
                            se vaya a colocar</b>: los sanitarios clásicos aportarán un aire romántico,
                        perfectos para una decoración vintage, mientras los encastrados a la pared
                        servirán en aquellos proyectos que buscan un toque moderno y
                        contemporáneo.<br>&nbsp;</p>
                    <p> </p>

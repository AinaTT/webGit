                    <h2>2.Suelos y paredes: resistencia y estilo</h2>
                    <p>Los <b>revestimientos del baño</b> deben satisfacer los gustos decorativos, pero
                        también soportar la humedad. Gres porcelánico, mármol o azulejos de gran formato
                        son algunas de las opciones que encontramos en el mercado. En este aspecto, el
                        <a target="_blank" class="links" href="https://www.topciment.com/es/microcementos"><u>microcemento</u>
                        </a>cumple con las funciones deseadas: l<b>a ausencia de juntas evita la
                            acumulación de suciedad</b> y se facilita así la <u>
                             limpieza
                                del baño</u>. Además, se puede aplicar perfectamente en superficies
                        horizontales y verticales, logrando un cuarto completamente estanco, y también
                        en duchas, bañeras o encimeras de lavabos.
                    </p>
                    <p> </p>

                    <h2>5.La iluminación: buscar el equilibrio</h2>
                    <p>Esenciales para crear un ambiente, las luces nodeben descuidarse y su colocación
                        debe estar muy pensada. Existen muchas alternativas: lámparas de techo, luces de
                        pared, halógenos o alrededor del espejo. Si existe iluminación natural, ese baño
                        tiene una clara ventaja sobre el resto. En la mayoría de las ocasiones interesa
                        <b>contar con un foco frontal sobre el espejo para iluminar el rostro, mientras
                            en el resto de la estancia se puede reducir la intensidad y crear una
                            atmósfera relajada</b>. También se puede jugar con los colores: los claros
                        reflejan y multiplican la luz mientras los tonos oscuros la absorben y oscurecen
                        los baños de microcemento.

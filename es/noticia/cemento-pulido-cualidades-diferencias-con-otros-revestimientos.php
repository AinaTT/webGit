                <h1>Cemento pulido: cualidades y diferencias con otros revestimientos</h1>
                <div class="titular  fadeInRight estiloNormal16justificado">


                    <p>
                        <strong> Actualmente existe mucha confusión, fruto del desconocimiento, acerca del cemento
                            pulido.</strong> De
                        hecho, la mayoría de las personas lo relacionan con el típico pavimento de estilo industrial y
                        de fuerte tonalidad gris con efecto espejo que se emplea en los interiores y exteriores de
                        grandes superficies como oficinas, comercios, fábricas, naves y parkings. Incluso, aunque en
                        muchísima menor medida, en algunas viviendas particulares.

                    </p>
                    <p>
                        No obstante, <strong>en la mayoría de ocasiones</strong> lo que se da por hecho que es cemento
                        pulido <strong>es en realidad microcemento.</strong> El revestimiento decorativo que en estos
                        momentos es tendencia en la
                        decoración y diseño de interiores, tanto en casas como en comercios y locales. Un recubrimiento
                        que también se ha extendido como la pólvora en terrazas exteriores, fachadas o piscinas.

                    </p>
                    <p>
                        <strong> En este artículo encontrarás todo lo que tienes que saber y no sabías del cemento
                            pulido:</strong> qué
                        es, sus principales cualidades, los diferentes acabados que permite, el proceso de aplicación,
                        el precio por metro cuadrado y, sobre todo, las principales diferencias con el microcemento o
                        microcemento alisado.

                    </p>
                    <figure class="figure mt-4">
                        <img src="https://www.topciment.com/imagenes/news/empresa-suelo-cemento-pulido.jpg"
                            class="figure-img img-fluid " title="empresa suelo cemento pulido."
                            alt="planta baja empresa con suelo cemento pulido.">
                        <figcaption class="figure-caption">Imagen de la planta baja de una empresa industrial con suelo
                            de cemento pulido.</figcaption>
                    </figure>
                    <h2> Qué es el cemento pulido</h2>

                    <p>
                        <strong> El cemento pulido, también llamado hormigón pulido, es en realidad un acabado
                            decorativo,</strong> no un
                        revestimiento puramente dicho. Este producto se utiliza sobre todo en espacios exteriores de
                        aparcamientos, centros comerciales o pistas deportivas y, en mucha menor medida, en alguna
                        estancia de interior.

                    </p>
                    <p>
                        El cemento pulido es un acabado resultante de<strong> espolvorear un mortero cementoso con
                            áridos y, después,</strong> pulir el suelo para sacarle brillo con una pulidora rotativa.
                        Esta técnica de pulir el
                        pavimento se denomina <strong>fratasado</strong>.

                    </p>
                    <h2>Cualidades y ventajas cemento pulido</h2>

                    <p>
                        El cemento pulido está pensado para recubrir superficies donde lo más importante es la
                        funcionalidad del pavimento más que la estética del mismo. ¿Por qué? Puesto por que el cemento
                        pulido se caracteriza por su <strong>gran durabilidad y resistencia,</strong> dos elementos que
                        lo hacen ideal
                        para revestir aquellas superficies industriales que están expuestas a un <strong>alto tránsito y
                            peso.</strong>
                        De hecho son muy utilizados para áreas de carga y descarga, ya que el pavimento de hormigón
                        pulido no se quiebra.

                    </p>
                    <p>
                        Además los pavimentos de cemento pulido resisten muy bien a la <strong>rodadura de
                            neumáticos.</strong> Es por
                        ello que observamos este tipo de acabados con mucha frecuencia en aparcamientos y parkings.

                    </p>
                    <p>
                        Otra de las cualidades de los suelos de cemento pulido es que su superficie lisa y brillante
                        <strong> evita la acumulación de polvo y suciedad.</strong> Un acabado que también favorece la
                        sensación de
                        amplitud.


                    </p>

                    <figure class="figure mt-4">
                        <img src="https://www.topciment.com/imagenes/news/pulidora-rotativa.jpg"
                            class="figure-img img-fluid " title=" pulidora rotativa."
                            alt=" operario puliendo pavimento cemento pulido con pulidora rotativa.">
                        <figcaption class="figure-caption"> Operario puliendo y sacando brillo al pavimento de cemento
                            pulido con una pulidora rotativa.</figcaption>
                    </figure>
                    <h2>Inconvenientes o desventajas del cemento pulido</h2>

                    <p>
                        Pero también hay que saber que el cemento pulido presenta una serie de inconvenientes y
                        desventajas a tener en cuenta. A continuación señalamos las principales:

                    </p>
                    <ul class="ml-3">
                        <li> El cemento pulido <strong>puede perder brillo</strong> con el tiempo a causa de líquidos
                            muy abrasivos como
                            lo son los disolventes industriales. Por tanto, habrá que volver a pulirlo.
                        </li>

                        <li> El hormigón pulido <strong>puede acabar fisurando</strong> si no se realiza de manera
                            correcta las juntas de
                            dilatación.
                        </li>

                        <li> Hay que evitar colocar este acabado en lugares con temperaturas frías o muy calurosas, ya
                            que el cemento pulido<strong> no es buen aislante térmico.</strong>
                        </li>

                        <li> El proceso de <strong>aplicación</strong> requiere de tiempo, ya que exige realizar<strong>
                                obras y utilizar maquinaria.</strong> A colación de esto, tiene juntas.
                        </li>

                        <li><strong> Escaso aislamiento acústico.</strong>
                        </li>

                        <li><strong> Baja porosidad.</strong>
                        </li>
                    </ul>
                    <h2> Acabados en cemento pulido</h2>

                    <p>
                        Aunque el cemento pulido se asocie sobre todo a los acabados de color <strong>gris</strong> por
                        los
                        emplazamientos en los que se suele aplicar, existe la posibilidad de elegir colores a priori más
                        arriesgados como <strong>negro, rojo, verde...</strong> El hormigón pulido o cemento pulido se
                        aplica en
                        aeropuertos, aparcamientos y parkings, centros comerciales y fábricas.

                    </p>

                    <h3>Baños cemento pulido</h3>

                    <p>
                        <strong>Los baños y lavabos de cemento pulido no existen.</strong> Se confunde este tipo de
                        acabado con el uso de
                        otros revestimientos decorativos como puede ser el cemento bruñido o un lavabo de microcemento.
                        De hecho, el microcemento es uno de los recubrimientos más empleados para renovar esta estancia
                        por las grandes posibilidades de uso que ofrece.

                    </p>
                    <p>
                        El microcemento en baños es perfecto para forrar platos de ducha o bañeras, gracias a la
                        capacidad <a class="links"
                            href="../microcemento/barniz-poliuretano-acrilico-alagua-topsealer#topsealer-proplus">impermeable</a> y <a class="links"
                            href="../microcemento/barniz-poliuretano-acrilico-alagua-topsealer#topsealer-anti-slip">antideslizante</a> que
                        tienen gracias al uso adecuado de selladores. Pero también para crear estanterías, muebles de
                        baño con microcemento, paredes y suelos.

                    </p>
                    <figure class="figure mt-4">
                        <img src="https://www.topciment.com/imagenes/news/nave-suelo-cemento-pulido.jpg"
                            class="figure-img img-fluid " title="nave suelo cemento pulido."
                            alt="nave industrial con suelo de cemento pulido.">
                        <figcaption class="figure-caption">Imponente nave industrial cuyo se ha revestido con cemento
                            pulido.</figcaption>
                    </figure>
                    <h2>Diferencias entre cemento pulido y microcemento</h2>

                    <p>
                        Vamos a abordar las principales diferencias entre el cemento pulido y el microcemento.

                    </p>
                    <h3>1. El cemento pulido tiene mayor grosor que el microcemento</h3>

                    <p>
                        La diferencia más palpable entre ambos acabados es su grosor. El microcemento <strong>apenas
                            tiene entre 2 y 3 milímetros,</strong> lo que posibilita que pueda aplicarse tanto en
                        superficies horizontales
                        (suelos, escaleras, techos...) como en superficies verticales como paredes. Es un material de
                        construcción tan fino que al no pesar <strong>puede aplicarse en cualquier lugar sin miedo a que
                            la estructura se vea afectada.</strong>

                    </p>
                    <p>
                        Algo que no ocurre con el cemento pulido y que, por tanto, se reserva únicamente para suelos y
                        pavimentos.

                    </p>
                    <h3> 2. El cemento pulido genera escombros, el microcemento no</h3>

                    <p>
                        Otra de las disparidades entre el microcemento y el cemento pulido hace referencia a los
                        escombros. El <strong>microcemento</strong> alisado <strong>se coloca sobre el material
                            existente,</strong> sea suelo o pared. Una
                        excelente adherencia que evita las obras y los escombros que estas conllevan.

                    </p>
                    <p>
                        Caso totalmente opuesto al <strong>cemento pulido</strong>, un acabado que <strong>requiere
                            obras para levantar por completo el suelo o pavimento existente</strong> y el empleo de
                        maquinaria para ello.

                    </p>
                    <h3>3. La instalación del cemento pulido es mucho más larga</h3>

                    <p>
                        Este punto está estrechamente relacionado con el anterior.<strong> Como el microcemento no
                            requiere de obras, el proceso de aplicación es muy rápido.</strong> Un procedimiento que en
                        el caso del cemento
                        pulido se extiende ostensiblemente.

                    </p>
                     <figure class="figure mt-4">
                        <img src="https://www.topciment.com/imagenes/news/pavimento-cemento-pulido-nave.jpg"
                            class="figure-img img-fluid " title="pavimento cemento pulido nave."
                            alt="Luminosa nave industrial con pavimento pulido.">
                        <figcaption class="figure-caption">Una de las salas de amplia nave industrial en la que se ha
                            instalado pavimento de cemento pulido.
                        </figcaption>
                    </figure>
                    <h3>4. Compatibilidad vs incompatibilidad con suelo radiante</h3>
                    <p>
                        La calefacción por suelo radiante es una gran alternativa a los radiadores y la calefacción
                        central por sus beneficios. No obstante, este sistema no es compatible con cualquier
                        revestimiento.

                    </p>
                    <p>
                        El <strong>microcemento</strong>, que goza de una gran conductividad térmica, es perfectamente
                        <strong>compatible con un suelo radiante.</strong> De hecho es uno de los materiales de
                        construcción más recomendables. <a class="links"
                            href="../noticia/microcemento-en-suelo-radiante">Aquí</a>
                        encontrarás todo lo que tienes que saber. <strong>Una cualidad de la que
                            no puede presumir el cemento pulido.</strong>

                    </p>
                    <h3>5. Solo el cemento pulido necesita juntas de dilatación</h3>

                    <p>
                        El <strong>cemento pulido</strong> necesita de la confección de <strong>juntas de
                            dilatación</strong>, aproximadamente<strong> cada 4 o 5 metros</strong> de distancia. En
                        cambio el microcemento es un revestimiento continuo y, por lo tanto, no
                        hay que elaborar juntas de dilatación. Es por esto que el uso del microcemento alisado se ha
                        disparado en interiores en detrimento del hormigón pulido.

                    </p>
                    <h3>6. El microcemento es más versátil que el cemento pulido</h3>

                   
                    <p>
                        El microcemento no es solo un recubrimiento continuo que no precisa de obras o juntas de
                        dilatación, sino que además su aplicación es más rápida y sencilla.

                    </p>
                    <p>
                        Pero no solo por eso, sino porque el microcemento alisado <strong>también proporciona la
                            seguridad de ser un revestimiento de excelente durabilidad y resistencia</strong> no solo al
                        tránsito como el cemento
                        pulido, sino también a golpes, productos químicos, o ralladuras.

                    </p>
                    <p>
                        Y, por encima de todo, porque el microcemento ofrece más soluciones decorativas que el cemento
                        pulido ya que puede aplicarse sobre cualquier superficie (horizontal o vertical) y material
                        (hormigón, azulejos, baldosas, cerámica...). <strong>Un abanico más amplio que facilita que el
                            microcemento se utilice en interiores y exteriores,</strong> tanto de viviendas particulares
                        como de
                        locales, negocios y demás empresas.

                    </p>


                    <p>
                        En definitiva, por todo esto y más el microcemento es mucho más versátil que el cemento pulido.

                    </p>


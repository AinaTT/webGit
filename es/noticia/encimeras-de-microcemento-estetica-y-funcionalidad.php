                        Desde un estilo más rústico y vintage hasta otro más vanguardista, minimalista, urbano o
                        industrial. Entre la amplia de <a class="links" href="../microcemento/pigmentos-para-microcemento-arcocem">colores de microcemento</a> para encimeras de
                        Topciment está el blanco, gris, marrón o negro, entre muchos otros.


                    <h2>¿Cómo hacer una encimera de microcemento?</h2>

                    <p>
                        El proceso de aplicación de una encimera de microcemento <strong>dependerá del tipo de acabado que se persiga y del material que haga de base</strong> de la superficie, si es un revestimiento absorbente como
                        pladur, yeso, enlucido de cemento y arena o DM, o si por el contrario es un revestimiento no
                        absorbente como mármol, terrazo, azulejos o granito.

                    </p>
                    <p>
                        Detallamos la aplicación de microcemento en encimera, paso a paso, en función del resultado que
                        se busque:

                    </p>
                    <h3>1.Colocar la malla sobre la futura encimera de microcemento. </h3>

                    <p>
                        El primer paso es colocar la malla en la superficie que hará de base a la encimera de
                        microcemento: mármol, granito, pladur... Es súper importante porque esto evitará que aparezcan
                        fisuras con el tiempo.

                    </p>
                    <h3>2.Imprimación de la encimera de microcemento</h3>

                    <p>
                        El segundo paso de la aplicación del microcemento en una encimera consiste en la imprimación,
                        que facilitará la unión del revestimiento absorbente o no absorbente existente con la nueva
                        encimera de microcemento. Para el primer caso, se aplicará el promotor de adherencia PRIMACEM
                        ABS y, para el segundo, la imprimación PRIMACEM PLUS.

                    </p>
                    <h3>3. Aplicar dos capas de microcemento Microbase en la encimera</h3>

                    <p>
                        A continuación hay que aplicar dos manos del microcemento Microbase. La primera capa tendrá que
                        ser sin pigmento y, tras lijar, será el turno de una segunda capa de Microbase ya pigmentada en
                        función del color de microcemento elegido para la encimera. Después, habrá que lijar de nuevo.
                        Si se busca un acabado pétreo o rústico, tras las dos capas de microcemento Microbase se tendría
                        que sellar para proteger la encimera de microcemento primero con el barniz acrílico Presealer y
                        después con el barniz poliuretano Topsealer.

                    </p>
                    <h3>4. Aplicar dos capas de microcemento Microfino en la encimera</h3>

                    <p>
                        Si en cambio se busca que la encimera de microcemento tenga un marcado aspecto de aguas o
                        estuco, tras las dos capas de microcemento Microbase será el turno de dos manos de microcemento
                        Microfino, ambas pigmentadas. Será necesario lijar después de cada capa, sin excepción. Una vez
                        se apliquen las dos manos de Microfino, se protegería el soporte primero con Presealer y con
                        posteridad con Topsealer.

                    </p>
                    <h3>5. Aplicar dos capas de microcemento Microdeck en la encimera</h3>

                    <p>
                        Si prefieres que la encimera de microcemento destaque por su aspecto natural, tras las dos capas
                        de Microbase se recomienda aplicar dos manos de microcemento Microdeck pigmentadas lijando al
                        final. Para terminar, igual que ocurre en las dos aplicaciones anteriores, se tendrá que aplicar
                        sobre el soporte el barniz Presealer y el sellador de microcemento Topsealer.

                    </p>

                    <h3>5. Proteger y sellar la encimera de microcemento</h3>

                    <p>
                        Una vez que se hayan aplicado las dos capas de Microbase, Microfino o Microdeck, en función del
                        acabado deseado, hay que proteger la encimera de microcemento para evitar problemas. Para sellar
                        correctamente el mueble, son necesarias dos manos de Presealer y otras dos de Topsealer, tal y
                        como se ha explicado con anterioridad.

                    </p>

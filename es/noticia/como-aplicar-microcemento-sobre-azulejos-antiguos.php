                    <h2>Renovar los azulejos antiguos con microcemento: aplicación y consejos</h2>

                    <p>
                        Para colocar microcemento sobre azulejos es <strong>imprescindible contar con un aplicador
                            profesional y cualificado </strong>que haga un buen trabajo, como los instaladores de
                        microcemento que trabajan con
                        nuestros microcementos.

                    </p>
                    <p>
                        <strong> Solo así no tendrás que lamentar</strong> algunos de los <strong><a class="links" href="../microcemento/como-evitar-manchas-o-marcas-no-deseadas-al-aplicar-microcemento">problemas</a></strong>
                      más frecuentes que son
                        consecuencia de errores en la aplicación del microcemento.

                    </p>
                    <h3>8 pasos para aplicar microcemento sobre azulejos</h3>

                    <h4>1. Evaluar estado de los azulejos </h4>

                    <p>
                        Lo primero que hay que hacer antes de aplicar microcemento directamente sobre los azulejos
                        antiguos, es comprobar el estado en el que se encuentran los azulejos. Ya que <strong>si los
                            azulejos</strong>
                        del suelo o pared<strong> tienen alguna imperfección no se puede revestir con microcemento como
                            si nada.</strong>
                        Habrá que reparar el sustrato antes de colocar nuestro microcemento alisado. De ahí la
                        importancia de que la renovación sea llevada a cabo por un profesional, porque sabrán localizar
                        dichos desperfectos.

                    </p>
                    <h4>2. Limpiar los azulejos antes de aplicar microcemento </h4>

                    <p>
                        Después de reparar los azulejos, en los casos que sea preciso, hay que limpiar a conciencia los
                        azulejos antes de aplicar microcemento alisado<strong> para que no hayan grasas ni
                            polvo.</strong>

                    </p>
                    <h4>3. Rellenar con microcemento las juntas de los azulejos</h4>

                    <p>
                        Una vez que la pared o suelo en la que se encuentran los azulejos estén limpios, toca ponerse
                        manos a la obra. El primer paso propiamente dicho de la aplicación de microcemento sobre
                        azulejos consiste en rellenar las juntas. Puesto que el microcemento alisado es un revestimiento
                        continuo y, por tanto, si<strong> no se rellenan las juntas</strong> de los azulejos el soporte
                        que hará de base
                        no será estable y habrá desniveles que marquen las juntas.

                    </p>
                    <h4> 4. Imprimar los azulejos que van a ser revestidos con microcemento</h4>

                    <p>
                        Cuando la superficie esté nivelada y las juntas de los azulejos perfectamente rellenadas, será
                        el turno de aplicar la imprimación o promotor de adherencia correspondiente. Un producto
                        fundamental ya que<strong> sirve de puente de unión entre los azulejos y el microcemento, para
                            que este último se adhiera mejor.</strong>

                    </p>
                    <p>
                        En Topciment hemos fabricado una amplia gama de imprimaciones en función de la superficie sobre
                        la que se aplica el microcemento. En este caso, que se trata de azulejos, nuestros expertos
                        aconsejan emplear uno de estos dos: Primacem Impoxy A+B o Primacem Plus. Vamos a desarrollar
                        ambos para que sepas cuál utilizar en cada ocasión.

                    </p>
                    <p>
                        <strong> <a class="links" href="../microcemento/imprimacione-y-promotores-de-adherencia-primacem#primacem-impoxy">Primacem Impoxy A + B</a> </strong> es nuestra imprimación epoxi antihumedad
                        que<strong> bloquea la humedad por capilaridad </strong>y actúa como
                        <strong> barrera de vapor</strong>. Un promotor de adherencia 100% sólidos y
                        bicomponente que es perfecto para los azulejos de la pared de la ducha o los azulejos del suelo
                        del baño. Un innovador producto que, además,<strong> evita el calcado de las juntas</strong> en
                        el microcemento.

                    </p>
                    <p>
                        Por otro lado, también se puede utilizar<strong> <a class="links" href="../microcemento/imprimacione-y-promotores-de-adherencia-primacem#primacem-plus">Primacem Plus </a></strong>, nuestra
                        imprimación
                        específica para superficies no absorbentes como el es caso de los azulejos y que se aplica en
                        una única mano.

                    </p>
                    <figure class="figure mt-4">
                        <img src="https://www.topciment.com/imagenes/news/azulejos-microcemento-calabaza-pared-banyo.jpg"
                            class="figure-img img-fluid " title="Microcemento color Calabaza azulejos pared baño" alt="Azulejos pared de baño recubiertos microcemento color Calabaza ">
                        <figcaption class="figure-caption">El antes y después de un cuarto de  baño en el que se ha usado nuestro microcemento color Calabaza para recubrir los azulejos de la pared del baño.</figcaption>
                    </figure>
                  
                    <h4> 5. Colocar<strong> malla de fibrio</strong> en la primera capa de microcemento sobre azulejos
                    </h4>

                    <p>
                        Tras dejar las horas de secado pertinentes (mirar fichas técnicas), colocaremos la malla de
                        fibrio sobre los azulejos antes de aplicar el microcemento <strong>para proteger el futuro
                            revestimiento y evitar fisuras, grietas, etc. </strong>Un paso que no todo el mundo tiene en
                        cuenta y que después se
                        convierte en un gran quebradero de cabeza.

                    </p>
                    <p>
                        Acto seguido, se aplicará la<strong> primera capa de microcemento base
                        </strong>(Microbase)<strong> sin pigmento sobre los azulejos.</strong> Una vez estén bien
                        cubiertos los azulejos por el microcemento, sin
                        irregularidades, se lijará el soporte.

                    </p>
                    <h4> 6. Aplicar segunda capa de microcemento base sobre los azulejos </h4>

                    <p>
                        Aplicar la segunda capa de microcemento base (Microbase). <strong>En esta ocasión sí tendrá que
                            estar pigmentada </strong>con el color de microcemento que se haya elegido para revestir los
                        azulejos antiguos.
                        De la misma manera que en el paso anterior, la superficie deberá ser lijada.

                    </p>
                    <h4>7. Aplicar dos capas de microcemento Microdeck o Microfino sobre azulejos</h4>

                    <p>
                        <strong>Después del microcemento base, hay que aplicar dos capas de microcemento Microdeck o dos
                            capas de Microfino</strong> sobre azulejos. El primero, suele utilizarse para suelos y, el
                        segundo, para
                        paredes. Aunque en función de los gustos, preferencias y acabados que se busque se pueden hacer
                        combinaciones. Ambas manos estarán ya pigmentadas. Después, lijar.

                    </p>
                    <h4>8. Proteger y sellar el microcemento sobre azulejos</h4>

                    <p>
                        El último paso es uno de los más importantes. Se trata de<strong> proteger el revestimiento de
                            microcemento sobre azulejos.</strong> Para ello, contamos con todo tipo de barnices y
                        selladores. Aunque
                        en esta ocasión, nosotros recomendamos utilizar<strong> dos manos</strong> de
                        <strong><a class="links" href="../microcemento/barniz-poliuretano-acrilico-alagua-topsealer#presealer">Presealer</a></strong> y otras
                        <strong> dos manos</strong> de <strong><a class="links" href="../microcemento/barniz-poliuretano-acrilico-alagua-topsealer#topsealer-one-coat">Topsealer WT One Coat</a></strong> 

                    </p>
                      <figure class="figure mt-4">
                        <img src="https://www.topciment.com/imagenes/news/azulejos-microcemento-piedraparis-suelo-pared-cocina.jpg"
                            class="figure-img img-fluid " title="Microcemento color  Piedra Paris pared y suelo cocina" alt="Cocina con azulejos en pared y suelo revestidos con microcemento color Piedra Paris.">
                        <figcaption class="figure-caption">Impresionante renovación de una cocina después de utilizar el microcemento color Piedra París Topciment sobre los azulejos del suelo y pared. </figcaption>
                    </figure>
                    <h3>Consejos y recomendaciones para revestir azulejos con microcemento</h3>

                    <h4>1. Respetar tiempos de secado</h4>

                    <p>
                        En el caso de utilizar una imprimación para superficies no absorbentes como<strong> Primacem
                            PLUS</strong>, es
                        muy importante que cuando se revista con microcemento los azulejos respetar el<strong> tiempo de
                            secado.</strong>
                        Si no se siguen a rajatabla las indicaciones y tiempos de secado especificados en las fichas
                        técnicas, quedarán humedades atrapadas entre manos y por tanto se marcarán las juntas de los
                        azulejos.

                    </p>
                    <p>
                        Con este consejo evitarás producir el fallo más común al aplicar microcemento sobre azulejos. Un
                        posible error que se evita al utilizar la imprimación Primacem Impoxy A+B, para la que no es
                        necesario ser tan meticuloso con las horas de secado por su capacidad de bloquear la humedad por
                        capilaridad.

                    </p>
                    <h4>2. Dejar secar 24 horas antes de aplicar la segunda capa de microcemento</h4>

                    <p>
                        Otra de las recomendaciones de los expertos de Topciment, en el caso también de utilizar
                        <strong> Primacem PLUS</strong>, es dejar un periodo de <strong>24 horas de secado entre la
                            primera y segunda capa de microcemento base.</strong>

                    </p>
                    <p>
                        Un proceso que no todo el mundo hace ya que no es obligatorio, pero con el que el profesional se
                        asegura que haya muchas menos probabilidades de que se marquen las juntas por un error en la
                        aplicación del microcemento sobre azulejos. Con Primacem Impoxy A+B se hará con el sistema
                        normal. Es decir, aplicando la segunda mano de Microbase inmediatamente después de la primera.

                    </p>
                    <h4>3. Imprimar los azulejos antes de aplicar la segunda mano de microcemento</h4>

                    <p>
                        Recomendamos además que <strong>24 horas después de haber aplicado la primera mano</strong> de
                        microcemento,
                        tiempo más que suficiente para que haya secado como toca,<strong> se vuelva a imprimar</strong>
                        el microcemento
                        que reviste los azulejos con <strong>Primacem PLUS.</strong>

                    </p>
                    <p>
                        Un consejo dirigido solo a los aplicadores más expertos y gracias al cual se conseguirá<strong>
                            igualar el nivel de absorción de las próximas manos de aplicación de microcemento sobre
                            azulejos.</strong> Una
                        igualdad con la que se reducen de manera drástica las marcas visuales de las juntas que cuentan
                        con diferentes puntos de absorción.

                    </p>







                    <h2>Cómo aplicar microcemento en un suelo radiante: paso a paso</h2>
                    <p><strong>Nuestro equipo técnico lleva cerca de dos décadas</strong>  asesorando y resolviendo dudas a los
                        profesionales sobre la aplicación del microcemento. Una de las más recurrentes es precisamente
                        acerca de la posibilidad de instalar un suelo radiante de microcemento.
                    </p>
                    <p>A continuación, te explicamos el paso a paso para que la  <a class="links" href="../microcemento/como-aplicar-microcemento-aplicacion-de-microcemento-paso-a-paso"> aplicación del microcemento</a> sobre suelo
                        radiante sea todo un éxito. Toma nota de nuestros consejos.
                    </p>
                    <h3>Calentar el suelo antes de aplicar el microcemento</h3>
                    <p>Antes de aplicar el microcemento en el suelo radiante hay que realizar un <strong>protocolo de encendido
                        de la calefacción.</strong> De esta manera, se evitará que las fisuras ocasionadas por los cambios de
                        temperatura en la solera puedan perjudicar al microcemento.
                    </p>

                    <p>Encender la calefacción al menos 4 semanas antes de colocar el mortero de cemento.
                    </p>
                    <p>Sostener el agua de entrada a los circuitos a 25ºC durante un periodo de 2 o 3 días.
                    </p>
                    <p>Ir incrementando de manera gradual la temperatura del agua hasta alcanzar los 45ºC. Mantenerla
                        así varios días.
                    </p>
                    <p>
                        Apagar la calefacción 48 horas antes de comenzar a instalar el microcemento sobre el suelo
                        radiante. Es muy importante que la temperatura máxima del suelo no sobrepase los 18ºC.

                    </p>
                    <h3>Aplicación microcemento sobre suelo radiante
                    </h3>
                    <p>Los suelos de microcemento han de colocarse <strong>sobre morteros cementosos que hayan logrado una fase
                        avanzada de fraguado.</strong> Para alcanzar esa fase óptima han de pasar tres o cuatro semanas, un plazo
                        que puede sufrir variaciones en función de la climatología. Es fundamental comprobar que el
                        mortero esté prácticamente seco (máximo de 5% de humedad).
                    </p>
                    <h3>Encender la calefacción por suelo radiante tras la instalación del microcemento
                    </h3>
                    <p>1. Cuando el microcemento ya esté correctamente aplicado sobre el suelo radiante, hay que <strong>esperar
                        mínimo 48 horas</strong> para poder encender de nuevo la calefacción. Hay que tener en cuenta los
                        siguientes aspectos:
                    </p>
                    <p>2. El encendido de la calefacción deberá ser paulatino.Se irá subiendo a 5ºC cada día.</p>
                    <p>3. Cambiar la temperatura de forma escalonada al principio y al final del uso de la calefacción por
                        suelo radiante.
                    </p>
                    <p>4. Asegurar que la humedad relativa del aire de la estancia no sea demasiado baja.
                    </p>
                    <p>5. Evitar que el calor se acumule como consecuencia del uso de alfombras y moquetas o, también, por
                        haber poco espacio entre los muebles y el suelo radiante.
                    </p>

                    </p>
                    <h3>Elegir microcemento de mala calidad</h3>
                    <p>
                        Aunque el microcemento es un revestimiento continuo muy resistente al calor que no debería
                        fisurar, no hay que escatimar en la calidad del mismo. A mayor calidad, mayor resistencia y por
                        tanto menos probabilidades de que aparezcan problemas del microcemento en suelo radiante.

                    </p>

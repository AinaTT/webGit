                    <h2>Cómo aplicar microcemento en escaleras</h2>
                    <p>
                        Lo primero que hay que tener en cuenta a la hora de aplicar microcemento en una escalera, es que
                        este proceso <strong>solo puede ser llevado a cabo por un profesional experimentado</strong>. Ya que revestir con
                        microcemento una escalera requiere de una técnica de aplicación avanzada para obtener el
                        resultado deseado. Una serie de particularidades que hay que tener en cuenta y que solo un
                        aplicador formado será capaz de garantizar.

                    </p>
                    <h3>Aplicar microcemento en escaleras en 8 pasos</h3>

                    <p>
                        Toma nota de los 8 pasos a seguir en las escaleras revestidas con microcemento.

                    </p>
                    <h4>1. Evaluar estado del soporte</h4>

                    <p>
                        Hay que analizar el estado del soporte existente en las escaleras antes de aplicar microcemento.
                        Ya que si<strong> el soporte que va a hacer de base</strong> a la escalera de microcemento <strong>no se encuentra en óptimo estado</strong>,
                         podrían acabar apareciendo todo tipo de <strong>grietas o defectos</strong>. Por eso es tan
                        importante contar con un buen profesional, que sabrá evaluar correctamente la calidad del
                        soporte.

                    </p>
                    <h4>2. Imprimación de la escalera de microcemento</h4>

                    <p>
                        Una vez nos aseguremos que el soporte está en buen estado, el segundo paso para hacer una
                        escalera de microcemento es colocar la <strong>imprimación o promotor de adherencia.</strong>

                    </p>
                    <h4>3. Nivelar los escalones de la escalera de microcemento</h4>

                    <p>
                        El tercer paso para hacer una escalera de microcemento consiste en nivelar y proteger los
                        escalones. De esta forma <strong>se previenen roturas y desgastes futuros </strong>al eliminar cualquier
                        irregularidad que pueda haber.

                    </p>
                    <h4>4. Aplicar primera capa microcemento Microbase en la escalera</h4>

                    <p>
                        El cuarto paso de aplicación del microcemento en escaleras consta de aplicar una <strong>primera capa de Microbase </strong>sin pigmento. Una vez hecho, se lijaría.

                    </p>
                      <figure class="figure mt-4">
                                                    <img src="https://www.topciment.com/imagenes/news/escalera-microcemento-interior.jpg" class="figure-img img-fluid " title="escalera microcemento interior con soporte central" alt="escalera de microcemento interior con soporte central ">
                                                    <figcaption class="figure-caption">Escalera de microcemento en el interior de una vivienda con soporte central.
                                                    </figcaption>
                                                </figure>
                    <h4>5. Aplicar segunda capa microcemento Microbase en la escalera</h4>

                    <p>
                        El quinto paso de aplicación en escaleras de microcemento se trata de <strong>aplicar una segunda capa de Microbase</strong>, en este caso <strong>con pigmento</strong>. Se tiene que repetir el procedimiento y lijado del paso
                        anterior.

                    </p>
                    <h4>6. Aplicar primera capa microcemento Microdeck en la escalera </h4>

                    <p>
                        El sexto paso para aplicar el microcemento en escaleras es una <strong>primera capa delgada de Microdeck</strong>
                        con color. Una vez seque la capa habrá que lijar la superficie.

                    </p>
                    <h4>7. Aplicar segunda capa microcemento Microdeck en la escalera </h4>

                    <p>
                        El sexto paso para hacer escaleras de microcemento es aplicar la <strong>segunda capa de Microdeck.</strong> En
                        cuanto seque será necesario lijar la superficie.

                    </p>
                    <h4>8. Sellar y proteger la escalera de microcemento</h4>

                    <p>
                        El último paso de la aplicación del microcemento en escaleras es uno de los más importantes, ya
                        que se trata de <strong>proteger el revestimiento y que su estado óptimo se prolongue en el tiempo.</strong>
                        Para ello, en Topciment aconsejamos aplicar dos manos del <strong><a class="links" href="../microcemento/barniz-poliuretano-acrilico-alagua-topsealer#presealer">barniz acrílico Presealer</a></strong> y
                        otras dos manos de uno de nuestros barnices de poliuretano, especialmente <strong><a class="links" href="../microcemento/barniz-poliuretano-acrilico-alagua-topsealer#topsealer-one-coat">Topsealer WT One Coat</a></strong>
                        .

                    </p>
                    <h2>3 consejos para aplicar microcemento en escaleras</h2>

                    <p>
                        Como se ha comentado con anterioridad, <strong>no todo el mundo está capacitado para realizar escaleras de microcemento,</strong> solo aplicadores profesionales con años de experiencia debido a la complejidad
                        que entraña.

                    </p>
                    <p>
                        Estos son los tres consejos principales que hay que tener muy presentes a la hora de hacer
                        escaleras de microcemento:

                    </p>
                    <h3>1. Proteger con perfil de aluminio los bordes de la escalera de microcemento </h3>

                    <p>
                        Las esquinas o bordes de los peldaños de las escaleras de microcemento son, sin duda alguna, la
                        zona que más expuesta a todo tipo de rozaduras y golpes. Por ello, como fabricantes aconsejamos
                       <strong> aplicar cantoneras o<a class="links" href="../microcemento/herramientas-microcemento#perfil"> perfiles de aluminio </a></strong>. Complementos que pueden quedar
                        ocultos a la vista (aunque nosotros recomendamos dejarlos visibles porque aportan un punto de
                        elegancia) y que <strong>protegerán mejor y por más tiempo</strong> las escaleras de microcemento, evitando así
                        que se produzcan roturas o desgastes.

                    </p>
                      <figure class="figure mt-4">
                                                    <img src="https://www.topciment.com/imagenes/news/escalera-microcemento-marfil-con-perfil-aluminio.jpeg"
                                                        class="figure-img img-fluid " title="escalera microcemento exterior Acero" alt="Escalera de microcemento exterior color Acero">
                                                    <figcaption class="figure-caption">Escalera de microcemento de color Acero de
Topciment en el exterior de una vivienda.</figcaption>
                                                </figure>
                    <h3>2. Cantos de la escalera de microcemento mínimamente redondeados</h3>

                    <p>
                        Aunque se puede dejar que el escalón de la escalera de microcemento tenga 90 grados, es
                        recomendable que los cantos estén redondeados aunque sea mínimamente. Siguiendo este consejo y
                        "cortando" los bordes de la futura escalera de microcemento,<strong> se evitan los desconchones o golpes</strong>
                        ya que el impacto sobre la escalera de microcemento es menor.

                    </p>
                    <h3>3. Malla de fibrio sí o sí en una escalera de microcemento</h3>

                    <p>
                        Aunque algunos aplicadores no siempre colocan una malla de fibrio en la capa base del
                        microcemento, en Topciment somos partidarios de hacerlo siempre. Gracias a la malla <strong>logramos reforzar la escalera de microcemento</strong>

                    </p>



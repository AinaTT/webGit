                    <h2>Tipos de escaleras de microcemento</h2>

                    <p>
                       <strong> Las posibilidades de diseño</strong> que permite el microcemento para revestir escaleras <strong>son ilimitadas.</strong>
                        En la categorización que nosotros vamos a hacer, hemos tenido en cuenta dos factores. En primer
                        lugar, si los peldaños de las escaleras de microcemento se encuentran entre las paredes o no y
                        el tipo de contrahuellas de las mismas. 
                    </p>
                    
                    <p>
                        Con esta diferenciación, nos encontramos las siguientes escaleras de microcemento: escaleras de
                        microcemento cerradas con dos paredes, escaleras de microcemento cerradas con una pared,
                        escaleras de microcemento cerradas sin paredes, escaleras de microcemento abiertas con dos
                        paredes, escaleras de microcemento abiertas con una pared, escaleras de microcemento abiertas
                        sin paredes, escaleras de microcemento empotradas en una pared, escaleras de microcemento
                        empotradas sin paredes y escaleras de microcemento con soporte central.

                    </p>

                    <h2> Qué microcemento y color utilizar para las escaleras</h2>

                    <p>
                        Aunque el gris es el color más demandado para revestir con microcemento las escaleras, se pueden
                        aplicar <strong>todo tipo de colores</strong> con los que dotar de un elemento diferenciador a la decoración de
                        tu casa o negocio. En Topciment, contamos con un amplio catálogo de colores de microcemento para
                        escaleras. Desde colores neutros para un diseño más minimalista e industrial a colores más vivos
                        si se busca una decoración más arriesgada. 
                    </p>
                    <p>
                       <strong> En cuanto al tipo de microcemento</strong>, nuestros expertos recomiendan el microcemento de terminación
                        Microdeck para revestir las escaleras. En Topciment hemos desarrollado tres tipos de Microdeck:
                        <a class="links" href="../microcemento/microcemento-bicomponente-sttandard#sttandard-micrideck">Sttandard Microdeck</a> (microcemento bicomponente tradicional), 
                        <a href="../microcemento/microcemento-bicomponente-unlimitted#unlimitted-microdeck">Unlimitted Microdeck</a> (microcemento
                        bicomponente que mejora la trabajabilidad) y<a href="../microcemento/microcemento-monocomponente-evoluttion#evoluttion-microdeck"> Evoluttion Microdeck</a> (microcemento monocomponente).

                    </p>

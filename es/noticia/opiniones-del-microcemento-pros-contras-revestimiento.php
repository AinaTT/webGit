                    <h2> Problemas del microcemento más comunes </h2>

                    <p>
                        Hay que remarcar que<strong> si se utiliza un material de calidad</strong> y la<strong>
                            aplicación es llevada a cabo por un profesional, el microcemento no presentará contras.
                        </strong>Y es que la aparición de fisuras,
                        grietas, marcas y manchas, los problemas que más preocupan en torno al microcemento alisado, se
                        deben sobre todo a fallos en pasos fundamentales de la aplicación del microcemento o al mal
                        estado del soporte que sirve de base del nuevo revestimiento.

                    </p>
                    <h3>Problemas del microcemento por una mala aplicación</h3>

                    <p>
                        La aplicación del microcemento tiene unos pasos muy marcados. <strong>Alterar el orden o cometer
                            errores en el proceso, especialmente en la imprimación y en el sellado,</strong> conllevará
                        automáticamente a un
                        resultado no deseado. De ahí insistir que la instalación del microcemento alisado ha de ser
                        realizada por un aplicador profesional. Describimos a continuación los errores más frecuentes al
                        aplicar microcemento.

                    </p>
                    <h4>1. Aplicar microcemento con una temperatura demasiado baja o alta</h4>

                    <p>
                        La <strong>temperatura ambiente ideal</strong> para aplicar microcemento está oscila
                        <strong>entre los 15ºC y 25ºC.</strong> Si la
                        instalación del revestimiento se realiza con una temperatura inferior a los 15ºC o superior a
                        los 25ºC pueden surgir problemas.

                    </p>
                    <p>
                        Si la temperatura es demasiado baja, un factor que ocurre en la época invernal, la superficie
                        recubierta con micro cemento puede tardar mucho tiempo en endurecer correctamente. En cambio, si
                        la temperatura es excesiva ocurre justo lo contrario, el micro cemento endurecería demasiado
                        rápido.

                    </p>
                    <h4>2. Mezclar mal el pigmento de microcemento</h4>

                    <p>
                        Pigmentar el microcemento adecuadamente es clave. Si no se siguen las indicaciones
                        cuidadosamente <strong>puede haber un exceso de grupos que provoquen rayas oscuras</strong> en
                        la superficie
                        revestida con microcemento.

                    </p>
                    <p>
                        Para obtener la gran variedad de colores de microcemento del catálogo Topciment, hay que
                        <strong> respetar la dosificación indicada en cada etiqueta </strong>tanto para la cantidad como
                        para el tipo de
                        microcemento empleado.

                    </p>
                    <p>
                        Se tendrá además que agitar con fuerza el pigmento hasta lograr un líquido homogéneo sin
                        irregularidades. Después verter un poco de dicho líquido de amasado, en un recipiente, añadir la
                        totalidad del pigmento y mezclar concienzudamente. A continuación añadir poco a poco el
                        microcemento y el resto de líquido de amasado en el mismo recipiente hasta lograr que la
                        viscosidad requerida para cada aplicación y un color de microcemento homogéneo.

                    </p>
                    <h4>3. No respetar los pasos al instalar microcemento</h4>

                    <p>
                        <strong>La aplicación del microcemento requiere de unos pasos que hay que seguir a
                            rajatabla.</strong> No
                        respetarlos puede resultar fatal para el acabado final.

                    </p>
                    <p>
                        La instalación del microcemento en suelos o paredes, por ejemplo, no es la misma. Ya que varía
                        el tipo de microcemento alisado que hay que aplicar. Del mismo modo no acondicionar y nivelar el
                        soporte existente que se va a revestir, puede provocar futuros problemas importantes.

                    </p>
                    <p>
                        Como tampoco se pueden cometer fallos en la imprimación del soporte o en el sellado del mismo.
                        <a class="link" href="../microcemento/como-aplicar-microcemento-aplicacion-de-microcemento-paso-a-paso">En esta página</a>,
                        encontrarás todo lo que tienes que saber para no realizar
                        errores en la aplicación de microcemento.

                    </p>
                    <figure class="figure mt-4">
                        <img src="https://www.topciment.com/imagenes/news/restaurante-con-microcemento-blance-en-pared.jpg"
                            class="figure-img img-fluid " title="restaurante microcemento pared y suelo"

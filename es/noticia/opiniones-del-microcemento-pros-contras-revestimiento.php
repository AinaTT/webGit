                        <figcaption class="figure-caption">Restaurante con microcemento blanco en la pared y
                            microcemento gris en el suelo.</figcaption>
                    </figure>
                    <h3> Problemas del microcemento por una mala reparación de la superficie</h3>

                    <p>
                        Cuando es necesario arreglar el microcemento por alguno de los errores previamente descritos o,
                        <strong> al utilizar un material de mala calidad se han generado desconchones por los golpes,
                        </strong>se ha de
                        reparar correctamente.

                    </p>
                    <p>
                        Como se trata de un pavimento continuo, sin juntas, para <strong>reparar</strong> correctamente
                        el microcemento
                        alisado hay que hacerlo <strong>por zonas que sean grandes para que no se perciba mucho el
                            contraste</strong> y
                        el acabado final acabe siendo un desastre.

                    </p>
                    <h3>Fisuras y grietas del microcemento</h3>
                    <p>
                        <strong> Si el microcemento es un revestimiento tan resistente y duro</strong>, ¿por qué hay
                        casos en los que
                        superficies revestidas con microcemento alisado fisuran y aparecen grietas? Principalmente por
                        tres motivos que desarrollamos a continuación.

                    </p>
                    <h4> 1. Fisuras microcemento mala calidad</h4>

                    <p>
                        Aunque sea una obviedad, es importante señalar que <strong>no todos los microcementos garantizan
                            la no fisuración, solo los de altas prestaciones.</strong> Cuanto menor sea la calidad del
                        revestimiento, más
                        posibilidades de que fisure con el tiempo. Por eso es muy importante no escatimar con la calidad
                        del microcemento alisado.

                    </p>
                    <p>
                        <strong>En Topciment garantizamos al profesional que nuestros microcementos no
                            fisurarán</strong> gracias a sus
                        excelentes prestaciones y resistencias.

                    </p>
                    <h4>2. Fisuras microcemento por rellenar juntas dilatación</h4>
                    <p>
                        Como se ha comentado con anterioridad, el microcemento alisado no necesita de juntas de
                        dilatación ya que es un revestimiento continuo. No obstante hay que tener mucho cuidado sobre
                        qué soporte se aplica, ya que aunque es compatible con múltiples materiales hay que tener en
                        consideración aquellos que presentan juntas de dilatación como una solera de hormigón.

                    </p>
                    <p>
                        <strong> Si el microcemento se coloca sobre una superficie con juntas de dilatación, es muy
                            importante que se respeten y no se rellenen.</strong> De hacerlo, el "microcemento" podría
                        fisurar.

                    </p>
                    <h4> 3. Fisuras microcemento por malas condiciones soporte</h4>
                    <p>
                        A colación de la condición anterior, <strong>si el soporte sobre el cual se va a instalar el
                            microcemento no está en buenas condiciones habrá que repararlo.</strong> Porque si se
                        reviste dicho
                        soporte no óptimo con microcemento, acabará fisurando por el mal estado del soporte existente y
                        que funciona como base del microcemento alisado.

                    </p>
                    <h4>4. Fisuras y grietas microcemento sobre parquet</h4>
                    <p>
                        El parquet y el microcemento no se llevan bien, debido a que el parquet es un material que está
                        sujeto a variaciones en función de las condiciones ambientales como <strong>la humedad.</strong>
                        Una humedad que
                        <strong>provoca que el parquet se dilata y contraiga,</strong> por lo que si se aplica
                        microcemento encima
                        acabará fisurando.

                    </p>
                    <p>
                        Además, como el parquet es una <strong>superficie inestable al poseer gran cantidad de juntas
                            móviles,</strong>
                        la malla de fibra de vidrio que se aplica antes de la base del microcemento se puede romper y,
                        como consecuencia,<strong> acabarían surgiendo grietas.</strong>

                    </p>

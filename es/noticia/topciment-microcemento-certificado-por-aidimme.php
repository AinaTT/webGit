                <h1> Topciment microcemento con ensayos y certificación AIDIMME</h1>
                <p>Topciment en su continuo proceso de mejora y adopción de las buenas metodologías de trabajo da un
                    paso más en la certificación de sus productos para asegurar la calidad de producción a sus clientes
                    y distribuidores.</p>
                <div id="mitadTextoIzquierda" >

                    <p>El Laboratorio de AIDIMME (Instituto Tecnológico Metalmecánico, Mueble, Madera, Embalaje y
                        Afines), que ha realizado estos ensayos de certificación, es unos de los mejor equipados de
                        Europa, y dispone de un conocimiento acumulado desde 1994, cuando se inauguraron las
                        instalaciones, para dar servicio a una necesidad creciente de las empresas de certificar la
                        calidad en los procesos de fabricación.</p>

                    <p>Los ensayos de certificación realizados por AIDIMME consisten en reproducir las condiciones a las
                        que pueden ser expuestos los productos y ver cómo responden. Los laboratorios de AIDIMME están
                        acreditados por ENAC y otras entidades de ámbito internacional como ISTA.</p>
                    <p>Para cuantificar el desgaste de nuestros recubrimientos se estudia la resistencia a la abrasión
                        por el método Taber para pinturas y barnices bajo la norma UNE 48 250:1992, realizado con lijas
                        CS-17, con carga y diferentes ciclos de abrasión, simulando un uso medio o alto.</p>


                </div>
                <div id="mitadTextoDerecha" data-animate-scroll='{"x": "0",
                    "y": "50",
                    "alpha": "0",
                    "duration": "2"}'>
                    <p>Además se analiza la dureza Shore bajo la norma UNE-EN ISO 868 determinando penetración en la
                        superficie del recubrimiento y la resistencia al impacto bajo la norma UNE EN 6272- dónde se
                        determina la deformación rápida. </p>
                    <p>Se estudia la adherencia que tienen los recubrimientos mediante el Ensayo de Corte Enrejado bajo
                        la norma UNE en 2409:2013, valorando así si hay desprendimiento de revestimiento.</p>
                    <img src="https://www.topciment.com/imagenes/aidimme/microcemento-aidimme.jpg" width="100%" alt="Topciment microcemento con ensayos y certificación AIDIMME">
                    <p>También se examina la resistencia a los líquidos bajo la norma UNE EN ISO 2813-3:2013,y la
                        resistencia a los agentes químicos bajo la norma UNE EN 13442:2013, evidenciando así el uso y el
                        mantenimiento adecuado de nuestros desarrollos.</p>
                    <p>Igualmente se hacen ensayos específicos a cada producto según su aplicación final, como por
                        ejemplo la determinación del valor de la resistencia al deslizamiento/resbalamiento de
                        pavimentos comprobando que nuestro producto es apto para el uso en superficies con pendiente
                        igual o mayor que el 6% y en escaleras.</p>
                    <p>Topciment, siguiendo su filosofía de ofrecer la máxima calidad en todos los productos que
                        fabrica, continuará con su labor de desarrollo I+D+i para ofrecer productos innovadores y de
                        alta exigencia tecnológica gracias a una actualización e investigación constantes.</p>

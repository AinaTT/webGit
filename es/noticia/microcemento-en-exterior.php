                        <figcaption class="figure-caption">

                            Lujosa vivienda cuyo suelo de microcemento en terraza y piscina favorecen la continuidad del
                            espacio.
                        </figcaption>
                    </figure>

                    <h3> Proceso de aplicación de un suelo de microcemento en exterior</h3>

                    <p>
                        La<strong><a class="links"
                                href="../microcemento/como-aplicar-microcemento-aplicacion-de-microcemento-paso-a-paso">
                                aplicación de un suelo de microcemento </a></strong>exterior es siempre la misma. Lo
                        único que hay que elegir es el tipo de microcemento que queremos (nosotros recomendamos
                        Sttandard Microstone) y el color.

                    </p>
                    <h3>Proceso de aplicación de microcemento exterior en paredes</h3>

                    <p>
                        La aplicación de microcemento exterior en paredes cambia respecto a la de pavimentos. En esta
                        otra <strong><a class="links"
                                href="../microcemento/como-aplicar-microcemento-aplicacion-de-microcemento-paso-a-paso">página</a></strong>
                        encontrarás de manera detallada el paso a paso.

                    </p>
                    <h3>Proceso de aplicación de microcemento exterior en piscinas</h3>

                    <p>
                        El carácter antideslizante e impermeable de las piscinas de microcemento, así como su
                        resistencia a los efectos del sol, hacen que sea una de los usos más inteligentes que se le
                        puede otorgar al microcemento exterior. Como se ha comentado con anterioridad, su aplicación
                        resulta algo más compleja. <strong><a class="links"
                                href="../microcemento/como-aplicar-microcemento-aplicacion-de-microcemento-paso-a-paso">Aquí</a>
                        </strong> encontrarás todo lo que tienes que saber de
                        la aplicación del microcemento exterior en piscinas.

                    </p>
                    <p>
                        Como has podido comprobar, el<strong> microcemento en exteriores da muchísimo juego.</strong> Se
                        pueden crear
                        todo tipo de estructuras y ambientes en función de dónde se aplique. Así que ya que conoces todo
                        lo que tienes que saber del microcemento para exteriores,<strong> ¿a qué estás esperando a
                            empezar a remodelar tu espacio?</strong>

                    </p>
















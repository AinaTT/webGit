                        <figcaption class="figure-caption">Moderna piscina exterior de doble altura recubierta con
                            microcemento. </figcaption>
                    </figure>
                    <h2>¿Cuánto cuesta el microcemento en exterior? </h2>

                    <p>
                        Puede dar la impresión de que el <strong><a class="links" href="precio-del-microcemento">precio
                                del microcemento</a></strong> en exterior sea
                        más elevado que en interiores. No obstante dicha apreciación es completamente errónea. El coste
                        final de la aplicación del microcemento para exterior e interior <strong>depende</strong> de los
                        mismos factores:
                        <strong>estado del soporte</strong> (si tiene humedades, si no es estable, si hay que
                        arreglarlo, etc), la
                        superficie en<strong> metros cuadrados</strong> que hay que revestir y la
                        <strong>dificultad</strong> de la obra en sí misma.

                    </p>
                    <p>
                        Solo en una ocasión el <strong>precio del microcemento en exterior es mayor</strong>: en el caso
                        de las <strong>piscinas</strong>.
                        ¿Por qué? Porque <strong><a class="links"
                                href="../microcemento/microcemento-para-piscinas-atlanttic#aplicacion">su
                                aplicación</a></strong> es mucho más compleja y requiere de
                        una mano de obra más cara.

                    </p>
                    <h2>Cómo aplicar microcemento en exteriores correctamente</h2>

                    <p>Para aplicar microcemento en exteriores sin problemas hay que seguir a rajatabla una serie de
                        pasos. Cometer fallos en el proceso supondría un acabado final muy distinto del deseado y
                        esperado. De ahí, que sea tan importante contar con un profesional capacitado y experimentado.
                    </p>
                    <p>
                        Estos son los seis pasos para aplicar microcemento en exteriores:

                    </p>

                    <p>
                        1.<strong> Acondicionar el soporte</strong>que va a ser revestido con el microcemento para
                        exteriores. Ver que está en buen estado y, de no ser así, repararlo.
                    </p>
                    <p>
                        2.<strong> Colocar la malla de fibra </strong>de vidrio sobre el futuro suelo de microcemento
                        exterior o pared, que ayudará a reforzar el futuro revestimiento decorativo continuo de
                        exterior.
                    </p>
                    <p>3<strong>. Imprimar el futuro revestimiento</strong>de microcemento exterior con un promotor de
                        adherencia. </p>
                    <p> 4. Aplicar<strong> dos manos de microcemento base </strong>para exteriores. </p>
                    <p> 5. Aplicar <strong>dos manos de microcemento de terminación</strong> para exteriores. </p>
                    <p>6.<strong> Sellar el recubrimiento </strong>de microcemento exterior con un buen barniz. </p>

                    <figure class="figure mt-4">
                        <img src="https://www.topciment.com/imagenes/news/microcemento-exterior-terraza.jpg"
                            class="figure-img img-fluid " title="Suelo microcemento exterior terraza"

                <h1> Suelos de microcemento: aplicación, tipos, ventajas y consejos
                </h1>
                <div class="titular  fadeInRight estiloNormal16justificado">
                    <p>El microcemento es <strong>uno de los pavimentos continuos más extendidos del mundo</strong> por
                        las oportunidades
                        decorativas que ofrece tanto para interiores como exteriores. Un revestimiento que puede
                        aplicarse en suelos de cocina, suelos de baños, suelos de terraza, etc.</p>

                    <p>Un material de construcción que, aunque cuenta con años de historia, se ha vuelto mucho más
                        <strong> popular en los últimos años</strong> , especialmente para recubrir todo tipo de
                        pavimentos. A continuación
                        te descubrimos todo lo que tienes que saber de los suelos de microcemento: cómo se aplica, sus
                        principales ventajas, los tipos de microcemento que hay para suelos, consejos... y mucho más.
                    </p>


                    <h2>Beneficios del microcemento en suelos</h2>
                    <p>Aplicar microcemento en suelos ofrece <strong>innumerables ventajas.</strong> Hacemos un resumen
                        de algunos de los
                        beneficios más
                        importantes de los que puede presumir este innovador revestimiento decorativo como pavimento:
                    </p>

                    <ul>
                        <li> No eleva demasiado el nivel del suelo gracias a su grosor mínimo de entre 2 y 3 mm.</li>
                        <li>Es un pavimento continuo, lo que significa que no hay juntas de dilatación ni uniones. Toda
                            la renovación del suelo se hace de una sola pieza.</li>
                        <li>Se adhiere a cualquier tipo de material sin necesidad de retirar el soporte existente. Por
                            lo que se evita generar escombros y reformas pesadas.</li>
                        <li>Es inalterable a los rayos UV y posee muy buena resistencia térmica.</li>
                        <li>Presenta resistencias envidiables al tránsito, al desgaste por abrasión y a los productos de
                            limpieza domésticos.</li>
                        <li>No hay dos suelos de microcemento iguales gracias a la variedad de colores y acabados que
                            ofrece este revestimiento decorativo.</li>
                        <li>Puede utilizarse como pavimento interior pero también como pavimento exterior, por ejemplo
                            en terrazas.</li>
                    </ol>

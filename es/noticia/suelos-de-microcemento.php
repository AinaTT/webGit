                    <h2>Cómo instalar un suelo de microcemento</h2>

                    <p>Ahora que ya conoces todas las virtudes del microcemento para suelos, ha llegado el momento de
                        explicar cuál es el proceso de aplicación. Estos son los <strong> seis pasos</strong> a tener en
                        cuenta para
                        instalar un suelo de microcemento con éxito:</p>

                    <h3> 1. Limpiar el futuro suelo de microcemento</h3>

                    <p>El pavimento que va a recubrirse con microcemento ha de estar en perfectas condiciones. Por eso,
                        el primer paso supone limpiar el futuro suelo de microcemento de <strong> polvo, manchas,
                            grasa</strong> o
                        cualquier otro tipo de partículas; además de asegurarse que la base esté en perfectas
                        condiciones. Muy importante comprobar el grado de humedad del pavimento, habrá que esperar a que
                        seque o directamente aplicar una barrera impoxy para bloquear la humedad por capilaridad o
                        también como barrera de vapor.</p>

                    <h3>2. Imprimación</h3>

                    <p>Una vez esté limpia la superficie, aplicar una capa de imprimación sobre el pavimento para
                        <strong> potenciar la adherencia entre el soporte existente y el nuevo suelo de
                            microcemento.</strong> Si la
                        superficie base es absorbente, como puede ser el hormigón o autonivelante, se empleará <a
                            class="links"
                            href="https://www.topciment.com/es/microcemento/imprimacione-y-promotores-de-adherencia-primacem#primacem-abs">
                            PRIMACEM
                            ABS </a> ; si por el contrario es no absorbente como puede ser terrazo, azulejo o mármol, se
                        utilizará <a class="links"
                            href="https://www.topciment.com/es/microcemento/imprimacione-y-promotores-de-adherencia-primacem#primacem-plus">PRIMACEM
                            PLUS</a>.</p>

                    <h3>3. Primera capa de microcememento base</h3>

                    <p>Después de la imprimación, colocar la primera capa de microcemento base sin pigmentar. <strong>Es
                            recomendable el uso de una malla de fibra de vidrio</strong> , ya que ayudará al profesional
                        a minimizar
                        las posibilidades de que aparezcan fisuras con el tiempo. Una vez aplicada, lijar la superficie.
                    </p>

                    <h3>4. Segunda capa de microcemento base </h3>
                    <p>Una vez la superficie esté completamente seca, aplicar una segunda capa de microcemento base
                        pigmentada. En este paso, <strong> se elegirá el color de microcemento </strong> deseado para recubrir el pavimento
                        continuo y, una vez la superficie esté perfectamente cubierta, se lijará el suelo de
                        microcemento.
                    </p>

                    <h3>5. Dos capas de microcemento de terminación</h3>
                    <p>El quinto paso consiste en aplicar   <strong>dos capas de microcemento de terminación</strong> o acabado con
                        pigmento. En función del acabado y el grado de resbalicidad que se busque, se puede utilizar
                        microcemento medio o grueso. Nosotros como especialistas recomendamos dos manos de <strong>Microdeck</strong> ,
                        nuestro microcemento medio óptimo para la aplicación de microcemento en suelos.
                    </p>
                    <h3>6. Protección microcemento suelo </h3>
                    <p>El último paso de la aplicación del microcemento en suelo conforma uno de los más importantes. El
                        suelo de microcemento ya está listo, ahora falta protegerlo. Para ello se aplicarán  <strong>dos capas de
                        <a class="links"
                            href="https://www.topciment.com/es/microcemento/barniz-poliuretano-acrilico-alagua-topsealer#presealer">PRESEALER</a></strong>
                        mediante rodillo y, después, otras  <strong>dos manos de
                        <a class="links" href="https://www.topciment.com/es/microcemento/barniz-poliuretano-acrilico-alagua-topsealer">TOPSEALER
                            WT</a></strong>  . Además de
                        proteger el microcemento del suelo, se podrá impermeabilizar e incluso conseguir un acabado
                        antideslizante.
                    </p>
                    <p>
                        <a class="links" href="../microcemento/como-aplicar-microcemento-aplicacion-de-microcemento-paso-a-paso">En esta página</a>,
                        podrás informarte también de la aplicación de
                        microcemento en paredes y piscinas.</p>

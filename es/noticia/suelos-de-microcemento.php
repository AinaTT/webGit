                    <h2>Tipos de microcemento para suelos </h2>

                    <p>Si hay una particularidad que define a la perfección al microcemento es su versatilidad, ya que
                        existen diferentes tipos según la textura y acabado que uno quiera obtener: monocomponente,
                        bicomponente y listo al uso.   <strong>En Topciment contamos hasta con cuatro sistemas de microcemento
                        para suelos</strong> distintos:
                    </p>
                    <p class="h3">Microcemento bicomponente</p>
                    <p>El microcemento bicomponente (cemento + resina) es uno de los más utilizados sin duda en los
                        suelos de microcemento. Por ello, hemos creado dos líneas: <a class="links"
                            href="../microcemento/microcemento-bicomponente-sttandard">Sttandard</a>, la tradicional y
                        una de las más reclamadas; y <a class="links"
                            href="../microcemento/microcemento-bicomponente-unlimitted">Unlimitted</a>, la nueva gama
                        que proporciona acabados más
                        suaves y resistencias mecánicas mayores.
                    </p>
                    <p class="h3">Microcemento monocomponente</p>
                    <p>A base de agua y cemento está formulado <strong><a class="links"
                            href="../microcemento/microcemento-monocomponente-evoluttion">Evoluttion</a></strong> , nuestro
                        microcemento monocomponente
                        para suelos con resistencias mecánicas excelentes y una gran trabajabilidad. Se pueden diseñar
                        todo tipo de suelos de microcemento con él.
                    </p>

                    <p class="h3">Microcemento listo al uso</p>
                    <p> <a class="links" href="../microcemento/microcemento-listo-al-uso-efectto">Efectto Quartz </a> es nuestro
                        microcemento listo al uso para suelos. Gracias a su amplia gama
                        de colores y sus cuatro granulometrías, se puede crear cualquier suelo de microcemento que
                        puedas imaginar.
                    </p>

                    <h2>Suelos de microcemento en baños y cocinas</h2>

                    <p>El microcemento puede utilizarse tanto como pavimento exterior como pavimento interior en
                        cualquier estancia. De entre sus múltiples cualidades, su capacidad de ser impermeable (con el
                        uso de un sellador especial para ello) y su excelente resistencia térmica, al tránsito y a las
                        manchas convierten a este revestimiento en un material idóneo sobre todo  a para cocinas <!-- (link
                        noticia nueva)  -->y baños <!-- (link noticia nueva) -->.
                    </p>
                    <p>Gracias a la gran cantidad de colores de microcemento que existen, se puede crear infinidad de
                        pavimentos. En nuestra <a class="links" href="../microcemento/galeria">galería</a> encontrarás
                        múltiples suelos de microcemento en
                        baños y cocinas.
                    </p>

                    <h2>Colores para suelos de microcemento</h2>

                    <p>El microcemento blanco es sin duda uno de los colores más demandados debido a un marcado estilo
                        minimalista cada vez más presente en la decoración de interiores de viviendas particulares y
                        también de locales comerciales. Pero los suelos de microcemento gris tampoco se quedan atrás. Y
                        es que el estilo industrial cada vez gana más adeptos a su causa, un estilo que da la sensación
                        que no pasará de moda por mucho que pasen los años ya que encaja a la perfección con oficinas,
                        grandes superficies y naves y, también, casas.
                    </p>

                    <p>Aunque el blanco y gris sean los colores de microcemento por excelencia, existe un amplio abanico
                        de posibilidades. En Topciment contamos con múltiples colores para suelos de microcemento en
                        función del carácter que se quiera imprimir a la estancia en la que se va a aplicar el suelo de
                        microcemento. Hemos diseñado colores específicos según  <a class="links" href="https://www.topciment.com/es/microcemento/productos"> los sistemas de microcementos que
                        fabricamos</a> .
                    </p>

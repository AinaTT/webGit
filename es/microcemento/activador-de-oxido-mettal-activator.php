<h2>Cómo aplicar el activador de óxido Mettal Activator</h2>

<p>
    La aplicación del activador de óxido Mettal Activator varía en función de la superficie sobre la que se quiere conseguir un específico acabado óxido metálico concreto. A continuación explicamos cómo aplicar el activador de óxido Mettal Activator sobre nuestros revestimientos y pinturas metálicas Classic Mettal y Pure Mettal. Para conocer el resto de posibilidades de aplicación que ofrece nuestro activador de óxido, ver la ficha técnica.

</p>    
<h3 class="en3"> Aplicación activador de óxido Mettal Activator sobre Classic Mettal y Pure Mettal</h3>
                                <p>
                              <strong>      1. </strong> 24 horas después de la última mano de Pure Mettal o Classic Mettal, lijar la superficie con grano 120 para facilitar la oxidación de las partículas metálicas.

                                </p>    
<p>
 <strong>   2. </strong> Aplicar primera mano del activador de óxido Mettal Activator. Dejar actuar entre 3 y 4 horas.

</p>    
<p>
<strong>    3. </strong> Aplicar segunda mano del activador de óxido Mettal Activator hasta obtener el acabado óxido metálico deseado. Dejar secar 24 horas.

</p>    
<p>
<strong>    4. </strong> Sellar la superficie con dos manos del barniz acrílico al agua Presealer, dejando 4 horas de secado entre capas.

</p>    
<p>
   <strong> 5. </strong> Proteger finalmente la superficie con dos manos del barniz de poliuretano al agua Topsealer WT One Coat, dejando 24 horas de secado entre capas.

</p>    

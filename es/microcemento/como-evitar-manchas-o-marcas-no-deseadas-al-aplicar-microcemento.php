                <h1> Cómo evitar manchas o marcas no deseadas al aplicar microcemento</h1>
                <p>
                    <strong>Una mala aplicación del microcemento puede llevar a la aparición de manchas y marchas no deseadas.</strong>
                    Problemas visuales que perjudiquen al acabado final. Si has llegado hasta aquí es porque te preocupa
                    esta circunstancia y, sobre todo, porque quieres saber cómo puedes evitarlas a toda costa.

                </p>
                <p>
                    Estás en el lugar correcto. En este artículo no solo<strong> ahondaremos en los motivos  más comunes</strong> que
                    producen esas temidas marcas y manchas a causa de una mala instalación del microcemento alisado.

                </p>
                <p>
                    Además nuestros <strong>aplicadores experimentados </strong>te darán una serie de<strong> tips, consejos y soluciones</strong> a cada
                    uno de los problemas para que no aparezcan imperfecciones no deseadas. ¡Toma nota!
                </p>
                <h2>Marcas de las juntas de los azulejos al aplicar microcemento</h2>
                <p>
                    ¿Quieres aplicar el microcemento sobre azulejos? Estos son los <strong>tres puntos a tener en consideración</strong>
                    para evitar que se marquen las juntas de los azulejos.

                </p>
                <h3>1. Prestar atención a la diferencia de absorción entre las baldosas y juntas</h3>
                <p>
                    La resina contenida en el microcemento se absorbe de manera diferente. Si no se toma ninguna
                    precaución corremos el riesgo de que aparezcan en forma de espectro visual al secar (patrón visual).
                    La humedad en las juntas también provoca este defecto.

                </p>
                <p>
                  <figure class="figure mt-4">
                        <img src="https://www.topciment.com/imagenes/marcas-de-las-juntas-de-los-azulejos-por-humedad.jpg" class="figure-img img-fluid " title="marcas de las juntas de los azulejos por humedad" alt="marcas de las juntas de los azulejos por humedad" width="auto" height="auto">
                        <figcaption class="figure-caption">Suelo de microcemento en el que se marcan las juntas de los
                            azulejos a causa de la humedad.</figcaption>
                    </figure>

                </p>
                <h3>2. Las juntas de los azulejos suelen ser huecas</h3>
                <p>
                    Las juntas son generalmente huecas, no están en el mismo plano que el azulejo. Asimismo, la
                    retracción del Microcemento una vez seco crea un relieve en la zona de juntas (patrón en relieve).
                    Por todo esto, con nuestros sistemas de microcemento creamos una capa que aísla y nivela.

                </p>
                <h3>3. Dejar secar 24 horas antes de aplicar microcemento</h3>
                <p>
                    Es muy importante que, tras rellenar las juntas de los azulejos, se deje secar 24 horas antes de
                    comenzar con la aplicación de la base de microcemento.

                </p>
              
                <h2> Marcas de la llana en el microcemento</h2>

                <p>
                    Tendremos que <strong>aplicar el microcemento sin dejar rebabas o relieves</strong> que evidencien el paso de la
                    llana, lijando estas imperfecciones en cada mano. Si aplicamos microcemento sobre una capa con
                    rebabas, se copiarán estos relieves.

                </p>
                <p>
                    Con la llana de acero y un exceso de presión estas imperfecciones provocan el efecto "quemado”,
                    manchas oscuras que reflejan la falta de lijado de la capa inferior de microcemento.

                </p>
                <figure class="figure mt-4">
                        <img src="https://www.topciment.com/imagenes/marca-de-quemado-de-llana-1.jpg" class="figure-img img-fluid " title="marca de quemado de llana" alt="marca de quemado de llana" width="auto" height="auto">
                        <figcaption class="figure-caption">Suelo de microcemento en el que se observa la marca de
                            quemado de la llana.</figcaption>
                    </figure>
               
                <h2> Irregularidades superficies microcemento</h2>

                <p>
                    <strong>Después de la aplicación de cada capa de microcemento, hay que lijar la superficie.</strong> Un paso
                    innegociable para evitar el efecto descrito en el apartado anterior. Efectuaremos un suave
                    desbastado con una lijadora roto-orbital de bajas revoluciones o mediante guante de lijado, y lijas
                    de carburo de silicio.

                </p>
                <p>
                    Aproximadamente a las 3 horas de la aplicación del microcemento alisado, se aprecia un cambio de
                    tono del revestimiento volviéndose más claro. Un cambio de tonalidad que indica que el microcemento
                    es transitable y lo suficientemente duro para proceder al lijado. Cabe indicar que la temperatura,
                    la humedad ambiente y la ventilación de la estancia influyen en este plazo de tiempo.

                </p>
                <p>
                    Recomendamos lijar <strong>antes de las 12 horas</strong> de la aplicación del microcemento para que el recubrimiento
                    no haya alcanzado una dureza excesiva y sea más complicado este paso.

                </p>
                <h2> Manchas microcemento por exceso de lijado</h2>

                <p>
                    Cuando efectuemos el lijado de la última mano de microcemento debemos asegurarnos de trabajar el
                    material suavemente. Con una mano cogeremos la lijadora o el guante de lijado y con la otra iremos
                    comprobando con el tacto si el lijado es correcto. <strong>Si trabajamos la superficie adecuadamente evitaremos tres tipos de manchas características: círculos, curvas de nivel y marcas negras.</strong>

                </p>
                <p>
                    La lijadora tendrá que ser del tipo roto-orbital para evitar dejar círculos. Procuraremos mantenerla
                    en movimiento de forma constante sin incidir de forma prolongada en el mismo sitio. Si el lijado
                    fuese excesivo acabaríamos viendo el microcemento alisado de la anterior mano y dejando unas "curvas
                    de nivel" características que indicarían que hemos pasado a la capa inferior. En cuanto la lija se
                    desgaste tendremos que cambiarla, si no realizamos el cambio, la lija pasa de lijar a pulir dejando
                    marcas negras en el microcemento.

                </p>
                <h2>Marcas microcemento por exceso de bruñido</h2>
                <p>
                    Si el acabado de microcemento no es el deseado, lo más adecuado es aplicar una capa de microcemento
                    alisado adicional. Ya que <strong>realizar retoques sobre la superficie ya seca</strong> dejará marcas notables y muy
                    poco estéticas.

                </p>
                <p>
                  <strong>  Repasar en exceso una zona con la llana dejará marcas oscuras.</strong> Esto puede ocurrir por ejemplo,
                    cuando se acumule material en suelos al final del recorrido de la aplicación obligándonos a recoger
                    el microcemento alisado sobrante y a trabajarlo de más con la llana.

                </p>
                <p>
                   <strong> Si repasas en exceso una zona lo que haces es forzar el secado. </strong>Este problema se refleja en el
                    blanqueamiento de la zona. Por ejemplo: Una pared negra podría tener manchas grises mucho más claras
                    que el color de microcemento elegido.

                </p>
                <p>
                    Si el aplicador repasa mucho toda la superficie o aprieta demasiado el soporte de microcemento
                    alisado puede quedar de un color más claro. Es muy típico ver una pared negra que parece gris.


                </p>



                    <div id="mitadTextoIzquierda">
                        <h2>Condiciones previas a la aplicación</h2>
                        <h3>Limpieza.</h3>
                        <p> El soporte debe estar limpio, libre de polvo y grasas.</p>
                        <h3> Planicidad.</h3>
                        <p> El microcemento se aplica en capas no superiores a 1mm, y la suma de éstas no superará los cuatro
                            milímetros, por lo que no podremos resolver cuestiones de planicidad con este material, deberemos
                            solventar las irregularidades del soporte antes de iniciar la aplicación. El <a href="../microcementos">Microcemento</a> puede ser aplicado en suelos, paredes e incluso en
                            techos, el resultado final dependerá del soporte y del trabajo del aplicador.</p>
                        <h3> Consistencia.</h3>
                        <p> La base de aplicación debe ser estable. No son adecuados los soportes con morteros pulverulentos o
                            azulejos sueltos, en función de su estado habrá que retirarlos o consolidarlos con morteros de
                            fraguado rápido, de retracción compensada. </p>
                        <h3> Libre de humedades.</h3>
                        <p> En caso de haber humedad en el soporte no podremos aplicar microcemento. Será necesario determinar
                            primero su origen y eliminarla, aconsejamos emplear un medidor de humedad con el fin de comprobar
                            que la humedad nunca supere el 5%.</p>
                        <p> El soporte deberá haber pasado por un proceso de fraguado suficiente. Debemos tener en cuenta que
                            la temperatura y la humedad relativa del ambiente junto con la ventilación del espacio de trabajo,
                            inciden en el proceso de secado de los morteros y acelera o retrasa el proceso de fraguado.</p>
                        <h3> Protección de la zona de trabajo</h3>
                        <p> Debemos proteger la zona de trabajo adecuadamente y las superficies que no vayamos a revestir, el
                            microcemento se adhiere con mucha fuerza y es difícil de limpiar cuando seca. Emplearemos material
                            de protección común en los trabajos de pintura, como cintas de pintor, papel para proteger,
                            plásticos cubretodo...</p>
                        <h3> Soporte recomendado en el sistema de piscinas</h3>
                        <p> Se aplica sobre hormigón proyectado o gunitado, estando el soporte limpio, consolidado y en buenas
                            condiciones de planimetría. <strong>No se debe aplicar sobre gresite.</strong></p>
                    </div>
                    <div id="mitadTextoDerecha">
                        <h2>Preparación del soporte</h2>
                        <p>La aplicación del microcemento se iniciará con la imprimación del soporte usando promotores de
                            adherencia y con la colocación de la malla de fibra de vidrio. </p>
                        <h3> Imprimación con promotores de adherencia</h3>
                        <p> Los promotores de adherencia cumplen varias funciones, aseguran una mejor adherencia sobre
                            cualquier tipo de soporte, aumentan la cohesión de éste, mejoran la velocidad de aplicación de la
                            primera mano del microcemento, y evitan la pérdida prematura de humedad del micro-mortero.</p>
                        <p> Los soportes tienen diferente capacidad de absorción, en consecuencia debemos emplear soluciones
                            para cada una de ellos, promotores para superficies no absorbentes, para superficies absorbentes,
                            para superficies que necesitan cierto grado de consolidación o promotores para superficies que
                            puedan tener problemas de humedad. </p>
                        <h3> Microcemento sobre gres, terrazo, baldosa hidráulica o mármol</h3>
                        <p> Para revestir este tipo de soportes poco o nada porosos debemos emplear Primacem Plus, promotor
                            para superficies no absorbentes, el proceso será el siguiente:</p>
                        <ul>
                            <li> Rellenar las juntas con Microbase y dejar secar 24 h. Si las juntas son muy finas podemos
                                emplear Microdeck o incluso Microfino. </li>
                            <li> Aplicar una capa de Primacem PLUS mediante rodillo junto con la malla Builtex. </li>
                            <li> Dejar secar mínimo 30 minutos y aplicar la primera mano de Microbase. </li>
                            <li> Dejar secar 24 h, darle una mano de Primacem PLUS y aplicar la segunda mano de Microbase.</li>
                        </ul>
                        <h2>Aquí vemos un sistema de aplicación diferente con Impoxi</h2>
                        <iframe width="100%" height="315" src="https://www.youtube.com/embed/8ugv0YVaXsM" frameborder="0" allowfullscreen></iframe>
                        <h3> Microcemento sobre yeso, pladur, tableros de DM</h3>
                        <p> Sobre soportes absorbentes emplearemos Primacem Abs, siguiendo estos pasos:</p>
                        <ul>
                            <li>Aplicar la imprimación con un rodillo común de pintor.</li>
                            <li>Dejar actuar el puente de unión un mínimo de 30 minutos y un máximo de 24 horas antes de colocar
                                la malla junto con la primera mano de microcemento.</li>
                        </ul>
                        <h3><br>
                            Microcemento sobre mortero de cemento, hormigón proyectado o gunitado.</h3>
                        <p> Para superficies absorbentes que puedan necesitar un cierto grado de consolidación, debemos
                            proceder de la siguiente manera:</p>
                        <ul>
                            <li> Aplicar la imprimación Acricem con un rodillo común de pintor.<br>
                            </li>
                            <li>Dejar actuar el puente de unión un mínimo de 30 minutos y un máximo de 24 horas antes de colocar
                                la malla junto con la primera mano de microcemento. </li>
                        </ul>
                        <h3> Microcemento sobre soportes que puedan presentar humedad</h3>
                        <p> En soportes consolidados donde pueda aparecer humedad deberemos aplicar una barrera a base de
                            resinas epoxi para prevenir que la humedad afecte al microcemento. Impoxy resuelve los problemas de
                            humedad permitiendo aplicar sobre el soporte revestimientos de microcemento.</p>
                        <ul>
                            <li> Imprimación a rodillo con Impoxy a 0,35 kg/m2</li>
                            <li> Espolvorear con arena de sílice seca de 0,3-0,8 mm a 1kg/m2</li>
                            <li> Dejar secar 24 horas</li>
                            <li> Retirar la arena de sílice sobrante mediante barrido y aspirado.</li>
                            <li> Aplicación de Impoxy mediante llana a 3 kg/m2</li>
                        </ul>
                        <h3> Instalación de la malla</h3>
                        <p> El microcemento Topciment no fisura, siempre y cuando la base sobre la que se ha colocado no lo
                            haga. La malla Topciment es un seguro adicional en las aplicaciones de microcemento, es una malla de
                            altas prestaciones, flexible, resistente y muy fácil de trabajar, se recubre con Microbase con
                            facilidad, sin hacer bolsas. La malla permite desolidarizar el revestimiento del soporte de
                            aplicación.</p>
                        <p>Fijación de la malla:</p>
                        <p> La malla se puede fijar con el promotor de adherencia Primacem Plus, o cuando se utilice como
                            promotor Primacem ABS o la resina Acricem, con la primera mano de Microcemento Base. Se deberán
                            solapar las tiras de malla, no más de 1cm, y aplicaremos siempre dos manos de Microbase para cubrir completamente el
                            entramado.</p>

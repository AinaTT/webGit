                <h2 class="mt-5">Precio</h2>
                <p> El precio de los cursos de formación Topciment
                    depende de
                    varios factores como el <strong>nivel de
                        especialización</strong> .</p>
                <p>
                    El coste
                    será inferior en el curso de iniciación, donde se realiza una
                    mera introducción de los revestimientos continuos, y
                    superior en los cursos avanzados.
                </p>
 
                <p> <strong>El coste del material,</strong> que Topciment
                    proporciona a cada profesional para la realización del
                    curso, es otro de los elementos que influyen en el
                    importe. El material se encarece conforme el nivel de
                    especialización es más elevado.</p>
                <p><strong>El profesorado </strong> también repercute en
                    el precio, ya que algunos de los instructores residen
                    fuera de España y vienen expresamente a impartir el
                    curso a Valencia. Este desplazamiento supone un coste
                    que se refleja en el precio final del curso.</p>


              <h3>¿Puede aplicarse microcemento sobre suelo radiante?</h3>

            </a>
            <p style="font-size:16px;">Sí, pero antes de aplicarlo deberemos realizar el <strong>protocolo de
                encendido</strong> de la calefacción para evitar que las fisuras producidas por los cambios de
              temperatura en la solera se reproduzcan en el microcemento.</p>

            <a href="javascript:MostrarOcultar('texto1_4');" id="enlace1_1" style="color:#000; text-decoration:none;"><strong>+información</strong></a>
          </div>
          <div class="ocultable" id="texto1_4" style="text-align:left;font-size:16px;">
            <p><strong>El protocolo de encendido será el siguiente:</strong></p>
            <p>1. Encender la calefacción como mínimo 4 semanas después de la colocación del mortero de cemento.</p>
            <p>2. Mantener el agua de entrada a los circuitos a 25 °C durante 2 o 3 días. A continuación, aumentar
              progresivamente la temperatura del agua hasta los 45 °C y mantenerla durante varios días.</p>
            <p>3. Apagar la calefacción como mínimo 48 h antes de iniciar la instalación del microcemento. La
              temperatura máxima del suelo deberá ser de 18 °C. </p>
            <p><strong>Los pavimentos de microcemento deben aplicarse</strong> sobre morteros cementosos que hayan
              alcanzado una fase avanzada de fraguado. Esto ocurre transcurridas tres o cuatros semanas, en función de
              la climatología. En todo caso, debemos asegurarnos de que el mortero esté prácticamente seco. Para ello
              deberá haber en el soporte un máximo de 5 % de humedad que comprobaremos con un medidor de humedad
              relativa. </p>
            <p>Recomendamos utilizar morteros de cemento autonivelantes de calidad.</p>
            <p>No son adecuados los soportes con morteros pulverulentos ya que el estado de la base de aplicación debe
              ser consistente y de lo contrario habrá que consolidarlo.</p>
            <p><strong>Después de aplicar el microcemento</strong>, deberemos esperar al menos 48 horas antes de volver
              a encender la calefacción de forma gradual (+5 °C cada día).</p>
            <p>Cambie siempre la temperatura de forma gradual al inicio y al final del período de uso de la calefacción.
            </p>
            <p>Asegúrese de que la humedad relativa del aire en las habitaciones no sea demasiado baja.</p>
            <p>Evite siempre la acumulación de calor causada por alfombras y moquetas o la falta de espacio entre los
              muebles y el suelo.</p>
            <p><strong>¡Importante!</strong> Entre la solera y los paramentos verticales siempre habrá junta de
              dilatación para que esta pueda dilatar. En caso de no respetar esta indicación, podrían aparecer fisuras.
            </p>


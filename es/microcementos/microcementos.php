          <div style="border-top:thin solid #000; padding:10px 0;  text-align:left;"><a href="javascript:MostrarOcultar('texto1_1');" id="enlace1_1" style="color:#000; text-decoration:none;">
              <h3>¿Cuál es la resistencia y dureza del microcemento?
              </h3>
            </a>
            <p style="font-size:16px;">Pese a su bajo espesor, una de las propiedades de este material es su elevada
              resistencia y dureza, Sttandard Microdeck, Sttandard Microstone y Evoluttion Microdeck pueden emplearse
              incluso en zonas de alto tránsito.</p>
            <a href="javascript:MostrarOcultar('texto1_1');" id="enlace1_1" style="color:#000; text-decoration:none;"><strong>+información</strong></a>
          </div>
          <div class="ocultable" id="texto1_1" style="text-align:left;font-size:16px;">
            <p>En la resistencia y la dureza del microcemento influyen varios factores como son el soporte sobre el que
              se aplicará, el tipo de microcemento, así como el sellador para microcemento.</p>
            <p>Podemos diferenciar entre dos tipos de resistencia:</p>

            <p><strong>Resistencias mecánicas</strong><br>
              <strong>A golpes:</strong> Aunque es un material relativamente duro, la resistencia a un impacto depende
              en cierta medida de la dureza del soporte. El microcemento, debido a su bajo espesor, se deformará ante un
              fuerte golpe si el soporte se deforma. Un ejemplo sería el microcemento aplicado sobre yeso. Si se deforma
              la base por ser poco resistente, el microcemento se deformará también. Mientras que sobre soportes como el
              terrazo, la resistencia será mayor.
            </p>
            <p><strong>A la abrasión y al desgaste:</strong> Esta resistencia depende tanto del tipo de microcemento
              como del sellador. Topciment dispone de dos revestimientos continuos para pavimentos: Sttandard MicroDeck
              y
              MicroStone,Evoluttion Microdeck y Efectto Medium. Todos son revestimientos con áridos seleccionados de
              mayor
              tamaño de partícula, por lo que los hacen más resistentes a la compresión y a la abrasión.

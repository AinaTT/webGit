                        <figcaption class="figure-caption">Salón con microcemento Topciment en suelo y paredes.</figcaption>
                    </figure>
                    <h2 class="en3"> 3. Alto valor decorativo</h2>
                    <p>
                        El microcemento es <strong>aplicable tanto a interiores como exteriores</strong>
                        , se adapta a la
                        perfección a suelos, paredes, techos,
                        terrazas, piscinas e incluso muebles. Por ello el microcemento posee un alto
                        valor decorativo porque si puedes
                        imaginarlo, puedes hacerlo.
                    </p>
                    <h2 class="en3"> 4. Gran resistencia</h2>
                    <p>

                        Si apuestas por el microcemento, apuestas también por la resistencia, ya que es
                        un material que aguanta a la perfección
                        frente <strong>a la fuerza mecánica, la abrasión o el efecto de los rayos
                            ultravioleta</strong> ,
                        entre otros. No todos los materiales
                        pueden decir lo mismo.
                    </p>

                    <h2 class="en3"> 5. Instalación rápida y limpia
                    </h2>
                    <p>
                        Uno de los mayores quebraderos de cabeza a la hora de hacer reformas en casa es
                        pensar en los escombros que vas
                        acumulando a medida que avanzas en la obra. ¡Problema resuelto! Con el
                        microcemento no generarás <strong>ni un solo escombro</strong> y
                        lo que es igual de importante, es muy rápido y cómodo de instalar.
                    </p>


                    <figure class="figure">
                        <img src="https://www.topciment.com/imagenes/news/MICRO_CEMENT21168.jpg" class="figure-img img-fluid " title="Cocina microcemento " alt="  Cocina con microcemento gris Topciment">
                        <figcaption class="figure-caption">Cocina de microcemento gris de en suelo de Topciment.</figcaption>
                    </figure>

                    <h2 class="en3"> 6. Limpieza y mantenimiento
                    </h2>
                    <p>
                        En relación al punto anterior, la limpieza y mantenimiento son de lo más
                        sencillos. <strong>Con apenas agua y jabón neutro</strong>
                        lograrás que el microcemento esté siempre en perfecto estado y que sus
                        propiedades sigan intactas.
                    </p>
                    <p> Para lograr una óptima limpieza de este revestimiento decorativo, descubre los
                        <a class="links" href="../sistemas/limpieza-y-cuidado-del-microcemento.html">limpiadores
                        </a> que ha diseñado el fabricante de microcemento Topciment.
                    </p>

                    <h2 class="en3"> 7. Grosor
                    </h2>
                    <p>A diferencia del cemento alisado, el grosor del microcemento es ínfimo, ya que se
                        aplica una <strong>fina capa entre 2 y 3 mm.</strong>
                        De esta manera, aunque se coloque encima de cualquier otra superficie el
                        resultado final es imperceptible.
                    </p>

                    <h2 class="en3"> 8. Relación calidad-precio
                    </h2>

                    <p>
                        En último lugar, y no por ello menos importante, la joya de la Corona.
                        <strong>El hecho
                            de no ser necesario eliminar la anterior
                            superficie</strong> para instalar el microcemento <strong>reduce
                            drásticamente</strong> <strong>el coste total</strong> de
                        una obra o reforma que no pierde ni un
                        ápice de distinción y elegancia.
                    </p>


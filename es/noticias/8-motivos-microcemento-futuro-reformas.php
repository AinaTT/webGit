                        enamorarán y con la que lograrás la casa de tus sueños.</p>




                    <h2 class="en3">1. Infinidad de colores
                    </h2>
                    <p>La mezcla de los diferentes tipos de pigmentos de color del microcemento ofrece
                        al usuario una multiplicidad de colores
                        con los que dar rienda suelta a su creatividad. <strong>Ilimitadas
                            combinaciones</strong> que
                        dotarán de vida propia a cada estancia o
                        mueble y que reforzarán la personalidad de un hogar único e irrepetible.
                    </p>


                    <h2 class="en3"> 2. Compatible con todas las superficies</h2>
                    <p>Una de las grandes ventajas del microcemento es su posibilidad de adaptarse
                        prácticamente a todas las superficies
                        existentes <strong>gracias a una gran adherencia</strong> , por lo que no es
                        necesario eliminar el
                        material anterior como ocurre en muchas
                        obras y reformas del hogar. ¡Y nada de tener miedo por si se despega! No
                        ocurrirá.
                    </p>


                    <figure class="figure">

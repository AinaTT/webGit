                <h2> Irregolarità sulle superfici in microcemento</h2>
                <p> Dopo aver applicato ogni strato di microcemento, la superficie deve essere levigata
                    Un passo essenziale per evitare l'effetto descritto nella sezione precedente. Faremo una
                    sgrossatura morbida con una levigatrice roto-orbitale a basso numero di giri o per mezzo di un
                    guanto da levigatura, e carta vetrata al carburo di silicio. </p>
                <p>Circa 3 ore dopo l'applicazione del microcemento levigato, si può notare un cambiamento nel tono del
                    rivestimento, che diventa più chiaro. Un cambiamento di tono che indica che il microcemento è
                    passabile e abbastanza duro per procedere alla levigatura. Va notato che la temperatura, l'umidità e
                    la ventilazione della stanza influenzano questo periodo di tempo. </p>
                <p>Si consiglia di carteggiare prima di 12 ore dall'applicazione del microcemento in
                    modo che il rivestimento non abbia raggiunto una durezza eccessiva e questo passaggio sia più
                    complicato. </p>
                <h2>Macchie di microcemento dovute a una levigatura eccessiva</h2>
                <p>Quando si leviga l'ultimo strato di microcemento dobbiamo assicurarci di lavorare il materiale in
                    modo uniforme. Con una mano terremo la levigatrice o il guanto da levigatura e con l'altra
                    controlleremo con il tatto se la levigatura è corretta. Se lavoriamo bene la superficie
                        eviteremo tre tipi di macchie caratteristiche: cerchi, linee di contorno e segni neri.
                </p>
                <p>La levigatrice dovrà essere di tipo roto-orbitale per evitare di lasciare cerchi. Cercheremo di
                    mantenerlo in costante movimento senza un impatto prolungato nello stesso posto. Se la levigatura
                    fosse eccessiva, finiremmo per vedere il microcemento levigato della mano precedente e lasceremmo
                    alcune caratteristiche "linee di contorno" che indicherebbero che siamo passati allo strato
                    inferiore. Appena la carta vetrata è consumata dovremo cambiarla, se non facciamo il cambio, la
                    carta vetrata passa dalla levigatura alla lucidatura lasciando segni neri sul microcemento. </p>
                <h2>Segni di microcemento per eccesso di brunitura</h2>
                <p>Se la finitura del microcemento non è quella desiderata, la cosa più appropriata da fare è applicare
                    uno strato di microcemento lisciato supplementare. Poiché toccare sulla superficie già
                        asciutta lascerà dei segni evidenti e poco estetici. </p>
                </p>
                <p> L'eccessiva frattazzatura di un'area lascerà segni scuri. Questo può accadere, per
                    esempio, quando il materiale si accumula sui pavimenti alla fine del percorso di applicazione,
                    costringendoci a raccogliere il microcemento levigato in eccesso e a lavorarlo troppo con la
                    cazzuola. </p>
                <p> Se si passa troppo sopra una zona, ciò che si fa è forzare l'asciugatura. Questo
                    problema si riflette nello sbiancamento della zona. Per esempio: un muro nero potrebbe avere macchie
                    grigie molto più chiare del colore del microcemento scelto. </p>
                <p>Se l'applicatore passa troppo su tutta la superficie o spreme troppo il supporto di microcemento
                    levigato, il colore può essere più chiaro. È molto tipico vedere un muro nero che sembra grigio.
                </p>


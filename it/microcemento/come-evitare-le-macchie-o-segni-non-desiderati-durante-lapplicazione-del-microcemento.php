                <h2>I segni delle fughe delle piastrelle quando si applica il microcemento</h2>
                <p>Vuoi applicare il microcemento sulle piastrelle? Questi sono i tre punti da prendere in
                        considerazione per evitare di segnare le fughe delle piastrelle. </p>
                <h3>1. Fare attenzione alla differenza di assorbimento tra piastrelle e giunti</h3>
                <p>La resina contenuta nel microcemento viene assorbita in modo diverso. Se non si prendono precauzioni,
                    c'è il rischio che appaiano sotto forma di uno spettro visivo sull'asciugatura (pattern visivo).
                    Anche l'umidità nei giunti causa questo difetto. </p>
                <p>
                    <figure class="figure mt-4">
                        <img src="https://www.topciment.com/imagenes/marcas-de-las-juntas-de-los-azulejos-por-humedad.jpg"
                            class="figure-img img-fluid " title="tile joint marks due to moisture"
                            alt="tile joint marks due to moisture" width="auto" height="auto">
                        <figcaption class="figure-caption">Pavimento in microcemento dove i giunti delle piastrelle sono
                            segnati dall'umidità.</figcaption>
                    </figure>
                </p>
                <h3>2. Le fughe delle piastrelle sono di solito cave</h3>
                <p>Le fughe sono di solito cave, non sono sullo stesso piano della piastrella. Inoltre, il
                    restringimento di Microcemento una volta asciutto crea un rilievo nell'area dell'articolazione
                    (modello a rilievo). Per tutte queste ragioni, con i nostri sistemi di microcemento creiamo uno
                    strato che isola e livella. </p>
                <h3>3. Lasciare asciugare 24 ore prima di applicare il microcemento</h3>
                <p>È molto importante che, dopo aver riempito le fughe delle piastrelle, si lasci asciugare 24 ore prima
                    di iniziare l'applicazione della base di microcemento. </p>
                <h2>Segni di cazzuola sul microcemento</h2>
                <p>Dovremo applicare il microcemento senza lasciare bave o rilievi che mostrano il
                    passaggio della cazzuola, levigando queste imperfezioni in ogni mano. Se applichiamo il microcemento
                    su uno strato con bave, questi rilievi saranno copiati. </p>
                <p>Con la spatola d'acciaio e un eccesso di pressione queste imperfezioni causano l'effetto "bruciato",
                    macchie scure che riflettono la mancanza di levigatura dello strato inferiore di microcemento. </p>
                <figure class="figure mt-4">
                    <img src="https://www.topciment.com/imagenes/marca-de-quemado-de-llana-1.jpg"

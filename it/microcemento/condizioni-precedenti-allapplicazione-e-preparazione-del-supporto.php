                        <h2>Condizioni prima dell'applicazione</h2>
                        <h3>Pulizia.</h3>
                        <p>Il supporto deve essere pulito, privo di polvere e grasso.</p>
                        <h3>Piattezza.</h3>
                        <p>Il microcemento si applica in strati non superiori a 1 mm, e la somma di questi non supererà
                            i quattro millimetri, quindi non possiamo risolvere problemi di planarità con questo
                            materiale, dobbiamo risolvere le irregolarità del supporto prima di iniziare l'applicazione.
                            Il <a href="../microcementi">Microcemento</a> può essere applicato su pavimenti, pareti e
                            anche soffitti, il risultato finale dipenderà dal substrato e dal lavoro dell'applicatore.
                        </p>
                        <h3> Consistenza.</h3>
                        <p>La base di applicazione deve essere stabile. I sottofondi con malte polverose o piastrelle
                            sciolte non sono adatti, a seconda del loro stato dovranno essere rimossi o consolidati con
                            malte a presa rapida, con ritiro compensato. </p>
                        <h3>Senza umidità.</h3>
                        <p>Se c'è umidità nel substrato non possiamo applicare il microcemento. Sarà necessario prima
                            determinare la sua origine ed eliminarla, si consiglia di utilizzare un misuratore di
                            umidità al fine di verificare che l'umidità non superi mai il 5%.</p>
                        <p> Il supporto deve essere passato attraverso un processo di impostazione sufficiente. Bisogna
                            tener conto che la temperatura e l'umidità relativa dell'ambiente insieme alla ventilazione
                            dello spazio di lavoro, influenzano il processo di essiccazione delle malte e accelerano o
                            ritardano il processo di presa. </p>

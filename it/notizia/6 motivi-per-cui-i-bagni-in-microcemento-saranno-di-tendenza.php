                                <h3>4. Pareti e pavimenti in microcemento nei bagni</h3>
                                <p>L'uso più diffuso del microcemento non sarà un'eccezione nel caso dei bagni.
                                    Rivestire con microcemento le pareti, ma soprattutto i pavimenti del bagno è una
                                    delle opzioni preferite. Un tipo di pavimentazione molto da prendere in
                                    considerazione perché come detto sopra, può diventare antiscivolo. </p>
                                <h3> 5. Lavabi in microcemento</h3>
                                <p>Una delle migliori idee per applicare il microcemento nei bagni è costruire un lavabo
                                    per la sua bassa manutenzione, funzionalità, design ed estetica moderna. Qualunque
                                    sia il colore del microcemento, la finitura finale sarà molto bella, liscia e
                                    piacevole al tatto. </p>
                                <p>Quando si fabbrica un lavabo in microcemento, bisogna tenere conto di una serie di
                                    considerazioni che svilupperemo qui di seguito. </p>
                                <figure class="figure mt-4">
                                    <img src="https://www.topciment.com/imagenes/news/lavabo-bano-microcemento.jpg"
                                        class="figure-img img-fluid " title="Lavabo in microcemento ardesia in Belgio"

                                <h4>1. Lavabo in microcemento con una buona pendenza verso lo scarico</h4>
                                <p>È necessario assicurarsi che il futuro lavabo in microcemento abbia una buona caduta
                                    o pendenza verso lo scarico per evitare il ristagno dell'acqua, che sarebbe fatale.
                                    Assicurandovi di questo, eviterete la maggior parte dei problemi quando applicate il
                                    microcemento a un lavabo. </p>
                                <h4>2. Rinforzare i bordi del lavabo in microcemento</h4>
                                <p>Uno dei consigli dei nostri esperti, anche se a prima vista non necessario, è quello
                                    di rinforzare i bordi del lavabo in microcemento con profili di alluminio. In questo
                                    modo, il lavabo rivestito di microcemento levigato ha una qualità, una consistenza e
                                    un design in più. </p>
                                <h4> 3. Impermeabilità del lavabo in microcemento</h4> Uno dei grandi vantaggi di creare
                                un lavabo in microcemento è l'impermeabilità che si può dare a questo rivestimento con
                                dei sigillanti specializzati. Con le nostre vernici, il lavabo in microcemento resiste
                                senza problemi all'umidità ambientale e all'acqua corrente. <h4>4. Ampie possibilità per
                                    i lavabi in microcemento</h4>
                                <p>I lavabi in microcemento offrono un'ampia gamma di possibilità decorative a seconda
                                    del tipo di rivestimento. Se cercate un look marcato all'acqua o allo stucco e un
                                    tocco morbido, si raccomanda il microcemento Microfino. </p>
                                <p>Se invece si preferisce un aspetto più naturale, la scelta migliore è il microcemento
                                    Microdeck. Ma si può anche ottenere una finitura pietrosa o rustica applicando
                                    Microbase microcemento fresco su fresco. </p>

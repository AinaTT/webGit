                        <h2> Prodotti specializzati per la pulizia del microcemento </h2>
                        <p> Anche se l'acqua e il sapone neutro sono sufficienti per pulire il microcemento, sia i
                            pavimenti che le pareti, in Topciment proponiamo due prodotti specializzati per la pulizia
                            del microcemento. Due detergenti che migliorano ancora di più, se possibile, la durata dello
                            stato ottimale di questo rivestimento innovativo. </p>
                        <h3>Pulire il microcemento con Ecoclean Basic</h3>

                        <p>
                            <a class="links"
                                href="../microcemento/pulizia-e-cura-del-microcemento#ecoclean-basic">Ecoclean
                                Basic</a>
                            è un detergente ecologico rivolto ad un ambiente più domestico, in quanto è ideale per la
                            pulizia del microcemento di pareti e pavimenti nelle case private. Grazie al suo alto potere
                            sgrassante e disincrostante, rimuove rapidamente il grasso accumulato, i residui di cibo, il
                            calcare e le macchie d'olio. </p>
                        <p>Ma come si applica sul pavimento in microcemento o sul muro in microcemento? Molto facile.
                            Basta diluirlo con acqua e applicarlo sulla superficie con un mop, uno spruzzatore, una
                            palla o una spugna. Per una pulizia profonda del microcemento, il prodotto deve essere
                            lasciato agire tra 5 e 15 minuti. </p>
                        <h3>Pulire il microcemento con Ecoclean Pro </h3>

                        <p>
                            A Topciment abbiamo anche <a class="links"
                                href="../microcemento/pulizia-e-cura-del-microcemento#ecoclean-pro">Ecoclean Pro</a>, un
                            detergente alcalino ecologico. Un fattore che porta alla necessità di utilizzare una
                            protezione per pulire il microcemento su muri e pavimenti, quindi è destinato ad un uso
                            puramente professionale. </p>
                        <p> Ecolean Pro è altamente raccomandato per la pulizia di garage o officine, hotel, cucine di
                            ristoranti, ospedali, alberghi, aeroporti, centri commerciali e altre grandi superfici. A
                            differenza di Ecoclean Basic, il suo potere sgrassante è molto più alto e, quindi, è in
                            grado di rimuovere praticamente tutte le macchie e i segni. Anche i più difficili e
                            persistenti come le impronte di pneumatici, agenti distaccanti, inquinamento, fuliggine,
                            calcare, olio, residui di cibo, grasso, sangue, ecc. </p>
                        <p> Per quanto riguarda la sua applicazione su pareti o pavimenti in microcemento, può essere
                            applicato concentrato o diluito in acqua a seconda del grado di sporcizia della superficie.
                            Nel caso di diluire il detergente con acqua e poi spruzzarlo sulla superficie da trattare,
                            il prodotto deve essere lasciato agire da 5 a 15 minuti. Una volta trascorso questo tempo,
                            pulire con un panno o un tampone e poi risciacquare con acqua

                        </p>


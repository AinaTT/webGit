                    <h3> Riparazione superficiale errata </h3>
                    <p> Quando è necessario riparare il microcemento a causa di uno degli errori precedentemente
                        descritti o, utilizzando un materiale di scarsa qualità, sono state generate scheggiature da
                        urti, esso deve essere riparato correttamente. </p>
                    <p>Poiché si tratta di una pavimentazione continua, senza giunti, per riparare correttamente il
                        microcemento levigato bisogna farlo in grandi aree in modo che il contrasto non sia troppo
                        evidente e la finitura finale finisca per essere un disastro. </p>
                    <h3>Fessure e crepe nel microcemento</h3>
                    <p>Se il microcemento è un rivestimento così resistente e duro, perché ci sono casi in cui le
                        superfici rivestite con microcemento levigato si incrinano e compaiono crepe? Principalmente per
                        tre ragioni che sviluppiamo di seguito. </p>
                    <h4> 1. Fessurazioni da microcemento di scarsa qualità</h4>
                    <p>Anche se è ovvio, è importante notare che non tutti i microcementi garantiscono l'assenza di
                        fessurazioni, solo quelli con alte prestazioni. Più bassa è la qualità del rivestimento, più è
                        probabile che si incrini nel tempo. Ecco perché è molto importante non lesinare sulla qualità
                        del microcemento levigato. </p>
                    <p>A Topciment garantiamo al professionista che i nostri microcementi non si incrinano grazie alle
                        loro eccellenti prestazioni e resistenza. </p>
                    <h4>2. Crepe del microcemento dovute al riempimento dei giunti di dilatazione</h4>
                    <p>Come già detto, il microcemento levigato non ha bisogno di giunti di dilatazione, essendo un
                        rivestimento continuo. Tuttavia, è necessario fare molta attenzione a quale supporto viene
                        applicato, perché anche se è compatibile con molti materiali, è necessario prendere in
                        considerazione quelli che hanno giunti di dilatazione come un pavimento di cemento. </p>
                    <p>Se il microcemento è posto su una superficie con giunti di dilatazione, è molto importante che
                        questi siano rispettati e non riempiti. Fare ciò potrebbe causare la rottura del "microcemento".
                    </p>
                    <h4> 3. Fessurazioni del microcemento dovute a cattive condizioni del supporto</h4>
                    <p> In relazione alla condizione precedente, se il supporto su cui il microcemento deve essere
                        installato non è in buone condizioni, dovrà essere riparato. Perché se questo supporto è
                        rivestito con microcemento non ottimale, finirà per fessurarsi per il cattivo stato del supporto
                        esistente e che funziona come base per il microcemento levigato. </p>
                    <h4>4. Fessure e crepe microcemento su parquet</h4>
                    <p>Parquet e microcemento non vanno d'accordo, perché il parquet è un materiale che è soggetto a
                        variazioni a seconda delle condizioni ambientali come l'umidità. L'umidità fa sì che il parquet
                        si espanda e si contragga, quindi se il microcemento viene applicato sopra di esso, finirà per
                        creparsi. </p>
                    <p> Inoltre, poiché il parquet è una superficie instabile in quanto ha un gran numero di giunti
                        mobili, la rete in fibra di vetro che viene applicata prima della base in microcemento può
                        rompersi e, di conseguenza, possono apparire delle crepe. </p>

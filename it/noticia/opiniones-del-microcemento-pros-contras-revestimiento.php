                    <h2>Contrasti e svantaggi del microcemento </h2>
                    <p>Si deve notare che se si utilizza un materiale di qualità e l'applicazione viene effettuata da un
                        professionista, il microcemento non presenterà contro. Ed è che la comparsa di crepe, fessure,
                        segni e macchie, i problemi che più preoccupano il microcemento levigato, sono principalmente
                        dovuti a fallimenti in passaggi fondamentali nell'applicazione del microcemento o al cattivo
                        stato del supporto che serve come base del nuovo rivestimento. </p>
                    <h3>Fare errori nell'applicazione del microcemento</h3>
                    <p>L'applicazione del microcemento ha alcuni passi molto marcati. Alterare l'ordine o commettere
                        errori nel processo, specialmente nel priming e nella sigillatura, porterà automaticamente a un
                        risultato indesiderato. Pertanto, insistiamo sul fatto che l'installazione del microcemento
                        levigato deve essere effettuata da un applicatore professionale. Descriviamo di seguito gli
                        errori più comuni nell'applicazione del microcemento. </p>
                    <h4>1. Applicare il microcemento a una temperatura troppo bassa o troppo alta</h4>
                    <p>La temperatura ambiente ideale per l'applicazione del microcemento è compresa tra 15ºC e 25ºC. Se
                        il rivestimento viene installato a una temperatura inferiore a 15ºC o superiore a 25ºC, possono
                        sorgere problemi. </p>
                    <p>Se la temperatura è troppo bassa, un fattore che si verifica nella stagione invernale, la
                        superficie coperta di microcemento può impiegare molto tempo per indurirsi correttamente.
                        D'altra parte, se la temperatura è troppo alta, il micro cemento si indurisce troppo
                        rapidamente. </p>
                    <h4>2. Mescolare il pigmento di microcemento in modo scorretto</h4>
                    <p>Pigmentare correttamente il microcemento è la chiave. Se le indicazioni non sono seguite con
                        attenzione, si possono avere grumi in eccesso che causano striature scure sulla superficie
                        rivestita di microcemento. </p>
                    <p>Per ottenere la grande varietà di colori di microcemento del catalogo Topciment, il dosaggio
                        indicato su ogni etichetta deve essere rispettato sia per la quantità che per il tipo di
                        microcemento utilizzato. </p>
                    <p> Anche il pigmento deve essere agitato vigorosamente fino ad ottenere un liquido omogeneo senza
                        irregolarità. Poi versare un po' del liquido di miscelazione in un contenitore, aggiungere tutto
                        il pigmento e mescolare bene. Quindi aggiungere gradualmente il microcemento e il resto del
                        liquido di miscelazione nello stesso contenitore fino a raggiungere la viscosità richiesta per
                        ogni applicazione e un colore omogeneo del microcemento. </p>
                    <h4>3. Non rispettare i passaggi durante l'installazione del microcemento</h4>
                    <p>L'applicazione del microcemento richiede passaggi che devono essere seguiti alla lettera. Non
                        rispettarli può essere fatale per il finale. </p>
                    <p>L'installazione di microcemento su pavimenti o muri, per esempio, non è la stessa cosa. Poiché il
                        tipo di microcemento levigato da applicare varia. Allo stesso modo, non condizionando e
                        livellando il supporto esistente da rivestire, si possono causare grossi problemi futuri. </p>



                    <p>Così come non si possono fare errori nell'innesco del supporto o nella sigillatura dello stesso.
                        <a class="link" href="../microcemento/como-aplicar-microcemento-aplicacion-de-microcemento-paso-a-paso">In questa pagina</a>,
                        troverete tutto quello che dovete sapere per evitare di commettere errori nell'applicazione del
                        microcemento. </p>

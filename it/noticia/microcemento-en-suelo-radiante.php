                    <h3>Riscaldare il pavimento prima di applicare il microcemento</h3>
                    <p>Prima di applicare il microcemento sul riscaldamento a pavimento, bisogna eseguire un protocollo
                        di accensione del riscaldamento. Questo eviterà che le crepe causate dagli sbalzi di temperatura
                        nel massetto danneggino il microcemento. </p>
                    <p>Accendere il riscaldamento almeno 4 settimane dopo la posa della malta di cemento. </p>
                    <p>Mantenere l'acqua in ingresso ai circuiti a 25ºC per un periodo di 2 o 3 giorni. </p>
                    <p>Aumentare gradualmente la temperatura dell'acqua fino a raggiungere i 45ºC. Mantenetelo così per
                        diversi giorni. </p>
                    <p>Spegnere il riscaldamento 48 ore prima di iniziare la posa del microcemento sul riscaldamento a
                        pavimento. È molto importante che la temperatura massima del pavimento non superi i 18ºC. </p>
                    <h3>Applicazione del microcemento sul riscaldamento a pavimento</h3>
                    <p>I pavimenti in microcemento devono essere posati su malte cementizie che hanno raggiunto una fase
                        di presa avanzata. Tre o quattro settimane devono passare prima di raggiungere questo stadio
                        ottimale, un periodo che può variare a seconda del tempo. È essenziale controllare che la malta
                        sia praticamente asciutta (massimo 5% di umidità). </p>
                    <h3>Accensione del riscaldamento a pavimento dopo l'installazione del microcemento</h3>
                    <p>1. Quando il microcemento è stato applicato correttamente al riscaldamento a pavimento, è
                        necessario attendere almeno 48 ore prima di riaccendere il riscaldamento. I seguenti aspetti
                        devono essere presi in considerazione:</p>
                    <p>2. Il riscaldamento deve essere acceso gradualmente, aumentando di 5ºC ogni giorno. Cambiare la
                        temperatura per gradi all'inizio e alla fine dell'uso del riscaldamento a pavimento. </p>
                    <p>4. Assicurarsi che l'umidità relativa dell'aria della stanza non sia troppo bassa. </p>
                    <p>5. Evitare l'accumulo di calore a causa dell'uso di tappeti e moquette o anche perché c'è poco
                        spazio tra i mobili e il riscaldamento a pavimento. </p>




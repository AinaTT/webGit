                    <h2>Come fare un piano in microcemento? </h2>
                    <p>Il processo di applicazione di un piano di lavoro in microcemento dipenderà dal tipo di finitura
                        che si vuole ottenere e dal materiale che fa da base alla superficie, sia che si tratti di un
                        rivestimento assorbente come cartongesso, gesso, intonaco di cemento e sabbia o DM, sia che si
                        tratti di un rivestimento non assorbente come marmo, terrazzo, piastrelle o granito. </p>
                    <p>Dettaglieremo l'applicazione del microcemento sul piano di lavoro, passo dopo passo, a seconda
                        del risultato che state cercando:</p>
                    <h3>1.Mettete la rete sul futuro piano di lavoro in microcemento. </h3>
                    <p>Il primo passo è quello di posizionare la rete sulla superficie che costituirà la base del piano
                        d'appoggio in microcemento: marmo, granito, cartongesso... È super importante perché questo
                        impedirà la comparsa di crepe nel tempo. </p>
                    <h3>2.Primerizzazione del piano di lavoro in microcemento</h3>
                    <p>Il secondo passo nell'applicazione del microcemento su un piano di lavoro consiste nella
                        primerizzazione, che faciliterà l'incollaggio del rivestimento assorbente o non assorbente
                        esistente con il nuovo piano di lavoro in microcemento. Nel primo caso, si dovrebbe applicare il
                        promotore di adesione PRIMACEM ABS e, nel secondo caso, il primer PRIMACEM PLUS. </p>
                    <h3>3. Applicare due mani di microcemento Microbase sul piano di lavoro</h3>
                    <p>Si devono poi applicare due mani di microcemento Microbase. La prima mano dovrà essere senza
                        pigmento e, dopo la levigatura, sarà la volta di una seconda mano di Microbase già pigmentata a
                        seconda del colore del microcemento scelto per il piano di lavoro. In seguito, sarà necessario
                        carteggiare di nuovo. Se state cercando una finitura in pietra o rustica, dopo le due mani di
                        microcemento Microbase dovreste sigillare per proteggere il piano di lavoro in microcemento
                        prima con la vernice acrilica Presealer e poi con la vernice poliuretanica Topsealer. </p>
                    <h3>4. Applicare due mani di microcemento Microfino sul piano di lavoro</h3>
                    <p>Se invece si desidera che il piano di lavoro in microcemento abbia un marcato aspetto ad acqua o
                        a stucco, dopo le due mani di microcemento Microbase sarà la volta di due mani di microcemento
                        Microfino, entrambe pigmentate. Sarà necessario carteggiare dopo ogni mano, senza eccezione. Una
                        volta applicate le due mani di Microfino, il supporto deve essere protetto prima con Presealer e
                        poi con Topsealer. </p>
                    <h3>5. Applicare due mani di microcemento Microdeck sul piano di lavoro</h3>
                    <p>Se si preferisce che il piano di lavoro in microcemento risalti per il suo aspetto naturale, dopo
                        le due mani di Microbase si consiglia di applicare due mani di microcemento Microdeck
                        pigmentato, levigando alla fine. Per finire, come per le due applicazioni precedenti, la vernice
                        Presealer e il sigillante per microcemento Topsealer devono essere applicati al substrato. </p>
                    <h3>5. Proteggere e sigillare il piano di lavoro in microcemento</h3>
                    <p>Una volta applicate le due mani di Microbase, Microfino o Microdeck, a seconda della finitura
                        desiderata, il piano di lavoro in microcemento deve essere protetto per evitare problemi. Per
                        sigillare correttamente i mobili, sono necessarie due mani di Presealer e due mani di Topsealer,
                        come spiegato sopra. </p>




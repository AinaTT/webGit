                <h1>Opinions on microcement: pros and cons of this coating.</h1>

                <p>
                    <a href="../microcement">Microcement</a> is a coating that has become increasingly popular over the
                    last
                    few years in interior and exterior decoration. A coating that has become a real trend in the
                    renovation of homes and businesses because of the advantages it offers.

                </p>
                <p>
                    A building material which application is only reserved for an experienced professional. Otherwise,
                    mistakes can be made during installation that can lead to problems with microcement. This is why
                    there are all kinds of opinions about this covering.

                </p>
                <p>
                    In this article we are going to analyse the main pros and cons of this continuous coating, which
                    offers a wide variety of decorative solutions, in order to clarify and resolve any doubts that may
                    arise about it.

                </p>
                <figure class="figure mt-4">
                    <img src="https://www.topciment.com/imagenes/news/microcemento-gris-suelo-dormitorio.jpeg"

                <h3>Incorrect repair of microcement surfaces</h3>
                <p>
                    When it is necessary to repair the microcement due to any of the previously described errors or, by
                    using a poor quality material, chipping has been caused by knocks, it must be repaired correctly.

                </p>
                <p>
                    As it is a continuous pavement, without joints, in order to repair smoothed microcement correctly,
                    it must be repaired in large areas so that the contrast is not too noticeable and the final finish
                    ends up being a disaster.

                </p>
                <h3>Cracks and fissures in microcement</h3>

                <p>
                    If microcement is such a resistant and hard coating, why are there cases in which surfaces coated
                    with smoothed microcement crack and appear? Mainly for three reasons, which are explained below.

                </p>
                <h4>1. Poor quality microcement cracks</h4>

                <p>
                    Although it is obvious, it is important to point out that not all microcements guarantee no
                    cracking, only high-performance ones do. The lower the quality of the coating, the more likely it is
                    to crack over time. That is why it is very important not to skimp on the quality of the smoothed
                    microcement.

                </p>
                <p>
                    At Topciment we guarantee the professional that our microcements will not crack thanks to their
                    excellent performance and resistance.

                </p>
                <h4>2. Microcement cracks due to filling expansion joints</h4>

                <p>
                    As mentioned above, smoothed microcement does not need expansion joints as it is a continuous
                    coating. However, it is necessary to be very careful about the substrate on which it is applied, as
                    although it is compatible with many materials, it is necessary to take into consideration those that
                    have expansion joints, such as a concrete floor.

                </p>
                <p>
                    If microcement is laid on a surface with expansion joints, it is very important that they are
                    respected and not filled. If this is done, the "microcement" could crack.

                </p>
                <h4>3. Micro-cement cracks due to poor substrate conditions</h4>

                <p>
                    In relation to the previous condition, if the substrate on which the microcement is to be installed
                    is not in good condition, it will have to be repaired. If the substrate is not in good condition, it
                    will end up cracking due to the poor condition of the existing substrate, which acts as a base for
                    the smoothed microcement.

                </p>
                <h4>4. Cracks and fissures microcement on parquet flooring</h4>

                <p>
                    Parquet and microcement do not get along well, because parquet is a material that is subject to
                    variations depending on environmental conditions such as humidity. Humidity causes the parquet to
                    expand and contract, so if microcement is applied on top of it, it will end up cracking.

                </p>
                <p>
                    In addition, as parquet is an unstable surface with a large number of mobile joints, the fibreglass
                    mesh that is applied before the microcement base can break and, as a result, cracks may appear.

                </p>

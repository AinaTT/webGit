                <h2>Advantages and disadvantages of microcement </h2>

                <p>
                    It should be noted that if a quality material is used and the application is carried out by a
                    professional, microcement will not have any drawbacks. The appearance of fissures, cracks, marks and
                    stains, the problems that most concern the smoothed microcement, are mainly due to failures in
                    fundamental steps in the application of microcement or to the poor state of the substrate that
                    serves as the base for the new coating.

                </p>
                <h3>Making mistakes in microcement application</h3>

                <p>
                    The application of microcement has very specific steps. Altering the order or making mistakes in the
                    process, especially in priming and sealing, will automatically lead to an undesirable result.
                    Therefore, we insist that the installation of smoothed microcement must be carried out by a
                    professional applicator. We describe below the most frequent mistakes when applying microcement.

                </p>
                <h4>1. Applying microcement at too low or too high a temperature.</h4>

                <p>
                    The ideal ambient temperature for applying microcement is between 15¬∫C and 25¬∫C. If the coating is
                    installed at a temperature below 15¬∫C or above 25¬∫C, problems may arise.

                </p>
                <p>
                    If the temperature is too low, a factor that occurs in the winter season, the surface covered with
                    microcement may take a long time to harden properly. On the other hand, if the temperature is too
                    high, the micro cement will harden too quickly.

                </p>
                <h4>2. Mixing the microcement pigment incorrectly</h4>

                <p>
                    Pigmenting microcement properly is key. If the instructions are not followed carefully, there may be
                    an excess of clusters causing dark streaks on the microcement coating surface.

                </p>
                <p>
                    To obtain the wide variety of microcement colours in the Topciment catalogue, the dosage indicated
                    on each label must be respected for both the quantity and type of microcement used.

                </p>
                <p>
                    The pigment should also be shaken vigorously until a homogeneous liquid without irregularities is
                    obtained. Then pour a little of the mixing liquid into a container, add all the pigment and mix
                    thoroughly. Then add little by little the microcement and the rest of the mixing liquid in the same
                    container until the viscosity required for each application and a homogeneous microcement colour is
                    achieved.

                </p>
                <h4>3. Not respecting the steps when installing microcement</h4>

                <p>
                    The application of microcement requires certain steps that must be followed to the letter. Not
                    following them can be fatal for the final finish.

                </p>
                <p>
                    The installation of microcement on floors or walls, for example, is not the same. The type of
                    smoothed microcement to be applied varies. Similarly, failure to condition and level the existing
                    substrate to be coated can cause major problems in the future.

                </p>
                <p>
                    Neither can you make mistakes in the priming of the substrate or in the sealing of the substrate. On
                    <a href="../microcements/how-to-apply-microcement-microcement-application-step-by-step">this page</a> , you will find everything
                    you need to know to avoid making mistakes
                    when applying microcement.

                </p>
                <figure class="figure mt-4">

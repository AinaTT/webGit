                    <p>Among its many technical features, it is resistant to abrasion and scratching , as well as to
                        water and alkalinity. Moreover, as if that weren't enough, it is unalterable to ultraviolet
                        light and does not yellow! </p>
                    <p>But there is more, much more. In the case of ready-to-use microcement coatings, our new Topsealer

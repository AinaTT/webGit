                    <h2>
                        Microcement kitchens for all styles</h2>
                    <p>If there is one thing that characterises this material, it is the wide range
                        of possibilities it offers. For every decorative style, there is a perfect
                        microcement kitchen. At Topciment we propose four different styles of
                        microcement kitchens:
                    </p>
                    <h3>Minimalist microcement kitchens</h3>
                    <p>For those who opt for a kitchen in which straight lines predominate both in
                        the furniture and in the layout of the space. The absence of joints favours
                        continuity in microcement kitchens, a fundamental factor in minimalist
                        design.
                    </p>
                    <h3>
                        Eclectic microcement kitchens </h3>
                    <p> For people who prefer a kitchen that combines different types of materials
                        (wood, plasterboard, slate or tiles). Eclectic microcement kitchens can
                        combine the traditional with the modern, luxury with simplicity.
                    </p>
                    <h3>Nordic microcement kitchens</h3>
                    <p> You don't need a huge kitchen to make it shine. The Scandinavian or Nordic
                        style is ideal for small microcement kitchens, as it enhances the beauty of
                        the smallest details. White tones will not only magnify the dimensions of
                        your kitchen, but will also enhance the natural light in the room.
                    </p>
                    <h3>Industrial microcement kitchens </h3>
                    <p> Do you prefer a kitchen with a more modern, avant-garde and urban aesthetic?
                        We propose a 100% industrial style, which is becoming more and more
                        widespread in the decoration of interiors such as kitchens. In industrial
                        microcement kitchens, the combination of walls and floors of this continuous
                        coating in grey and black colours predominates.
                    </p>

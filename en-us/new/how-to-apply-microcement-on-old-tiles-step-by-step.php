     <figcaption class="figure-caption">Spacious and bright bathroom with old tiles on the floor and shower tray.</figcaption>
                    </figure>
                    <h2>Advantages of applying microcement on tiles</h2>

                    <h3>1. Microcement can be applied on existing tiles without building work </h3>

                    <p>
                        The main benefit of microcement is its excellent adhesion and compatibility with all types of
                        materials, including tiles, which is the subject we are dealing with today. In this way,
                        microcement is applied directly onto the tiles without the need to remove them.

                    </p>
                    <p>
                        This means that it is not necessary to carry out work to remove the tiles and, as there is no
                        need to remove debris, no waste is generated, thus saving time and inconvenience, both for the
                        owner of the home or premises and for the neighbours. 
                    </p>

                    <h3>2. Microcement on tiles modernises rooms </h3>

                    <p>
                        Applying microcement on old tiles also modernises and quickly transforms spaces, completely
                        changing the design of the rooms. Multiple finishes are possible thanks to the wide variety of
                        microcement textures, varnishes and colours available.

                    </p>
                    <h3>3. Possibility of anti-slip and waterproof finishes of microcement on tiles</h3>

                    <p>
                        Some types of latest-generation microcements themselves provide anti-slip finishes that are very
                        useful, especially when smoothed microcement is used to renovate bathroom floor tiles or kitchen
                        floor tiles.

                    </p>
                    <p>
                        This is the case of some of the microcements that we manufacture at Topciment (products page),
                        which guarantee continuous non-slip flooring where falls and slips have no place. Although we
                        also have<strong> <a class="links" href="../microcements/polyurethane-waterbased-acrylic-varnishes-topsealer">varnishes and sealants</a></strong>that have these properties.

                    </p>
                    <p>
                        Another advantage of applying microcement on old tiles is that waterproof finishes can be
                        achieved. The bathroom, for example, is a room that constantly suffers from environmental
                        humidity and, therefore, not just any decorative coating will do. 
                    </p>
                    <p>
                        At Topciment we have designed specific varnishes with which to seal bathroom wall tiles or
                        shower wall tiles coated with microcement. Varnishes of the highest quality with which the
                        smoothed microcement will perfectly resist environmental humidity and running water (not
                        standing)

                    </p>

                    <h2>Microcement is perfect for coating bathroom and kitchen tiles</h2>

                    <p>
                        As you have seen, the advantages of applying microcement on tiles make this continuous coating
                        ideal for covering bathroom and kitchen tiles. In addition to these qualities, smoothed
                        microcement is easy to maintain and clean.

                    </p>
                    <p>
                        Below, we show you the before and after of some works in which microcement has been applied on
                        old tiles.

                    </p>

                    <h2>Renovating old tiles with microcement: application and tips</h2>

                    <p>
                        To apply microcement on tiles it is essential to have a professional and qualified applicator
                        who does a good job, such as the microcement installers who work with our microcements.

                    </p>
                    <p>
                        This is the only way you will not have to regret some of the 
                      <strong>  <a class="links" href="../microcements/how-to-avoid-unwanted-stains-or-marks-when-applying-microcement">most frequent problems</a></strong>
                        that are the result of mistakes in the application of microcement.

                    </p>

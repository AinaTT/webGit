                    <h3>8 steps to apply microcement on tiles</h3>

                    <h4>1. Assessing the state of the tiles</h4>

                    <p>
                        The first thing to do before applying microcement directly on old tiles is to check the
                        condition of the tiles. If the floor or wall tiles have any imperfections, they cannot be coated
                        with microcement as if nothing had happened. The substrate will have to be repaired before
                        laying our smoothed microcement. This is why it is important that the renovation is carried out
                        by a professional, because they will know how to locate these imperfections.

                    </p>
                    <h4>2. Cleaning the tiles before applying microcement</h4>

                    <p>
                        After repairing the tiles, where necessary, the tiles should be thoroughly cleaned before
                        applying smoothed microcement so that they are free of grease and dust.

                    </p>
                    <h4>3. Filling tile joints with microcement </h4>

                    <p>
                        Once the wall or floor on which the tiles are located is clean, it is time to get down to work.
                        The first step in the application of microcement on tiles is to fill the joints. Since smoothed
                        microcement is a continuous coating, if the tile joints are not filled, the substrate will not
                        be stable and there will be unevenness that will mark the joints.

                    </p>
                    <h4>4. Prime the tiles to be coated with microcement </h4>

                    <p>
                        When the surface is level and the tile joints perfectly filled, it is time to apply the
                        corresponding primer or adhesion promoter. This is an essential product as it acts as a bridge
                        between the tiles and the microcement, so that the latter adheres better.

                    </p>
                    <p>
                        At Topciment we have manufactured a wide range of promoter depending on the surface on which the
                        microcement is applied. In this case, which involves tiles, our experts recommend using one of
                        these two: Primacem Impoxy A+B or Primacem Plus. We are going to develop both so that you know
                        which one to use on each occasion.

                    </p>
                    <p>
                  <strong>   <a class="links" href="../microcements/primers-and-adhesion-promoters-primacem#primacem-impoxy">   Primacem Impoxy A+B</a></strong>   is our epoxy damp-proofing primer that blocks rising damp and
                        acts as a vapour barrier. A 100% solids, two-component adhesion promoter that is perfect for
                        shower wall tiles or bathroom floor tiles. An innovative product that also avoids the sticking
                        of joints in microcement.

                    </p>
                    <p>
                        On the other hand, you can also use <a class="links" href="../microcements/primers-and-adhesion-promoters-primacem#primacem-plus">Primacem Plus </a>, our specific primer for
                        non-absorbent surfaces such as tiles, which is applied in a single coat.

                    </p>
                    <figure class="figure mt-4">
                        <img src="https://www.topciment.com/imagenes/news/azulejos-microcemento-calabaza-pared-banyo.jpg" class="figure-img img-fluid "
                         title="Microcement bathroom wall tiles in pumpkin colour" alt="Bathroom wall tiles covered with microcement in Calabaza colour " width="auto" height="auto">
                        <figcaption class="figure-caption">The before and after of a bathroom in which our Calabaza coloured microcement has been used to cover the bathroom wall tiles.</figcaption>
                    </figure>
                    <h4>5. Laying fibre mesh on the first layer of microcement on tiles</h4>

                    <p>
                        After allowing the relevant drying time (see technical data sheets), place the fibre mesh on the
                        tiles before applying the microcement to protect the future coating and avoid fissures, cracks,
                        etc. This is a step that not everyone takes into account and which later becomes a big headache.

                    </p>
                    <p>
                        Next, apply the first layer of microcement base (Microbase) without pigment on the tiles. Once
                        the tiles are well covered by the microcement, without irregularities, sand the substrate.

                    </p>
                    <h4>6. Apply the second coat of microcement base on the tiles </h4> 

                    <p>
                        Apply the second layer of microcement base (Microbase). This time it must be pigmented with the
                        microcement colour chosen for coating the old tiles. In the same way as in the previous step,
                        the surface should be sanded.

                    </p>
                    <h4>7. Apply two coats of Microdeck or Microfino microcement on tiles</h4>

                    <p>
                        After the base microcement, apply two coats of Microdeck microcement or two coats of Microfino
                        on tiles. The former is usually used for floors and the latter for walls. Although combinations
                        can be made depending on the tastes, preferences and finishes sought. Both coats will already be
                        pigmented. Afterwards, sand.

                    </p>
                    <h4>8. Protecting and sealing the microcement on tiles </h4>

                    <p>
                        The last step is one of the most important. It is to protect the microcement coating on tiles.
                        For this, we have all kinds of varnishes and sealants. However, on this occasion, we recommend
                        using two coats of <strong><a class="links" href="../microcements/polyurethane-waterbased-acrylic-varnishes-topsealer#presealer">Presealer</a> </strong>
                         and another two coats of <strong><a class="links" href="../microcements/polyurethane-waterbased-acrylic-varnishes-topsealer#topsealer-one-coat">Topsealer WT One Coat</a></strong>.

                    </p>
<figure class="figure mt-4">
                        <img src="https://www.topciment.com/imagenes/news/azulejos-microcemento-piedraparis-suelo-pared-cocina.jpg" class="figure-img img-fluid " 
                        title="Microcement colour Piedra Paris wall and floor tiles kitchen" alt="Kitchen with wall and floor tiles coated with microcement in Piedra Paris colour." width="auto" height="auto">
                        <figcaption class="figure-caption">Impressive renovation of a kitchen after using the microcement colour Piedra Paris Topciment on the floor and wall tiles. </figcaption>
                    </figure>
                    <h3>Tips and recommendations for coating tiles with microcement</h3>

                    <h4>1. Respect drying times </h4>

                    <p>
                        When using a primer for non-absorbent surfaces such as Primacem PLUS, it is very important to
                        respect the drying time when coating tiles with microcement. If the instructions and drying
                        times specified in the technical data sheets are not strictly followed, moisture will be trapped
                        between coats and will therefore mark the tile joints.

                    </p>
                    <p>
                        With this advice you will avoid the most common failure when applying microcement on tiles. A
                        possible error that is avoided by using Primacem Impoxy A+B primer, for which it is not
                        necessary to be so meticulous with the drying times due to its capacity to block capillary
                        humidity.

                    </p>
                    <h4>2. Allow 24 hours to dry before applying the second layer of microcement </h4>

                    <p>
                        Another recommendation from the Topciment experts, also in the case of using Primacem PLUS, is
                        to leave a 24-hour drying period between the first and second coat of microcement base.

                    </p>
                    <p>
                        A process that not everyone does as it is not mandatory, but with which the professional ensures
                        that there is much less chance of marking the joints by a mistake in the application of
                        microcement on tiles. With Primacem Impoxy A+B it will be done with the normal system. That is,
                        applying the second coat of Microbase immediately after the first.

                    </p>
                    <h4>3. Prime the tiles before applying the second coat of microcement</h4>

                    <p>
                        We also recommend that 24 hours after applying the first coat of microcement, which is more than
                        enough time for it to have dried properly, the microcement covering the tiles should be
                        re-primed with Primacem PLUS.

                    </p>
                    <p>
                        This advice is only for the most expert applicators and will help to even out the level of
                        absorption of the next coats of microcement application on tiles. An equality that drastically
                        reduces the visual marks of joints with different absorption points.

                    </p>

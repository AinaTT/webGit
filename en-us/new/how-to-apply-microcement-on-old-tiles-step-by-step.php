                <h1>How to apply microcement on old tiles step by step.</h1>
                <div class="headline fadeInRight styleNormal16justified">
                    <p>
                        Microcement is the best decorative coating for renovating old tiles because it is a highly
                        adherent material and, above all, because it allows tiles to be renovated and covered quickly
                        and without building work.

                    </p>
                    <p>
                        This added value is not offered by all the coverings on the market and has boosted the use of
                        smoothed microcement on bathroom and kitchen tiles. One of the most sought-after decorative
                        solutions in recent years by both private individuals and professionals from decoration and
                        construction companies.

                    </p>
                    <p>
                        In this article, we will discuss the main advantages of using microcement as a continuous
                        coating to renovate tiles as well as the most important tips for successfully applying smoothed
                        microcement on tiles.

                    </p>
    <figure class="figure mt-4">
                        <img src="https://www.topciment.com/imagenes/news/bano-con-azulejos-suelo-y-ducha.jpg" class="figure-img img-fluid "

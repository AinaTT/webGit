                <h2> Problems with microcement in underfloor heating</h2>

                <p>
                    Like any worthy system, there are also a number of disadvantages that should be taken into account
                    to avoid problems with microcement in underfloor heating.

                </p>
                <h3>Choosing poor quality microcement </h3>
                <p>
                    Although microcement is a continuous coating that is highly resistant to heat and should not crack,
                    it is important not to skimp on its quality. The higher the quality, the greater the resistance and
                    therefore the less likely it is that microcement problems will appear in underfloor heating.

                </p>


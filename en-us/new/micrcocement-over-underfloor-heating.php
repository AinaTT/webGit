                <h1>Successfully applying microcement over underfloor heating.</h1>

                <p>
                    More and more people are choosing to install underfloor heating instead of traditional radiators for
                    two main reasons: it provides better thermal comfort and is more economical in the long term.
                    However, the installation of underfloor heating is not compatible with all materials. For example,
                    extreme care must be taken with many types of wood flooring.

                </p>
                <p>
                    Microcement is one of the most suitable building materials for underfloor heating for many reasons.
                    Mainly because it is a continuous coating, does not crack and above all has a good thermal
                    conductivity thanks to its excellent heat resistance. However, for the optimum combination of this
                    decorative covering and underfloor heating, a number of steps need to be rigorously carried out.

                </p>
                <h2>How to apply microcement on underfloor heating: step by step</h2>
                <figure class="figure mt-4">
                    <img src="https://www.topciment.com/imagenes/news/comedor-microcemento-suelo-radiante.jpg"
                        class="figure-img img-fluid " title="Dining room silver microcement over underfloor heating"
                        alt="Dining room microcement over underfloor heating Silver colour">
                    <figcaption class="figure-caption">Small dining room where our Silver microcement over underfloor
                        heating has been applied. </figcaption>
                </figure>.
                <p>
                    Our technical team has been advising and resolving doubts to professionals on the application of
                    microcement for almost two decades. One of the most recurrent is precisely about the possibility of
                    installing a radiant microcement floor.

                </p>
                <p>
                    Below, we explain the step-by-step instructions to ensure that the application of microcement on
                    underfloor heating is a complete success. Take note of our advice.

                </p>
                <h3>Heating the floor before applying the microcement</h3>

                <p>
                    Before applying the microcement on the underfloor heating, a heating protocol must be carried out.
                    This will prevent cracks caused by temperature changes in the screed from damaging the microcement.

                </p>
                <p>
                    Turn on the heating at least 4 weeks before laying the microcement.

                </p>
                <p>
                    Hold the inlet water to the circuits at 25ºC for a period of 2 or 3 days.

                </p>
                <p>
                    Gradually increase the temperature of the water until it reaches 45ºC. Keep it this way for several
                    days.

                </p>
                <p>
                    Turn off the heating 48 hours before starting to install the microcement on the underfloor heating.
                    It is very important that the maximum temperature of the floor does not exceed 18ºC.

                </p>
                <h3>Application of microcement on underfloor heating</h3>

                <p>
                    Microcement floors should be laid on cementitious mortars that have reached an advanced setting
                    stage. It takes three to four weeks to reach this optimum stage, a period that can vary depending on
                    the weather. It is essential to check that the mortar is practically dry (maximum 5% humidity).

                </p>
                <h3>Switch on the underfloor heating after the installation of microcement</h3>

                <p>
                    Once the microcement has been correctly applied on the underfloor heating, it is necessary to wait
                    at least 48 hours before turning on the heating again. The following aspects must be taken into
                    account:

                </p>
                <p>
                    The heating should be switched on gradually. The temperature should be raised by 5ºC every day.

                </p>
                <p>
                    Change the temperature in stages at the beginning and end of the use of underfloor heating.

                </p>
                <p>
                    Ensure that the relative humidity of the air in the room is not too low.

                </p>
                <p>
                    Avoid heat build-up due to the use of carpets and rugs, or because there is too little space between
                    the furniture and the underfloor heating.


                </p>
                <figure class="figure mt-4">
                    <img src="https://www.topciment.com/imagenes/news/suelo-de-microcemento.jpg"
                        class="figure-img img-fluid " title="living room steel microcement underfloor heating"
                        alt="Living room Steel microcement on underfloor heating">
                    <figcaption class="figure-caption">Spacious living room in which underfloor heating and our Steel
                        colour microcement have been installed. </figcaption>
                </figure>
                <h2>Advantages of microcement underfloor heating</h2>

                <p>
                    These are just some of the many benefits of using microcement over underfloor heating:

                </p>
                <h3>1. Low consumption</h3>

                <p>
                    One of the main advantages of microcement over underfloor heating is that it is a low consumption
                    and very efficient system. In other words, the amount of energy needed to reach an optimum
                    temperature is considerably reduced compared to other alternatives. In this way, you will save
                    energy and money during the coldest months of the year.

                </p>
                <h3>2. Feeling of thermal comfort</h3>

                <p>
                    Underfloor heating, thanks to its uniform distribution, helps to achieve that feeling of thermal
                    comfort more quickly, of balance between the temperature of the room and the body temperature of the
                    people in it. A harmony that is enhanced thanks to the microcement floor and its high thermal
                    conductivity.

                </p>
                <h3>3. Resistance and thermal conductivity</h3>

                <p>
                    The outstanding resistance of microcement to heat and its thermal conductivity make this covering
                    one of the most suitable for the installation of underfloor heating.

                </p>
                <h3>4. Non-cracking </h3>

                <p>
                    Closely related to the previous point, if the microcement is applied correctly by the professional,
                    the microcement floor will not crack. This is an aspect to be taken into account as not all
                    floorings are capable of resisting the heat radiated by a radiant floor and end up cracking and
                    cracking. This will not happen if microcement is used over underfloor heating.

                </p>
                <h3>5. Continuous tiling</h3>
 
                <p>
                    Last but not least, microcement is the continuous cladding par excellence. In this way, the
                    underfloor heating is covered without any visual breaks and therefore offers a pleasant feeling of
                    continuity.

                </p>

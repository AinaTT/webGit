                <p class=" m-0">More and more people are choosing to install underfloor heating. But can microcement be
                  applied over underfloor heating? Yes, microcement can be installed on underfloor heating without any
                  problems. It is necessary to follow a protocol for switching on the heating to prevent cracks caused
                  by temperature changes in the screed from being reproduced in the microcement. </p>



                <a href="javascript:MostrarOcultar('texto1_4');" id="enlace1_1"
                  style="color:#000; text-decoration:none;"><strong>+information</strong></strong></a>
                <div class="concealable" id="text1_4" style="text-align:left;font-size:16px;">
                  <p><strong>The switch-on protocol is as follows:</strong></p>
                  <p>1. Switch on the heating at least 4 weeks after laying the cement mortar. </p>
                  <p>2. Maintain the inlet water to the circuits at 25°C for 2 to 3 days. Then gradually increase the
                    water temperature to 45 °C and maintain it for several days.</p>
                  <p>3. Turn off the heating at least 48 hours before starting the installation of the microcement. The
                    maximum floor temperature should be 18 °C. </p>
                  <p><strong>Microcement flooring should be applied</strong> on cementitious mortars that have reached
                    an advanced setting stage. This occurs after three to four weeks, depending on the weather. In any
                    case, we must ensure that the mortar is practically dry. To do this, there must be a maximum of 5%
                    humidity in the substrate, which should be checked with a relative humidity meter. </p>
                  <p>We recommend using quality self-levelling cement mortars. </p>
                  <p>Supports with powdery mortars are not suitable as the state of the application base must be
                    consistent, otherwise it will be necessary to consolidate it. </p>
                  <p><strong>After applying the microcement</strong>, we must wait at least 48 hours before gradually
                    switching the heating back on (+5 °C each day).</p>
                  <p>Always change the temperature gradually at the beginning and end of the heating period. </p>
                  <p>Make sure that the relative humidity in the rooms is not too low.</p>
                  <p>Always avoid heat build-up caused by carpets and rugs or a lack of space between the furniture and
                    the floor.</p>
                  <p><strong>Important!</strong> Between the screed and the vertical walls there will always be an
                    expansion joint so that the screed can expand. If this is not observed, cracks may appear. </p>


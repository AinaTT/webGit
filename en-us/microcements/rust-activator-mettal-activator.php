                                metallic paints. For further application possibilities of our rust activator, please
                                check the data sheet.

                            </p>

                            <h3>Application of rust activator Mettal Activator on Classic Mettal and Pure Mettal</h3>

                            <p>1. 24 hours after the last coat of Pure Mettal or Classic Mettal, sand the surface with
                                120 grit sandpaper to facilitate the oxidation of the metal particles. </p>

                            <p>2. Apply the first coat of Mettal Activator rust activator. Leave to act for 3 to 4
                                hours. </p>

                            <p>3. Apply second coat of Mettal Activator rust activator until the desired metallic rust
                                finish is obtained. Allow to dry for 24 hours. </p>

                            <p>4. Seal the surface with two coats of Presealer water-based acrylic varnish, allowing 4
                                hours drying time between coats. </p>

                            <p>5. Finally protect the surface with two coats of the water-based polyurethane varnish
                                Topsealer WT One Coat, allowing 24 hours drying time between coats. </p>

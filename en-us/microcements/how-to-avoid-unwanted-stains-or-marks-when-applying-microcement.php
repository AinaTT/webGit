                <h1 id="queeselmicrocemento"><span style=" padding:5px;">How to avoid unwanted stains or marks when
                        applying microcement</span><span style="background-color:#FFF; padding: 5px;"></span></span>
                .</h1>

                <p> A poor application of microcement can lead to the appearance of unwanted stains and scuffs. Visual
                    problems that damage the final finish. If you have come this far, it is because you are concerned
                    about this circumstance and, above all, because you want to know how you can avoid them at all
                    costs. </p>
                <p>You've come to the right place. In this article we will not only delve into the most common reasons
                    that produce those dreaded marks and stains due to poor installation of smoothed microcement. </p>
                <p>In addition, our experienced applicators will give you a series of tips, advice and solutions to each
                    of the problems so that unwanted imperfections do not appear. Take note! </p>
                <h2>Marks on tile joints when applying microcement</h2>
                <p>
                    Do you want to apply microcement on tiles? Here are three points to take into consideration to avoid
                    marking tile joints.

                </p>
                <h3>1. Pay attention to the difference in absorption between tiles and joints</h3>
                <p>
                    The resin contained in microcement is absorbed differently. If no precautions are taken, there is a
                    risk that they will appear in the form of a visual spectrum on drying (visual pattern). Moisture in
                    the joints also causes this defect.

                </p>
                <figure class="figure mt-4">
                    <img src="https://www.topciment.com/imagenes/marcas-de-las-juntas-de-los-azulejos-por-humedad.jpg"
                        class="figure-img img-fluid " title="marks on tile joints due to dampness"
                        alt="marks on tile joints due to dampness">
                    <figcaption class="figure-caption">Microcement floor on which the tile joints are marked due to
                        dampness.</figcaption>
                </figure>
                <h3>2. Tile joints are usually hollow </h3>
                <p>
                    Joints are generally hollow, not in the same plane as the tile. Also, the shrinkage of the
                    microcement once dry creates a relief in the joint area (relief pattern). For all these reasons,
                    with our microcement systems we create a layer that insulates and levels.

                </p>
                <h3>3. Let dry 24 hours before applying microcement</h3>
                <p>
                    It is very important that, after filling the tile joints, it is allowed to dry 24 hours before
                    starting the application of the microcement base.

                </p>

                <h2> Trowel marks on the microcement</h2>

                <p>
                    We have to apply the microcement without leaving burrs or reliefs that show the passage of the
                    trowel, sanding these imperfections in each coat. If we apply microcement on a layer with burrs,
                    these reliefs will be copied.

                </p>

                <p>
                    With the steel trowel and excessive pressure these imperfections cause the "burnt" effect, dark
                    spots that reflect the lack of sanding the bottom layer of microcement.

                </p>

                <figure class="figure mt-4">
                    <img src="https://www.topciment.com/imagenes/marca-de-quemado-de-llana-1.jpg"
                        class="figure-img img-fluid " title="trowel burn mark" alt="trowel burn mark">
                    <figcaption class="figure-caption">Microcement floor showing the trowel burn mark </figcaption>
                </figure>
                <h2>Irregularities on microcement surfaces</h2>

                <p>
                    After the application of each layer of microcement, the surface must be sanded. An essential step to
                    avoid the effect described in the previous section. We will carry out a soft roughing with a
                    roto-orbital sander of low revolutions or by means of sanding glove, and sandpaper of silicon
                    carbide.

                </p>
                <p>
                    Approximately 3 hours after the application of the smoothed microcement, a change in the tone of the
                    coating is appreciated, becoming lighter. This change in tone indicates that the microcement is
                    ready to be walked on and is hard enough to be sanded. It should be noted that the temperature,
                    humidity and ventilation of the room influence this time period.

                </p>
                <p>
                    We recommend sanding before 12 hours after applying the microcement so that the coating has not
                    reached an excessive hardness and this step is more complicated.

                </p>
                <h2>Microcement stains from excessive sanding</h2>

                <p>
                    When sanding the last coat of microcement we must ensure that we work the material smoothly. With
                    one hand we will take the sander or sanding glove and with the other we will check with the touch if
                    the sanding is correct. If we work the surface properly we will avoid three types of characteristic
                    stains: circles, contour lines and black marks.

                </p>
                <p>
                    The sander will have to be of the roto-orbital type to avoid leaving circles. We will try to keep it
                    in constant movement without prolonged impact in the same place. If the sanding was excessive we
                    would end up seeing the smoothed microcement of the previous coat and leaving some characteristic
                    "contour lines" that would indicate that we have passed to the lower layer. As soon as the sandpaper
                    is worn we will have to change it, if we do not make the change, the sandpaper goes from sanding to
                    polishing leaving black marks on the microcement.

                </p>
                <h2>Microcement marks due to excessive burnishing </h2>

                <p>
                    If the microcement finish is not as desired, it is best to apply a layer of additional smoothed
                    microcement. Touching up on the already dry surface will leave noticeable and very unsightly marks.

                </p>
                <p>
                    Excessive troweling of an area will leave dark marks. This can occur, for example, when material
                    accumulates on floors at the end of the application path, forcing us to pick up the excess smoothed
                    microcement and work it over with the trowel.

                </p>
                <p>
                    If you overwork an area what you do is force drying. This problem is reflected in the whitening of
                    the area. For example: A black wall could have gray stains much lighter than the color of
                    microcement chosen.

                </p>
                <p>
                    If the applicator goes over the entire surface too much or presses too hard, the smoothed
                    microcement substrate may be lighter in color. It is very typical to see a black wall that looks
                    gray.

                </p>

                    <h2>Where to apply Classic Mettal metallic and rust effect paints</h2>

                    <p>
                        Classic Mettal paints can be applied on almost any type of material. They are compatible with
                        microcement, concrete, brick, ceramic, glass, plaster, plasterboard or paper, among others. In
                        this sense, it is highly recommended for use as a paint for metal due to its high content of
                        metallic particles.

                    </p>
                    <p>
                        You will get the most out of walls, ceilings, facades, walls, pillars and furniture with Classic
                        Mettal. You will be able to transform any room into a completely new one that will not leave you
                        indifferent.

                    </p>
                    <p>
                        Classic Mettal can be applied by trowel, brush, brush, sponge, roller or spray.


                            <h2>Microcement application step by step: application method</h2>
                            <p>The process of applying microcement depends on the Topciment microcement system chosen.
                            </p>

                            <p>However, in general terms, the application of microcement on floors and walls is as
                                follows:</p>
                            <ol>
                                <li> Condition the work surface, lay the mesh and apply the primer or adhesion promoter.
                                </li>
                                <li> Apply two coats of preparatory or base microcement, on a mesh, which will serve as
                                    a substrate for the finishing microcement. 
                                </li>
                                
                                <li>Next, apply two coats of finishing microcement. This is the one that combines the
                                    desired texture and colour, giving the final appearance to the surface.</li>
                                <li>Finally, to protect the microcement, apply two coats of Presealer and two coats of
                                    Topsealer </li>
                            </ol>

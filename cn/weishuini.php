        <h1 class="mb-3">什么是微水泥？ 用途，类型，应用和优点</h1>

       <p>
          Topciment微水泥是一种装饰涂层，由水泥，水基树脂，添加剂和矿物颜料组成，由于其极好的附着力，可用于多种垂直和水平表面。这种装饰材料脱颖而出，是因为它可以直接应用于现有的材料：瓷砖、石膏板、大理石、水磨石或灰泥等等。几乎不做任何拆除工作，厚度只有3毫米，因此可以对空间进行快速翻新。

       </p>  

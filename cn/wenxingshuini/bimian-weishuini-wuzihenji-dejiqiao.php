              <h2>铺涂微水泥时瓷砖接缝处出现痕迹</h2>
<p>
    你想在瓷砖上涂抹微水泥吗？以下是为避免在瓷砖接缝处出现痕迹而需要考虑的三点。

</p>    
<h3>1.注意瓷砖和接缝处的吸收性差别</h3>

<p>微水泥中树脂成分被吸收的方式不同，如果不采取预防措施，有可能在干燥后有视觉上的差异（视觉差异）。接缝的湿度也会导致这种差异感。</p>



<h2>
     微水泥的缺点

</h2>  
<p>
    必须先了解，如果使用高质量的材料，并由专业人士进行应用，微水泥不会有任何缺点。裂缝、裂纹、痕迹和污点，是微水泥最常出现的问题，但是这主要是未遵循微水泥应用过程中的基本步骤，和涂料前的基面状况不佳。
     
</p> 


<h3>
    在应用微水泥时犯的错

</h3>  
<p>
    微水泥的应用有非常明确的步骤。改变顺序或在过程中犯错，特别是在打底漆和密封方面，最后将导致不理想的结果。因此，我们坚持认为，微水泥的应用施工必须由专业人士进行。下面我们将描述最常见的错误。

</p>    


<h4>
    1.在温度过低或过高的环境应用微水泥。

</h4> 
<p>
    应用微水泥的理想环境温度是15ºC和25ºC之间。如果在温度低于15ºC或高于25ºC工作，可能会出现问题。

</p>    
<p>
    如果温度太低，例如发生在冬季，覆盖着微水泥的表面可能需要很长的时间才能正常硬化。另一方面，如果温度太高，微水泥将过快硬化。

</p>    
<h4>
    2.不正确地混合微水泥颜料

</h4> 
<p>
    适当地混合颜料是关键。如果不仔细遵守指示，可能会出现过量的团块，导致涂上微水泥的表面出现黑条纹。

</p>    
<p>
    为了获得Topciment目录中多种多样的微水泥颜色，必须确实遵守每个标签上的用量，包括使用的微水泥数量和类型。

</p>    
<p>
    还应大力摇晃颜料，直到获得均匀的液体。然后将少量倒入容器中，加入所有的颜料并充分混合。然后在同一容器中一点一点地加入微水泥和其余的混合液，直到达到每次应用所需的粘度和均匀的微水泥颜色。

</p>    
<h4>
    3.应用微水泥时不遵守步骤

</h4> 
<p>
    微水泥的应用要求必须严格遵守步骤。如果不遵守，会严重影响最终的效果。

</p>    
<p>
    例如，涂抹在地板或墙壁上是不一样的，因为要应用的微水泥的类型各不相同。同样地，如果没有调节和平整好基面，也会在将来造成重大问题。

</p>    
<p>
    也不能在上底漆或密封方面犯错。在这个页面上，你会学到所有你需要知道的一切，以避免在应用微水泥时犯错。

</p>  
<h3>微水泥的不正确修复</h3>
<p>当有必要修复由前述错误带来的微水泥问题，或者由于使用劣质材料造成磕碰的崩裂。</p>
<p>由于是一个连续的涂面，没有接缝，为了正确地修复，必须大面积地进行，这样反差就不会太明显，避免最后的完成成为灾难。</p>

<h3>
    微水泥的裂缝和裂纹

</h3>  
<p>
    如果微水泥是如此具有抵抗力和硬度的涂层，为什么会出现表面开裂和裂缝的情况？主要有三个原因，解释如下。

</p> 
 <figure class="figure mt-4">
                        <img src="https://www.topciment.com/imagenes/news/restaurante-con-microcemento-blance-en-pared.jpg"
                            class="figure-img img-fluid " title="restaurante microcemento pared y suelo"
                            alt="Microcemento en pared y suelo de un restaurante">
                        <figcaption class="figure-caption">餐厅的墙壁上涂抹白色的微水泥，地板上涂抹灰色的微水泥。</figcaption>
                    </figure>     
<h4>
    1.质量差的微水泥裂缝

</h4> 
虽然这很明显，但必须指出，不是所有的微水泥都能保证不开裂，只有那些高质量的微水泥才能保证。越劣质，随着时间就越容易开裂。因此，非常重要的是，不要吝啬微水泥的质量。

在Topciment，我们向专业人士保证，我们的微水泥具有出色的性能和抗性，因此不会出现裂缝。

<h4>
    2.通过填充伸缩缝的微水泥裂缝

</h4> 
如上述保证，微水泥不需要伸缩缝，因为它是一个连续的涂层。然而，我们必须非常谨慎地调节基面，因为虽然微水泥和许多材料兼容，但必须考虑到那些有伸缩缝的材料如混凝土砂浆。

如果微水泥被放置在一个有伸缩缝的表面上，非常重要的是，它们被尊重而不是被填充。如果这样做，"微水泥 "可能会开裂。

<h4>
    3.由于基面条件差的微水泥裂缝

</h4> 
如果涂抹微水泥的基面状况不佳，就必须进行修复。因为如果做为涂抹微水泥的基础基面条件差，最终会出现裂缝。

<h4>
    4.复合木地板上的微水泥裂缝和裂纹

</h4> 
<p>复合木地板和微水泥不兼容，因为木地板是一种极受环境条件（如湿度）影响而发生变化的材料。湿度会导致木地板膨胀和收缩，所以如果在上面涂抹微水泥，最终会导致开裂。</p>
<p>
    此外，由于木地板是一个不稳定的表面，因为它有大量的移动接缝，在微水泥基面应铺上的玻璃纤维网可能会破损，最后，微水泥表面可能会出现裂缝。

</p>    
                  
                  


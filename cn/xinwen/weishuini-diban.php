<h2>
    如何应用微水泥地板

</h2>  
<p>
    你知道了微水泥地板的所有优点，现在是解释应用过程的时候了。以下是应用微水泥地板需要考虑的六个步骤。

</p>    
<h3>清洁将来的微水泥地板</h3>

<p>要覆盖微水泥的基面必须处于完美状态。因此，第一步是清洁地板的灰尘，污渍，油脂或任何其他类型的颗粒，以确保基面处于完美状态。检查地板的湿度是非常重要的，等待干燥，或应用环氧树脂来阻止湿气上升或作为蒸汽屏障。</p>
<h3>底漆</h3>

<p>一旦表面清洁，在地板上必须涂上一层底漆，以增强现有基面和新的微水泥地板之间的粘合力。如果基面是吸收性的，如混凝土或自流平，应使用PRIMACEM ABS 吸收性底漆；如果是非吸收性的，如水磨石、瓷砖或大理石，应使用PRIMACEM PLUS 非吸收性底漆 (link).。</p>
<h3>第一层微水泥基础</h3>

<p>打完底漆后，涂上第一层未着色的微水泥基础。铺上玻璃纤维网，因为它将帮助最大限度地减少随着时间出现裂缝的机会。铺完后，就对表面进行抛光。</p>
<h3>第二层微水泥基础</h3>

<p>一旦表面完全干燥，再涂上第二层有颜料微水泥基础。在这一步骤中，选择所需颜色的微水泥来覆盖连续的地板，一旦表面被完美覆盖，就对微水泥地板进行抛光。</p>
<h3>两层微水泥地面面层</h3>

<p>第五步包括涂抹两层精致有颜料的微水泥面层。根据你所想要的光洁度，你可以使用中等或较厚的微水泥。作为微水泥专家，我们建议使用两层Microdeck地面面层，我们的微水泥M号粒度最适合用于地板。</p>
<h3>保护微水泥地板</h3>

<p>在应用微水泥的最后一步是最重要的一个步骤。微水泥地板已经准备好了，现在你需要保护它。要做到这一点，先用滚筒涂抹两层PRESEALER 封孔剂 ，然后再涂抹两层TOPSEALER 密封罩面剂。除了保护地板的微水泥外，它还可以防水，甚至得到防滑的效果。</p>


<h2>
    避免微水泥地板问题的建议

</h2>  
<p>
    在地板上涂抹微水泥后的最初几天，房间必须通风以确保干燥。如果不这样做，连续涂料的最终效果将与你所期望的相去甚远。

</p>    
<p>
    同样，在使用后的第一周，必须避免踩踏微水泥地板。同样重要的是，要避免沾到液体，以免出现污渍或损坏树脂，最后，要避免来回移动家具。如果遵守所有这些提示，将来就不会出现微水泥地板的问题。


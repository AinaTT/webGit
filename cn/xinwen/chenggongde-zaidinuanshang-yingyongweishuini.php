                   <h2>
                        如何在地暖上应用微水泥：每个步骤

                   </h2>  
<p>
    近二十年来，我们的技术团队一直为专业人士提供咨询和解决有关微水泥应用的疑惑。其中最经常出现的问题是在地暖上使用微水泥的可能性。
     
</p>
<p>
    下面，我们解释一下每个步骤，请注意我们的建议。
     
</p>
                <h3>
                    在应用微水泥前加热地板
     
                </h3>
<p>
    在地暖上涂抹微水泥之前，必须依照标准方法开启地暖。这将避免因温度变化而造成裂缝， 最后损害微水泥。
     
</p>
<p>
    在铺涂微水泥前至少4周要开启地暖。
     
</p>
<p>
    将地暖管道的水保持在25ºC，为期2或3天。
     
</p>
<p>
    逐渐提高水温，直到达到45ºC。这样保持数天。
     
</p>
<p>
    在安装微水泥前48小时关闭暖气，非常重要的是，地板的最高温度不超过18ºC。
     
</p>
<h3>在地暖上应用微水泥</h3>
 
<p>
    微水泥地板必须铺设在已经凝固的水泥砂浆上，在达到这一最佳阶段之前，必须经过三到四周的时间，时间可能因天气而异，必须检查砂浆是否完全干燥（最大湿度为5%）。
     
</p>
<h3> 铺涂完微水泥后，打开地暖</h3>
 
<p>
    一旦应用微水泥到地暖上，至少要等待48小时后才能重新打开暖气。必须考虑到以下几个方面：
     
</p>
<p>
    应逐渐开启，温度应每天提高5ºC。
     
</p>
<p>
    在使用地暖的开始和结束时要分阶段逐渐调节温度。
     
</p>
<p>
    确保房间内的相对湿度不要太低。
     
</p>
<p>
    避免使用地毯，或者因家具和地暖之间的空间太小而导致热量积聚。
     
</p>

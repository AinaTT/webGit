                <h2>
                    清洁微水泥的建议 

                </h2>  
<p>
    我们的专家建议在清洗微水泥时要考虑到一些因素，由于对微水泥墙的影响比地板小得多，因为它们不是可通行的表面，我们将重点放在微水泥地板。

</p>    
<p>
    这种正在急速成长的装饰性涂料，可以铺涂在所有类型的地板，无论是私人住宅还是大型仓库或商业场所。

</p>    
<p>
    这是关于清洁微水泥的四个最重要的建议：

</p>    
<p>
    1. 铺涂微水泥后的第一周，在任何情况下都不要清洁地板，这是非常重要的。在微水泥地板的干燥过程中，房间必须通风，使其完全干燥。洒在地板上的任何类型的液体都可能改变微水泥的颜色，使其地板表面变浅甚至变白。因此，要绝对避免，甚至在地板上行走。

</p>    
<p>
    2. 第一周之后，微水泥地板只能水，非常温和地清洁。 为什么？因为必须等待微水泥地板的孔隙完全封闭，而这是在第一周之后。

</p>    
<p>
    3. 至少要等一个月，让微水泥地板完全达到它的硬度和阻力。在这段时间后，才可以用水和中性肥皂清洁。

</p>    
<p>
    4. 在任何情况下都不能使用研磨性或破坏性产品，如金属刷、砂纸、酸或氨等， 因为微水泥地板会被严重损坏。
           
</p>           

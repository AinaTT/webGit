                  <h2>
                      微水泥厨房台面如何施工？

                  </h2>   
<p>
    微水泥厨房台面的应用过程决定于你要的最后饰面效果和基面材料，无论是吸收性表面如石膏板、灰泥、水泥和沙子等，还是非吸收性表面如大理石、水磨石、瓷砖或花岗岩等。

</p>    
<p>
    依照你要的效果，我们详细介绍微水泥厨房台面的应用：

</p>    
<h3>1. 铺网格在基面上</h3>

<p>
    第一步是将网格放在将形成微水泥台面基础的表面上：大理石、花岗岩、石膏板......。这是非常重要的，因为这将防止随着时间的推移出现裂缝。

</p>    
<h3>2. 微水泥厨房台面上底漆</h3>

<p>
    第二步的施工是涂上底漆，这将有利于吸收性或非吸收性表面基面与将铺涂的微水泥的结合。吸收性表面基面应使用PRIMACEM ABS 底漆，非吸收性表面基面应使用PRIMACEM PLUS底漆。

</p>    
<h3>3. 涂抹两层Microbase微水泥基础</h3>

<p>
    接下来，涂抹两层Microbase微水泥基础。第一层应该是没有颜料的，在打磨之后，再涂上第二层加了色浆的Microbase基础，可以选择你想要的厨房台面颜色。之后，再次进行打磨。如果你想要石头或乡村风格，在两层Microbase基础之后，就可以用Presealer封孔剂保护台面，然后再涂上Topsealer聚氨酯密封罩面剂。

</p>    
<h3>4. 涂抹两层Microfino微水泥面层</h3>

<p>
    然而，如果你想要厨房台面有明显的水印或灰泥的外观效果，在两层Microbase微水泥基础之后，再涂两层Microfino微水泥面层，两层都要有颜料。每一层之后都必须进行打磨，没有例外。当两层Microfino涂抹完毕后，先用Presealer封孔剂和Topsealer密封剂进行保护。

</p>    
<h3>5. 涂抹两层Microdeck微水泥面层</h3>

<p>
    如果你希望厨房台面是非常自然的外观，在两层Microbase基础之后，建议涂抹两层有颜料的Microdeck微水泥面层，最后再打磨。然后，与前两次应用一样，Presealer封孔剂和Topsealer密封剂保护。

</p>    

<h3>6. 保护和密封微水泥厨房台面</h3>

<p>一旦涂上两层Microbase基础、Microfino或Microdeck面层，根据所需的表面处理，必须正确地密封，如上所述，需要涂抹两层Presealer封孔剂和两层Topsealer密封罩面剂。</p>

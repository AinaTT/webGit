                    </p>
                    <p>
                        在旧瓷砖上铺涂微水泥的另一个好处是可以达到防水饰面。例如，浴室是一个经常受到环境湿度影响的空间，因此，不是任何装饰性涂料都能做防水性。

                    </p>

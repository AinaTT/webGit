                    <h2> 用微水泥翻新旧瓷砖：应用和技巧</h2>

                    <p>
                        要在旧瓷砖上铺涂微水泥，首先非常重要的要雇用专业、合格的施工人员，他们保证施工的质量，例如我们的专用微水泥施工人员。

                    </p>
                    <p>
                        只有专用施工人员的施工质量，你才可以避免一些问题，大多是施工错误造成的。

                    </p>
                    <h3> 在瓷砖上应用微水泥的步骤</h3>

                    <h4>1. 评估瓷砖的状况</h4>

                    <p>
                        在施工微水泥之前，首先要做的是检查瓷砖的状况。如果地板或墙砖有任何缺陷，不能直接覆盖微水泥，就像没看到一样。在铺设之前，必须对基面进行修复。这就是为什么由专业人员进行装修是很重要的，因为他们会知道如何找到这些缺陷。

                    </p>
                    <h4>2. 在应用微水泥之前先清洁瓷砖 </h4>

                    <p>在修复瓷砖后，应用微水泥之前，必须彻底清洁瓷砖，使其没有油脂或灰尘。</p>

                    <h4>3. 用微灰泥填充瓷砖缝隙</h4>

                    <p>
                        一旦瓷砖干净了，第一步是填充瓷砖接缝。由于光滑的微水泥是一个连续的覆盖涂料，如果瓷砖的接缝没有被填满，基面将不稳固，并且会有不均匀的接缝标记。

                    </p>
                    <h4>4. 在要覆盖的瓷砖上底漆</h4>

                    <p>
                        当表面平整，瓷砖缝隙被完美填充后，就可以涂抹适当的底漆。这是重要的产品，因为它是瓷砖和微水泥之间粘合的桥梁，使后者能更好地粘附。

                    </p>
                    <p>
                        在Topciment，我们依照不同类型的基面生产了一系列的底漆，对于瓷砖，我们的专家建议使用这两种中的一种：Primacem Impoxy A+B抗潮湿环氧底漆或Primacem
                        Plus非吸收性表面底漆。我们为你解释这两种产品，以便知道在那种基面使用什么底漆。

                    </p>
                    <p>
                        Primacem Impoxy A+B抗潮湿环氧底漆 ，可以阻挡上升的湿气并作为水汽屏障。 100%固化，双组分，是淋浴区墙砖或浴室地砖的完美选择。一种创新的产品，也避免了接缝痕迹。

                    </p>
                    <p>
                        另一方面，你也可以使用Primacem Plus非吸收性表面底漆，这是我们专门为瓷砖等非吸收性表面设计的底漆，只需涂抹一层。

                    </p>


                    <h3> 用微水泥铺设瓷砖的提示和建议</h3>

                    <h4>1.遵循干燥时间</h4>

                    <p>
                        当使用非吸收性表面的底漆时，如Primacem
                        PLUS，在涂抹微水泥前，遵循干燥时间是非常重要的。如果不严格遵守技术参数中规定的指示和干燥时间，水分会在涂层之间滞留，因此瓷砖的接缝处会出现痕迹。

                    </p>
                    <p>
                        遵守这个指示，你将避免在瓷砖上应用微水泥时最常见的失败。然而使用Primacem Impoxy
                        A+B环氧防潮底漆可以避免这种可能的错误，由于它具有完全阻隔孔隙湿度的能力，因此对干燥时间不必那么久。

                    </p>
                    <h4>2. 在涂抹第二层微水泥之前，请等待24小时的干燥时间。</h4>

                    <p>
                        Topciment专家的另一个建议是，在使用Primacem PLUS非吸收性表面底漆的情况下，在第一层和第二层微水泥之间要留出24小时的干燥时间。

                    </p>
                    <p>
                        这个过程不是每个人遵循的，因为它不是强制性的，但专业施工人员建议这样可以确保在瓷砖上应用微水泥时，不会在接缝处出现裂痕的标记。有了Primacem Impoxy
                        A+B环氧防潮底漆，就可以用依正常时间完成成。也就是说，在第一层之后可以立即涂抹第二层Microbase基础。

                    </p>
                    <h4>3. 在涂抹第二层微水泥之前，给瓷砖打底漆</h4>

                    <p>
                        我们还建议，在涂抹第一层微水泥24小时后，即有足够的时间让它正常干燥，然后再次用Primacem PLUS打底。

                    </p>
                    <p>
                        最专业，最有经验的专家的提示，让下一层的微水泥均匀地吸收。大大减少了不均匀的视觉痕迹。

                    </p>
 

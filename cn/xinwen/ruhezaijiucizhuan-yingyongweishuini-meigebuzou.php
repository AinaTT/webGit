                    <h4>5. 在瓷砖的第一层铺上网格</h4>

                    <p>
                        在干燥几小时后 ，在涂抹微水泥之铺上网格在瓷砖上，以防止未来的涂层裂缝、裂纹等。不是每个人都会考虑到这一步骤，而没有网格会带来很头痛的问题。

                    </p>
                    <p>
                        接下来，涂抹第一层没有颜料的微水泥基础（Microbase），一旦瓷砖被微水泥很好地覆盖均匀，没有不规则的地方，就用砂纸打磨, 然后再涂上一层的底漆。

                    </p>
                    <h4>6. 在瓷砖上涂抹第二层微水泥</h4>

                    <p>
                        涂抹第二层微水泥基础（Microbase），这时必须混合想要的颜色在微水泥基础上，以覆盖旧瓷砖。与上一步骤相同，进行打磨。

                    </p>
                    <h4>
                        7. 涂抹两层Microdeck微水泥面层或Microfino微水泥面层

                    </h4>
                    <p>
                        在微水泥基础之后，涂抹两层Microdeck面层或两层Microfino面层。前者通常用于地板，后者用于墙壁。但是可以根据你的口味、喜好和想要的饰面进行组合。这两层都应该已经混合色浆。之后打磨。

                    </p>
                    <h4>8. 保护和密封微水泥 </h4>

                    <p>
                        最后一步也是最重要的步骤之一，它是为了保护微水泥涂层。为此，我们有各种各样的清漆和密封剂。虽然在这个情况，我们建议使用两层Presealer封孔剂 和两层Topsealer WT One
                        Coat密封罩面剂。

                    </p>

                  <h2>
                       微水泥楼梯的类型

                  </h2>   
<p>
    微水泥让覆盖楼梯的设计有无限可能性，在它的分类中，有两个因素。首先，微水泥楼梯是不是在墙壁之间，以及楼梯台阶类型。

</p>    
<p>
    这样我们有几类的微水泥楼梯：在两面墙之间的楼梯，有一面墙的楼梯，没有墙的楼梯，

</p>    
<p>
    楼梯用哪种微水泥和颜色？
     
</p>
<p>
    虽然灰色是微水泥楼梯最流行的颜色，但你可以应用各种颜色，为你的住宅或公司的装饰提供一个与众不同的元素。在Topciment，我们有多种颜色，从简约和工业风的中性色彩到更鲜艳的装饰，都有色彩供选择。

</p>    
<p>
    至于微水泥的类型，我们建议用Microdeck微水泥地面面层来铺涂楼梯。在Topciment，我们已经开发了三种类型的Microdeck地面面层：Sttandard Microdeck（标准系列），Unlimitted Microdeck（无局限系列）和Evoluttion Microdeck（单组分微水泥系列）。
      
</p>                   

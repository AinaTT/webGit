                  <h2>  如何应用微水泥楼梯：</h2>

<p>
    在楼梯上应用微水泥时，首先要记住的是，只能由有经验的专业人员施工。由于用微水泥楼梯需要熟练的应用技术才能达到理想的效果。必须考虑到一系列的特殊性，只有经过培训的专业人士才能保证成功。

</p>    
<h3>8个步骤应用微水泥楼梯</h3>

<p>需要遵循8个步骤</p>

<h4>1.评估基面的状态</h4>

<p>
    在应用微水泥之前，必须查看楼梯上现有的基面状态。如果基面的状况不好，各种裂缝或缺陷可能会出现。一个好的专业人员会知道如何正确评估基面的质量。

</p>    
<h4> 2.为微型水泥楼梯打底 </h4>
 
<p>
    一旦确保基面的状况良好，第二步就是涂抹底漆。

</p>     
<h4>3.平整微水泥楼梯的台阶</h4>
 
<p>
    第三步是平整和保护台阶，要去除可能存在的任何不规则表面来防止未来可能的破损和裂缝。
     
</p>
<h4>4.  在楼梯上涂抹第一层微水泥Microbase基础 </h4>
 
<p>第四个步骤是涂上第一层不含色浆的Microbase基础， 然后打磨。</p>

                      <figure class="figure mt-4">
                                                    <img src="https://www.topciment.com/imagenes/news/escalera-microcemento-interior.jpg"
                                                        class="figure-img img-fluid " title="有中央支撑的室内微型水泥楼梯" alt="有中央支撑的室内微型水泥楼梯"> <figcaption class="figure-caption">有中央支撑的室内微型水泥楼梯。
</figcaption>
                                                </figure>
                   
<h4>5. 在楼梯上涂抹第二层微水泥Microbase基础</h4>
 
<p>
    第五步骤是涂上第二层Microbase基础，需要加上色浆， 然后打磨。
     
</p>
<h4>6. 在楼梯上涂抹第一层微水泥Microdeck地面面层</h4>
 
<p>
    第六步是涂上第一层薄薄的带颜色的Microdeck地面面层，干燥后，进行打磨。

</p>     
<h4>7. 在楼梯上涂抹第二层微水泥Microdeck地面面层</h4>
 
<p>第七步是涂抹第二层Microdeck地面面层，干燥后，进行打磨。</p>
 
<h4>8.  密封和保护微水泥楼梯</h4> 
 
<p>在楼梯上应用微水泥的最后一步是最重要的一步，是为了保护涂层，让它长久保持最佳状况。要做到这一点，Topciment建议应用两层Presealer封孔剂 和两层我们的聚氨酯清漆之一，特别是Topsealer WT One Coat 密封剂。</p>
                    
                  <h2>  在楼梯上应用微水泥的3个建议</h2>

<p>由于施工应用较复杂，不是每个人都会铺涂微水泥楼梯，只有具有多年经验的专业人士才能保证成功。</p>

<p>以下是应用微水泥楼梯的三个建议</p>

<h3>1.在微水泥楼梯的边缘用阳角条保护</h3>

<p>
    微水泥楼梯的角落或边缘无疑是最容易受到各种划痕的地方，作为制造商，我们建议使用铝阳角条 。它可以隐藏（但是我们建议不需要隐藏，因为它们提供视觉上的优雅感），并且可以更好地保护微水泥楼梯，从而防止断裂或磨损。

</p>    

                      <figure class="figure mt-4">
                                                    <img src="https://www.topciment.com/imagenes/news/escalera-microcemento-marfil-con-perfil-aluminio.jpeg"
                                                        class="figure-img img-fluid " title="Acero外部微水泥楼梯" alt="Acero外部微水泥楼梯"> <figcaption class="figure-caption">Topciment公司的外部微水泥楼梯在房子的外部。</figcaption>
                                                </figure>
                   <h3>2. 微水泥楼梯的最小圆角</h3>

<p>
    虽然可以将微水泥楼梯的台阶留成90度，但最好将边缘磨圆，哪怕只是最小的圆角。遵循这一建议可以避免将来的碰撞裂痕，这样对微水泥楼梯的影响较小。

</p>    
<h3>3.在微水泥楼梯必须使用网格</h3>

<p>
    虽然有人并不总是在微水泥的基面铺上网格，但在Topciment我们建议这么做，网格可以强化保护微水泥楼梯。

</p>    

                   

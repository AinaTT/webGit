                        <h2>Conditions prior to application</h2>
                        <h3>Cleanliness.</h3>
                        <p>The substrate must be clean, free of dust and grease.</p>
                        <h3>Flatness. </h3>
                        <p>Microcement is applied in layers of no more than 1mm, and the sum of these will not exceed
                            four millimetres, so we cannot solve flatness issues with this material, we must solve the
                            irregularities of the support before starting the application. The <a
                                href="../microcement">Microcement</a> can be applied on floors, walls and even ceilings,
                            the final result will depend on the substrate and the work of the applicator.</p>
                        <h3> Consistency.</h3>
                        <p>The application base must be stable. Substrates with powdery mortars or loose tiles are not
                            suitable; depending on their state, they will have to be removed or consolidated with
                            quick-setting mortars with compensated shrinkage. </p>
                        <h3>Free of damp.</h3>
                        <p>If there is damp in the substrate, microcement cannot be applied. It will be necessary to
                            first determine its origin and eliminate it, we recommend using a moisture meter to check
                            that the humidity never exceeds 5%.</p>
                        <p> The substrate must have undergone a sufficient setting process. We must bear in mind that
                            the temperature and relative humidity of the environment, together with the ventilation of
                            the work space, affect the drying process of the mortars and accelerate or delay the setting
                            process.</p>
                        <h3>
                            <h3>Protection of the work area</h3>
                            <p>We must protect the work area properly and the surfaces that we are not going to cover,
                                as microcement adheres very strongly and is difficult to clean when it dries. We will
                                use protection material common in painting works, such as painter's tape, protective
                                paper, plastic covers...</p>

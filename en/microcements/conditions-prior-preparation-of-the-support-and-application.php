                        <h2>Preparation of the substrate</h2>
                        <p>The application of the microcement will begin with the priming of the substrate using
                            adhesion promoters and the placement of the fibreglass mesh. </p>
                        <h3>Priming with adhesion promoters</h3>
                        <p>Adhesion promoters have several functions: they ensure better adhesion on any type of
                            substrate, increase its cohesion, improve the speed of application of the first coat of
                            microcement, and prevent the premature loss of moisture from the micro-mortar. </p>
                        <p>Supports have different absorption capacities, so we must use solutions for each of them,
                            promoters for non-absorbent surfaces, for absorbent surfaces, for surfaces that need a
                            certain degree of consolidation or promoters for surfaces that may have moisture problems.
                        </p>
                        <p>
                            <h3>Microcement on stoneware, terrazzo, hydraulic tile or marble</h3>
                            <p>To coat this type of support that is not very porous or not at all porous, we must use
                                Primacem Plus, promoter for non-absorbent surfaces, the process will be as follows:</p>
                            <ul>
                                <li>Fill the joints with Microbase and leave to dry for 24 hours. If the joints are very
                                    thin we can use Microdeck or even Microfino. </li>
                                <li> Apply a layer of Primacem PLUS by roller together with the Builtex mesh. </li>
                                <li> Allow to dry for at least 30 minutes and apply the first coat of Microbase. </li>
                                <li> Allow to dry for 24 hours, apply a coat of Primacem PLUS and apply the second coat
                                    of Microbase.




                <h2>Excess sanding stains</h2>
                <p>When sanding the last coat of microcement we must make sure to work smoothly, with one hand we will
                    hold the sander or the sanding glove and with the other we will check with the touch if the sanding
                    is correct. If we work properly we will avoid three types of characteristic stains: circles, contour
                    lines and black marks.</p>
                <p> The sander must be of the roto-orbital type to avoid leaving <strong>circles</strong>. Try to keep
                    it in constant motion without prolonged contact with the same spot. If the sanding were excessive,
                    we would end up seeing the microcement of the previous coat and leaving some <strong>level
                        curves</strong>&quot; characteristics that would indicate that we have moved on to the lower
                    layer. As soon as the sandpaper wears out we will have to change it, if we do not change it, the
                    sandpaper goes from sanding to polishing leaving <strong>black marks</strong> on the microcement.
                </p>
            </div>
            <div id="halfTextRight" data-animate-scroll='{"x": "100", "y": "0", "alpha": "0", "duration": "2"}'>
                <h2>Excessive burnishing on microcement</h2>
                <p>Touch-ups when the microcement is already starting to dry will leave obvious and unaesthetic marks,
                    the most appropriate thing to do when the finish is not the desired one is to reapply an additional
                    coat. </p>
                <p>Excessive trowelling will leave dark marks. This can happen, for example, when material accumulates
                    on floors at the end of the application process, forcing us to pick up the excess microcement and
                    overwork it with the trowel.</p>
                <p> If you over-trowel an area, what you are doing is forcing it to dry. This problem is reflected in
                    the whitening of the area. For example: A black wall could have grey stains that are much lighter
                    than the chosen colour. </p>
                <p>If the applicator goes over the whole surface too much or presses too hard the surface can become a
                    lighter colour. It is very typical to see a black wall that looks grey. </p>




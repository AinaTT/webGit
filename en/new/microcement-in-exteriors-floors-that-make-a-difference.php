                    <figcaption class="figure-caption">The design villa Carmen House has opted for microcement as
                        exterior coating for its façade.
                    </figcaption>
                </figure>
                <h2>Ideas for using microcement in exteriors</h2>

                <p>
                    Microcement can be used to perfection in many ways in outdoor spaces. Here are some of the best
                    ideas for using microcement for exteriors thanks to its unlimited customisation of finishes and
                    colours. Take notes!

                </p>
                <h3>Microcement for outdoor terraces</h3>

                <p>
                    Microcement flooring is the most common use of microcement outdoors, especially on terraces. It is a
                    coating capable of covering large areas such as terraces in a single piece, thus preventing the
                    accumulation of dirt. But above all for the beauty of its finishes and the magnificent resistance to
                    water and environmental humidity that microcement for exteriors presents.

                </p>
                <h3>Microcement for exterior façades</h3>

                <p>
                    Walls and façades coated with microcement offer the same benefits as pavements. And if it is also
                    combined with an exterior microcement floor, the feeling of spaciousness will be multiplied
                    exponentially. The wide range of customisation makes it possible to obtain finishes ranging from
                    more minimalist and industrial to rougher or rustic.

                </p>
                <h3>Microcement for outdoor swimming pools</h3>

                <p>
                    When it comes to building a swimming pool, it is common to resort to building pools. With
                    microcement pools, the same spectacular result can be achieved with the added advantage of not
                    having to carry out costly building work. This time saving is directly proportional to the saving in
                    labour costs.

                </p>
                <p>
                    Not to mention that outdoor microcement for swimming pools adapts perfectly to any type of space,
                    even if your terrace is small or has complicated slopes.

                </p>
                <figure class="figure mt-4">
                    <img src="https://www.topciment.com/imagenes/news/microcemento-exterior-piscina.jpg"
                        class="figure-img img-fluid " title="Double height microcement swimming pool"

                        microcement.</figcaption>
                </figure>
                <h2>How much does microcement for outdoors cost?</h2>

                <p>
                    It may seem that the <strong><a class="links" href="microcement-price">price of microcement
                        </a></strong> outdoors is higher than indoors.
                    However, this is completely wrong. The final cost of applying microcement for exteriors and
                    interiors depends on the same factors: the state of the substrate (if it is damp, if it is not
                    stable, if it needs to be repaired, etc.), the surface area in square metres to be coated and the
                    difficulty of the work itself.

                </p>
                <p>
                    Only on one occasion is the price of outdoor microcement higher: in the case of swimming pools. Why?
                    Because <strong><a class="links"
                            href="../microcements/microcement-for-pools-atlanttic#aplicacion">its
                            application</a></strong> is much more complex and requires more
                    expensive labour.

                </p>
                <h2>How to apply microcement in exteriors correctly</h2>

                <p>
                    In order to apply microcement on exteriors without problems, a series of steps must be followed to
                    the letter. Making mistakes in the process would mean a final finish very different from the desired
                    and expected one. That is why it is so important to have a trained and experienced professional.

                </p>
                <p>
                    These are the six steps to apply microcement in exteriors:

                </p>
                <p>1. Condition the substrate to be coated with microcement for exteriors. Check that it is in good
                    condition and, if not, repair it. </p>
                <p>2. Place the fibreglass mesh on the exterior microcement floor or wall, which will help to reinforce
                    the future continuous decorative coating for exteriors. </p>
                <p>3. Prime the exterior microcement coating with an adhesion promoter. </p>
                <p>4. Apply two coats of microcement base for exteriors. </p>
                <p>5. Apply two coats of exterior finishing microcement. </p>
                <p>6. Seal the exterior microcement coating with a good varnish. </p>
                <figure class="figure mt-4">
                    <img src="https://www.topciment.com/imagenes/news/microcemento-exterior-terraza.jpg"
                        class="figure-img img-fluid " title="Microcement floor outside terrace"

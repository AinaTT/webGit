                    <figcaption class="figure-caption">Luxurious house whose microcement floor on terrace and swimming
                        pool favour the continuity of the space.
                    </figcaption>
                </figure>
                <h3>Application process of an exterior microcement floor</h3>

                <p>
                    The <strong><a class="links"
                            href="../microcements/how-to-apply-microcement-microcement-application-step-by-step">application
                            of an exterior microcement floor</a> </strong> is always the same. The only
                    thing you have to choose is the type of microcement you want (we recommend Sttandard Microstone) and
                    the colour.

                </p>
                <h3> Application process of exterior microcement on walls</h3>

                <p>
                    The application of exterior microcement on walls differs from that of pavements.
                    <strong><a class="links"
                            href="../microcements/how-to-apply-microcement-microcement-application-step-by-step">In this
                            other page</a></strong>
                    you will find a detailed step-by-step instructions.

                </p>
                <h3>Process of applying exterior microcement to swimming pools</h3>

                <p>
                    The non-slip and waterproof nature of microcement pools, as well as their resistance to the effects
                    of the sun, make it one of the most intelligent uses for exterior microcement. As mentioned above,
                    its application is somewhat more complex. <strong><a class="links"
                            href="../microcements/microcement-for-pools-atlanttic">Here</a> </strong>you will find
                    everything you need to
                    know about the application of exterior microcement in swimming pools.

                </p>
                <p>
                    As you have been able to see, microcement in exteriors is very versatile. All kinds of structures
                    and atmospheres can be created depending on where it is applied. So now that you know everything you
                    need to know about microcement for exteriors, what are you waiting for starting to remodel your
                    space?
                </p>

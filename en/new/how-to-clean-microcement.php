                <h2> Tips and recommendations for cleaning microcement</h2>

                <p>
                    Our experts recommend taking into account a series of factors when cleaning microcement. As the
                    impact on microcement walls is considerably less than in the case of pavements, since they are not
                    walkable surfaces, we are going to focus on microcement floors.

                </p>
                <p>
                    This decorative coating is experiencing exponential growth and can be found as a covering for all
                    types of floors, both in private homes and in large warehouses or commercial premises.

                </p>
                <p>
                    Here you have four of the most important tips for cleaning microcement:

                </p>
                <p>
                    1. In the first week after applying the microcement, it is very important not to clean the floor
                    under any circumstances. During the drying of the microcement floor, it is essential to air the room
                    so that it dries properly before carrying out any cleaning work on the microcement. Spilling any
                    type of liquid on the floor could change the colour of the microcement floor, causing the surface to
                    lighten or even whiten. Therefore, avoid at all costs even walking on the floor.

                </p>
                <p>
                    2. After the first week following the application of microcement on the floor, the microcement floor
                    can be cleaned very gently using only water. Why? Because it is necessary to wait for the porosity
                    of the microcement floor to close completely, and this only happens after the first seven days.

                </p>
                <p>
                    3. Wait at least one month for the microcement floor to acquire the hardness and resistance it
                    provides. After this time, the microcement can be cleaned with water and neutral soap.

                </p>
                <p>
                    4. Do not use under any circumstances abrasive or aggressive products such as metallic brushes,
                    sandpaper, acids or ammonia, among others. If you do so, the microcement floor could be seriously
                    damaged.

                </p>

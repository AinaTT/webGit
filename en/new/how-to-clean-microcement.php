                <h2> Specialised products for cleaning microcement</h2>

                <p>
                    Although water and neutral soap are enough to clean microcement, both floors and walls, at Topciment
                    we offer two specialised products for cleaning microcement. Two cleaners that enhance even more the
                    durability of the optimal state of this innovative coating.

                </p>
                <h3>Cleaning microcement with Ecoclean Basic</h3>

                <p>
                    <a class="links"
                        href="https://topciment.com/en/microcements/cleaning-and-care-of-microcement#ecoclean-basic">Ecoclean
                        Basic</a>
                    is an ecological detergent cleaner aimed at a more domestic environment, as it is ideal for cleaning
                    microcement walls and floors in private homes. Thanks to its high degreasing and descaling power, it
                    quickly removes accumulated grease, food residues, limescale and oil marks.

                </p>
                <p>
                    But how is it applied to the microcement floor or the microcement wall? It's very easy. Just dilute
                    it with water and apply it to the surface with a mop, sprayer, spray ball or sponge. To clean the
                    microcement in depth, the product should be left to act for 5 to 15 minutes.

                </p>
                <h3>Cleaning microcement with Ecoclean Pro</h3>

                <p>
                    At Topciment we also have <a class="links"
                        href="https://topciment.com/en/microcements/cleaning-and-care-of-microcement#ecoclean-pro">
                        Ecoclean Pro </a>, an environmentally friendly, alkaline detergent cleaner. A feature that leads
                    to the need to use protection to clean microcement on walls and floors, which is why it is intended
                    for purely professional use.

                </p>
                <p>
                    Ecolean Pro is highly recommended for cleaning garages or workshops, hotels, restaurant kitchens,
                    hospitals, hotels, airports, shopping centres and other large surfaces. Unlike Ecoclean Basic, its
                    degreasing power is much higher, and it is therefore able to remove practically all stains and
                    marks. Even the most difficult and persistent ones such as tyre marks, release agents, pollution,
                    soot, limescale, oil, food residues, grease, blood, etc.

                </p>
                <p>As for its <strong>application </strong>on microcement walls or microcement floors, it can be
                    applied<strong>both concentrated and diluted in water</strong>depending on the degree of soiling of
                    the surface. In the case of diluting the cleaner with water and then spraying it on the surface to
                    be treated, the product should be left to act for 5 to 15 minutes. Once this time has elapsed, wipe
                    with a cloth or wipe and then rinse with water. </p>




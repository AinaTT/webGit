                <h1>Microcement worktops: aesthetics and functionality</h1>
                <p>
                    The excellent performance of microcement and its wide range of application possibilities have made
                    it possible for this decorative coating to be widely used by construction professionals to carry out
                    all kinds of works.

                </p>
                <p>
                    Microcement floors and microcement walls are undoubtedly the two most widespread uses of this
                    coating. However, it is becoming increasingly common to see other types of surfaces such as
                    terraces, roofs, facades and even furniture made of this innovative material.

                </p>
                <p>
                    In this sense, microcement worktops have become the number one piece of furniture covered with this
                    popular coating thanks to its aesthetics, functionality and wide variety of uses. Their distinctive
                    sense of continuity, chromatic variety and the possibility of achieving different looks and finishes
                    have made worktops with microcement a trendy piece of furniture. The following are the main
                    advantages of applying microcement on worktops.

                </p>
                <h2>Advantages of microcement worktops</h2>
                <figure class="figure mt-4">
                    <img src="https://www.topciment.com/imagenes/encimeras/encimera-cocina-con-microcemento-topciment.jpg"
                        class="figure-img img-fluid " title="black microcement worktop"
                        alt="Worktop covered with black microcement" width="auto" height="auto">
                    <figcaption class="figure-caption">Worktop covered with our Black colour microcement.
                    </figcaption>
                </figure>
                <p>
                    When creating a worktop, the choice of material should not be limited to selecting a colour or
                    texture, as some areas such as the splashback of the worktop and the water area require a material
                    that is highly resistant to stains, easy to clean and able to withstand abrasion.

                </p>
                <p>
                    Microcement worktops more than meet these requirements and, in addition, guarantee another series of
                    advantages that make this coating the perfect product to apply in kitchens, bathrooms, hotels,
                    restaurants or cafeterias, among many other spaces.

                </p>
                <h3>1. Microcement worktops are very easy to clean</h3>

                <p>
                    In this type of furniture, which is constantly exposed to dirt, comfort is above all else.
                    Microcement worktops are very easy to clean, just wipe them with a cloth or a cloth with neutral
                    soap and water is enough. A perfect material to avoid the proliferation of germs and bacteria on the
                    surface.

                </p>
                <h3>2. Good response of the microcement worktop to humidity</h3>

                <p>
                    Microcement worktops have a good resistance to environmental humidity and have the possibility of
                    being waterproof if sealed with one of our<a href="../microcements/polyurethane-waterbased-acrylic-varnishes-topsealer">
                        polyurethane varnishes </a>. These qualities have
                    boosted the coating of worktops with microcement.

                </p>
                <h3>3. Microcement countertop for many years</h3>

                <p>
                    As with cleaning, this material requires almost no maintenance. So you will have your microcement
                    worktop in optimum condition for a long, long time. A differential element with respect to other
                    coatings, since when renovating spaces, we always look for a material that does not need to be
                    repaired over the years.

                </p>


                <h3>4. Functionality and resistance of microcement worktops</h3>

                <p>
                    Microcement worktops are resistant to stains, cleaning products, abrasion, scratches and knocks. An
                    extraordinary functionality to which we must add that if the microcement with which the worktop is
                    coated is of high performance such as Topciment, it will not crack.

                </p>
                <h3>5. Microcement worktops: aesthetics and variety of colours</h3>

                <p>
                    Microcement worktops can be given any shape and decorative style thanks to the variety of colours
                    available to suit the room in which they are located. From a more rustic and vintage style to a more
                    avant-garde, minimalist, urban or industrial style. Among the wide range of <a
                        href="../microcements/pigments-for-microcement-arcocem">microcement colours</a>
                    for Topciment worktops are white, grey, brown or black, among many others.

                </p>
                <p>
                    Worktops with microcement where elegance and timelessness are the protagonists. A natural look that
                    is very difficult to imitate with other materials and that can be combined with other elements such
                    as concrete, wood or metals.

                </p>
                <h3>

                    6. Microcement can be applied over the pre-existing worktop
                </h3>

                <p>
                    The quintessential quality of microcement is its ability to be applied on all types of materials:
                    stone, tiles, ceramic, marble... Therefore, instead of building a microcement worktop from scratch,
                    microcement can be applied over the existing worktop, whatever the material, except for wood. In
                    addition, microcement can be easily adapted to any shape of worktop.

                </p>
                <p>
                    This advantage of microcement worktops contributes to a much faster and, of course, more economical
                    renovation of the worktop compared to other decorative coatings.

                </p>
                <figure class="figure mt-4">
                    <img src="https://www.topciment.com/imagenes/encimeras/microcemento-encimera.jpg"
                        class="figure-img img-fluid " title="grey microcement worktop the_ply"
                        alt="Grey microcement worktop made by the_ply" width="auto" height="auto">
                    <figcaption class="figure-caption"> Grey Topciment microcement worktop made by the_ply</figcaption>
                </figure>
                <h2>How to make a microcement worktop?</h2>

                <p>
                    The process of applying a microcement worktop will depend on the type of finish you are looking for
                    and the material used as the base of the surface, whether it is an absorbent coating such as
                    plasterboard, plaster, cement and sand plaster or MDF, or whether it is a non-absorbent coating such
                    as marble, terrazzo, tiles or granite.

                </p>
                <p>
                    We detail the application of microcement on worktops, step by step, depending on the desired result:

                </p>
                <h3>Lay the mesh on the future microcement worktop.</h3>

                <p>
                    The first step is to place the mesh on the surface that will form the base of the microcement
                    worktop: marble, granite, plasterboard, etc... This is extremely important because it will prevent
                    cracks from appearing over time.

                </p>
                <h3>Priming the microcement worktop </h3>

                <p>
                    The second step in the application of microcement on a worktop consists of priming, which will
                    facilitate the union of the existing absorbent or non-absorbent coating with the new microcement
                    worktop. In the first case, PRIMACEM ABS adhesion promoter should be applied and, in the second
                    case, PRIMACEM PLUS primer should be applied.

                </p>
                <h3>3. Apply two coats of Microbase microcement on the worktop</h3>

                <p>
                    Next, apply two coats of Microbase microcement. The first coat should be unpigmented and, after
                    sanding, a second coat of Microbase already pigmented according to the colour of microcement chosen
                    for the worktop. Afterwards, it will have to be sanded again. If a stone or rustic finish is
                    desired, after the two coats of Microbase microcement, the microcement worktop should be sealed to
                    protect it, first with Presealer acrylic varnish and then with Topsealer polyurethane varnish.

                </p>
                <h3>4. Apply two coats of Microfino microcement on the worktop </h3>

                <p>
                    If, on the other hand, you want the microcement worktop to have a marked water or stucco appearance,
                    after the two coats of Microbase microcement, it will be the turn of two coats of Microfino
                    microcement, both pigmented. It will be necessary to sand after each coat, without exception. Once
                    the two coats of Microfino have been applied, the substrate should be protected first with Presealer
                    and then with Topsealer.

                </p>
                <h3>5. Apply two coats of Microdeck microcement on the worktop</h3>

                <p>
                    If you prefer the microcement worktop to stand out for its natural appearance, after the two coats
                    of Microbase it is recommended to apply two coats of pigmented Microdeck microcement, sanding at the
                    end. To finish, as with the two previous applications, Presealer varnish and Topsealer microcement
                    sealer should be applied to the substrate.

                </p>

                <h3>6. Protecting and sealing the microcement worktop</h3>

                <p>
                    Once the two coats of Microbase, Microfino or Microdeck have been applied, depending on the desired
                    finish, the microcement worktop must be protected to avoid problems. To properly seal the furniture,
                    two coats of Presealer and two coats of Topsealer are necessary, as explained above.

                </p>
                <figure class="figure mt-4">
                    <img src="https://www.topciment.com/imagenes/encimeras/microcemento-topciment-para-encimera-cocina.jpg"
                        class="figure-img img-fluid " title="marfil microcement worktop"
                        alt="Marfil microcement worktop" width="auto" height="auto">
                    <figcaption class="figure-caption">Microcement worktop in Marfil colour by Topciment.
                    </figcaption>
                </figure>
                <h2>Types of microcement worktops</h2>

                <p>
                    However, it is important to bear in mind that in order to do a microcement worktop correctly, it is
                    essential to hire a qualified professional. Only an applicator with years of experience will prevent
                    future problems with microcement worktops, such as liquids seeping in or different stains appearing
                    due to poor priming or sealing.

                </p>
                <p>
                    There are a multitude of worktops covered with microcement due to the multiple benefits mentioned
                    above. This material stands out for its thin thickness (between 2 and 3 millimetres), which allows
                    for a wide variety of finishes and uses, as it does not affects the structural load of the surfaces.
                    For all
                    these reasons, it is very common to find microcement worktops in all types of rooms.

                </p>
                <h2>Price of microcement worktops</h2>

                <p>
                    Only a professional can determine the price of a microcement worktop. An amount that will fluctuate
                    depending on the shape of the worktop, the length and metres to be covered, the complexity of the
                    work or the type of microcement applied to it.

                </p>

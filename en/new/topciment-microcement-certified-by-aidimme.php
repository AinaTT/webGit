                    customers and distributors. </p>
                <div id="halfTextLeft" data-animate-scroll='{"x":"-100", "y": "0", "alpha": "0", "duration": "1"}'>
                    <p>The AIDIMME (Instituto Tecnológico Metalmecánico, Mueble, Madera, Embalaje y Afines) laboratory,
                        which has carried out these certification tests, is one of the best equipped in Europe, and has
                        accumulated knowledge since 1994, when the facilities were inaugurated, to serve a growing need
                        for companies to certify the quality of their manufacturing processes. </p>
                    <p>The certification tests carried out by AIDIMME consist of reproducing the conditions to which
                        products may be exposed and seeing how they respond. AIDIMME's laboratories are accredited by
                        ENAC and other international bodies such as ISTA.</p>
                    <p>To quantify the wear of our coatings, the resistance to abrasion is studied using the Taber
                        method for paints and varnishes under the UNE 48 250:1992 standard, carried out with CS-17
                        sandpaper, with load and different abrasion cycles, simulating medium or high use. </p>
                </div>
                <div id="halfTextRight" data-animate-scroll='{"x": "0", "y": "50", "alpha": "0", "duration": "2"}'>
                    <p>In addition, the Shore hardness is analysed under the UNE-EN ISO 868 standard, determining
                        penetration on the surface of the coating and resistance to impact under the UNE EN 6272
                        standard, where rapid deformation is determined. </p>
                    <p>The adhesion of the coatings is studied by means of the Lattice Shear Test under the UNE en
                        2409:2013 standard, thus assessing if there is detachment of the coating.</p> <img
                        src="https://www.topciment.com/imagenes/aidimme/microcemento-aidimme.jpg" width="100%"
                        alt="Topciment microcement with AIDIMME testing and certification">
                    <p>Resistance to liquids is also examined under the UNE EN ISO 2813-3:2013 standard, and resistance
                        to chemical agents under the UNE EN 13442:2013 standard, thus demonstrating the proper use and
                        maintenance of our developments. </p>
                    <p>Specific tests are carried out on each product according to its final application, such as the
                        determination of the value of the slip/slip resistance of pavements, proving that our product is
                        suitable for use on surfaces with a slope equal to or greater than 6% and on stairs.</p>
                    <p>Topciment, following its philosophy of offering the highest quality in all the products it

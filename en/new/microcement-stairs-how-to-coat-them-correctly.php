                <h1>Microcement stairs: how to coat them correctly</h1>
                <div class="headline fadeInRight styleNormal16justified">
                    <p>
                        Microcement is the decorative coating par excellence for floors and walls. In fact, it is very
                        common to see continuous flooring and walls coated with microcement in all types of commercial
                        premises and businesses. But also in the domestic sphere, where this coating has gained stellar
                        popularity in the renovation of bathrooms and kitchens thanks to the waterproof and non-slip
                        finishes offered by microcement.
                    </p>
                    <p>
                        A versatility to which we must add microcement stairs, a differentiating decorative element that
                        more and more professionals and individuals are taking into account due to its modern
                        aesthetics, the continuity it provides as it does not need expansion joints and, above all, its
                        extraordinary functionality compared to other materials.

                    </p>

                    <p>
                        It should not be forgotten that although microcement stairs are designed to facilitate and speed
                        up access to the different floors of the building in question, they are also a powerful design
                        element on a visual level due to their breaking character.

                    </p>
                    <p>
                        In this article we will cover everything you need to know about microcement stairs: advantages,
                        types, the most suitable colours, how to make a microcement staircase correctly and a series of
                        tips to help you become a real expert on the subject.

                    </p>

                    <figure class="figure mt-4">
                        <img src="https://www.topciment.com/img/microcemento/escalera-microcemento-plata.webp"
                            class="figure-img img-fluid " title="spectacular grey microcement spiral staircase"
                            alt="spectacular grey microcement spiral staircase" width="auto" height="auto">
                        <figcaption class="figure-caption">Spectacular spiral staircase covered with grey microcement by
                            Topciment.</figcaption>
                    </figure>

                    <h2>Advantages of microcement stairs</h2>

                    <p>
                        Why have microcement staircases become a decorative trend? Below we explain the main benefits
                        that have propelled microcement stairs to the top.

                    </p>
                    <h3>1. Microcement stairs encourage continuity</h3>

                    <p>
                        Stairs coated with microcement are becoming more and more popular for many reasons, especially
                        because they do not need expansion joints, unlike wooden or parquet stairs, for example. An
                        added value as microcement stairs promote the continuity of the staircase itself and, above all,
                        of the room in which they are located.

                    </p>
                    <p>
                        All the elements (staircase, floor, walls, ceiling) form part of a whole as they are perfectly
                        integrated. An appearance of absolute continuity that can go one step further if the colour of
                        the microcement staircase matches that of the flooring.

                    </p>
                    <h3>2.Forget about building work with microcement stairs</h3>

                    <p>
                        Microcement is a highly adherent decorative coating that is applied directly onto existing
                        material such as marble or concrete. A competitive advantage that allows you to renovate stairs
                        with microcement without the need for building work and rubble in just a few hours, with the
                        time and cost savings that this entails.

                    </p>
                    <h3>3. Easy cleaning of microcement stairs </h3>

                    <p>
                        Small staircases do not usually cause many problems when it comes to cleaning. But what about
                        those endless stairs connecting floors and floors that make you dizzy just looking at them?

                    </p>
                    <p>
                        Microcement stairs are very practical, as they are very easy to clean as they are a continuous
                        surface without joints, which makes the cleaning process of each step quick and easy. With water
                        and neutral soap, it is more than enough.

                    </p>

                    <h3>4. Microcement stairs are very resistant </h3>

                    <p>
                        If there is a differentiating element of microcement stairs, it is their durability. They will
                        remain in perfect condition over the years thanks to their excellent resistance, especially to
                        traffic, impact and abrasion. Topciment microcement for stairs is a material that withstands all
                        kinds of blows.

                    </p>
                    <h3>5. Microcement on stairs does not affect structural load</h3>

                    <p>
                        It should be noted that microcement is very thin (between 2 and 3 millimetres). This means that
                        coating stairs with microcement does not affect the structural load in any way.

                    </p>
                    <h3>
                        6. Microcement stairs for interiors and exteriors

                    </h3>
                    <p>
                        Microcement stairs are a very versatile architectural element that is mainly used in interior
                        design because of the feeling of stability, spaciousness and cleanliness it offers. However, it
                        has become increasingly fashionable to coat stairs with microcement also in exteriors, due to
                        the elegance, majesty and presence they exude.

                    </p>
                    <h3>7. Microcement stairs with anti-slip finish</h3>

                    <p>
                        Thanks to our Microdeck microcements, the microcement staircase will be a safe space free from
                        slips and falls, as they are types of smoothed microcement that guarantee this non-slip finish.
                        Also, if the applicator chooses another type of microcement, in Topciment we have several
                        varnishes within the Topsealer range that give the microcement staircase that non-slip
                        character.

                    </p>
                    <figure class="figure mt-4">
                        <img src="https://www.topciment.com/imagenes/news/escalera-microcemento-interior-gris.jpg"
                            class="figure-img img-fluid " title="open interior microcement staircase with one wall"
                            alt="open interior microcement staircase with one wall " width="auto" height="auto">
                        <figcaption class="figure-caption">Indoor open interior microcement staircase with a wall that
                            goes in line with the floor also coated with Topciment microcement.
                        </figcaption>
                    </figure>
                    <h2>Types of microcement stairs</h2>

                    <p>
                        The design possibilities that microcement allows for the coating of stairs are unlimited. In our
                        categorisation, we have taken two factors into account. Firstly, whether the steps of the
                        microcement stairs are between the walls or not and the type of risers.

                    </p>
                    <p>
                        With this differentiation, we find the following microcement stairs: closed microcement stairs
                        with two walls, closed microcement stairs with one wall, closed microcement stairs without
                        walls, open microcement stairs with two walls, open microcement stairs with one wall, open
                        microcement stairs without walls, recessed microcement stairs in one wall, recessed microcement
                        stairs without walls and microcement stairs with central substrate.

                    </p>

                    <h2> Which microcement and colour to use for stairs</h2>

                    <p>
                        Although grey is the most popular colour for coating stairs with microcement, all kinds of
                        colours can be applied to give a differentiating element to the decoration of your home or
                        business. In Topciment, we have a wide range of microcement colours for stairs. From neutral
                        colours for a more minimalist and industrial design to brighter colours if you are looking for a
                        more daring decoration.

                    </p>
                    <p>
                        As for the type of microcement, our experts recommend Microdeck finishing microcement for
                        coating stairs. At Topciment we have developed three types of Microdeck: Sttandard Microdeck
                        (traditional two-component microcement), Unlimitted Microdeck (two-component microcement that
                        improves workability) and Evoluttion Microdeck (one-component microcement).

                    </p>
                    <figure class="figure mt-4"> <img
                            src="https://www.topciment.com/imagenes/news/escalera-microcemento-exterior-acero.jpg"
                            class="figure-img img-fluid " title="outdoor microcement staircase Steel"
                            alt=" Exterior microcement staircase in Steel colour " width="auto" height="auto">
                        <figcaption class="figure-caption">Steel coloured microcement staircase by Topciment on the
                            exterior of a home</figcaption>
                    </figure>
                    <h2>How to make microcement stairs:</h2>

                    <p>
                        The first thing to bear in mind when applying microcement on a staircase is that this process
                        can only be carried out by an experienced professional. Coating a staircase with microcement
                        requires an advanced application technique to obtain the desired result. A series of
                        particularities that must be taken into account and that only a trained applicator will be able
                        to guarantee.

                    </p>
                    <h3>Applying microcement on stairs in 8 steps</h3>

                    <p>
                        Take note of the 8 steps to follow for stairs coated with microcement.

                    </p>
                    <h4>1. Assess the state of the substrate</h4>

                    <p>
                        The state of the existing substrate on the stairs must be analysed before applying microcement.
                        If the substrate that will form the base of the microcement staircase is not in optimum
                        condition, all kinds of cracks or defects could appear. That is why it is so important to have a
                        good professional who will know how to correctly assess the quality of the substrate.

                    </p>
                    <h4>2. Priming the microcement staircase</h4>

                    <p>
                        Once we are sure that the substrate is in good condition, the second step to make a microcement
                        staircase is to apply the primer or adhesion promoter.

                    </p>
                    <h4>
                        3. Levelling the steps of the microcement staircase

                    </h4>

                    <p>
                        The third step in making a microcement staircase consists of levelling and protecting the steps.
                        This prevents future breakage and wear by eliminating any irregularities that may be present.

                    </p>
                    <h4>
                        4. Apply the first layer of microcement Microbase on the staircase

                    </h4>
                    <p>
                        The fourth step in the application of microcement on stairs consists of applying a first coat of
                        Microbase without pigment. Once this is done, it is sanded.

                    </p>
                    <figure class="figure mt-4">
                        <img src="https://www.topciment.com/imagenes/news/escalera-microcemento-interior.jpg"
                            class="figure-img img-fluid " title="interior microcement staircase with central support"
                            alt="interior microcement staircase with central support " width="auto" height="auto">
                        <figcaption class="figure-caption">Microcement staircase inside a house with central support.
                        </figcaption>
                    </figure>
                    <h4>
                        5. Apply a second coat of Microcement Microbase on the staircase

                    </h4>
                    <p>
                        The fifth step of application on microcement stairs is to apply a second layer of Microbase, in
                        this case with pigment. The procedure and sanding of the previous step must be repeated.

                    </p>
                    <h4>
                        6. Applying the first layer of Microdeck microcement on the staircase

                    </h4>

                    <p>
                        The sixth step in applying microcement on stairs is a first thin layer of Microdeck with colour.
                        Once the layer is dry, the surface should be sanded.

                    </p>
                    <h4>
                        7. Apply a second layer of Microdeck microcement on the staircase

                    </h4>

                    <p>
                        The sixth step in making microcement stairs is to apply the second layer of Microdeck. As soon
                        as it dries it will be necessary to sand the surface.

                    </p>
                    <h4>
                        8. Sealing and protecting the microcement staircase

                    </h4>

                    <p>
                        The last step in the application of microcement on stairs is one of the most important, as it is
                        a question of protecting the coating and ensuring that it remains in optimum condition for a
                        long time.

                    </p>
                    <p> To do this, at Topciment we recommend applying two coats of
                        <strong><a class="links"
                                href="../microcements/polyurethane-waterbased-acrylic-varnishes-topsealer#presealer">
                                Presealer acrylic varnish</a></strong> and
                        another two coats of one of our polyurethane varnishes, especially
                        <strong> <a class="links"
                                href="../microcements/polyurethane-waterbased-acrylic-varnishes-topsealer#topsealer-one-coat">
                                Topsealer WT One Coat</a></strong> .

                    </p>
                    <h2> 3 tips for applying microcement on staircases</h2>

                    <p>
                        As mentioned above, not everyone is qualified to make microcement stairs, only professional
                        applicators with years of experience due to the complexity involved.

                    </p>
                    <p>
                        These are the three main tips to keep in mind when making microcement stairs:

                    </p>
                    <h3>
                        1. Protect the edges of the microcement stairs with aluminium profile .

                    </h3>
                    <p>
                        The corners or edges of the steps of microcement stairs are, without a doubt, the area most
                        exposed to all kinds of scratches and knocks. For this reason, as manufacturers, we recommend
                        the use of corner pieces or<strong><a class="links"  href="../microcements/microcement-tools#perfil"> aluminium profiles</a></strong>. These
                        complements can be hidden
                        from view (although we recommend leaving them visible because they add a touch of elegance) and
                        will protect the microcement stairs better and for longer, thus preventing breakage or wear and
                        tear.
                    </p>
                    <figure class="figure mt-4">
                        <img src="https://www.topciment.com/imagenes/news/escalera-microcemento-marfil-con-perfil-aluminio.jpeg"
                            class="figure-img img-fluid " title="outdoor microcement staircase Steel"
                            alt="Exterior microcement staircase in Steel colour" width="auto" height="auto">
                        <figcaption class="figure-caption">Steel coloured microcement staircase by Topciment on the exterior of a house.</figcaption>
                    </figure>
                    <h3>
                        2. Minimally rounded edges of the microcement staircase 
                    </h3>    
                    <p>
                        Although it is possible to leave the step of the microcement staircase at 90 degrees, it is
                        advisable that the edges are rounded, even if only minimally. By following this advice and
                        "cutting" the edges of the future microcement staircase, chipping or knocks are avoided as the
                        impact on the microcement staircase is less.

                    </p>
                    <h3> 3. Fibreglass mesh for microcement stairs
                    </h3>

                    <p>
                        Although some applicators do not always place a fibre mesh on the microcement base layer, at
                        Topciment we are in favour of always doing so. Thanks to the mesh, we can reinforce the
                        microcement staircase.
                    </p>


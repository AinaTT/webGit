                    <h2>How to install a microcement floor
                    </h2>
                    <p>Now that you know all the virtues of microcement flooring, it is time to
                        explain the application process. Here are the six steps to take into account
                        to install a microcement floor successfully:
                    </p>
                    <h3> 1. Cleaning the future microcement floor</h3>
                    <p>The flooring to be covered with microcement must be in perfect condition.
                        Therefore, the first step is to clean the future microcement floor of dust,
                        stains, grease or any other type of particles, as well as making sure that
                        the base is in perfect condition. It is very important to check the humidity
                        level of the floor, wait for it to dry or apply an impoxy barrier to block
                        rising damp or as a vapour barrier.
                    </p>
                    <h3>2. Primer</h3>
                    <p>Once the surface is clean, apply a coat of primer on the flooring to enhance
                        the adhesion between the existing substrate and the new microcement floor.
                        If the base surface is absorbent, such as concrete or self-levelling,
                        PRIMACEM ABS
                        <!-- (link) --> should be used; if it is non-absorbent, such as
                        terrazzo, tile or marble, PRIMACEM PLUS
                        <!-- <!-- (link) --> --&gt; should be used.
                    </p>
                    <h3>3. First coat of microcement base </h3>
                    <p>After priming, apply the first layer of unpigmented microcement base. The use
                        of a fibreglass mesh is recommended, as it will help the professional to
                        minimise the chances of cracks appearing over time. Once applied, sand the
                        surface.
                    </p>
                    <h3>4. Second coat of microcement base</h3>
                    <p>Once the surface is completely dry, apply a second coat of pigmented
                        microcement base. In this step, choose the desired microcement colour to
                        cover the continuous flooring and, once the surface is perfectly covered,
                        sand the microcement floor.
                    </p>
                    <h3>5. Two coats of finishing microcement </h3>
                    <p>The fifth step consists of applying two coats of finishing microcement or
                        pigmented finish. Depending on the finish and the degree of slipperiness
                        desired, medium or thick microcement can be used. As specialists, we
                        recommend two coats of Microdeck, our medium microcement, which is optimal
                        for the application of microcement on floors.
                    </p>
                    <h3>6. Microcement floor protection</h3>
                    <p>The last step in the application of microcement on floors is one of the most
                        important. The microcement floor is ready, now it needs to be protected. To
                        do this, two coats of PRESEALER
                        <!-- (link) --> are applied by roller, followed by
                        two coats of TOPSEALER
                        <!-- (link) -->. In addition to protecting the microcement of
                        the floor, it can be waterproofed and even achieve an anti-slip finish.

                    </p>

                    <p>
                        On this page

                    <h2>Types of microcement for floors
                    </h2>
                    <p>If there is one particularity that perfectly defines microcement, it is its
                        versatility, as there are different types depending on the texture and
                        finish you want to obtain: one-component, two-component and ready-to-use. At
                        Topciment we have up to four different microcement systems for floors:
                    </p>
                    <p class="h3">
                        Two-component microcement</p>
                    <p>Two-component microcement (cement + resin) is undoubtedly one of the most
                        widely used in microcement flooring. For this reason, we have created two
                        lines: Sttandard
                        <!-- (link) -->, the traditional and one of the most popular; and
                        Unlimitted
                        <!-- (link) -->, the new range that provides smoother finishes and greater
                        mechanical resistance.
                    </p>
                    <p class="h3">
                        One-component microceme</p>
                    <p>Evoluttion
                        <!-- (link) -->, our one-component microcement for floors with excellent
                        mechanical resistance and high workability, is formulated with water and
                        cement. All types of microcement floors can be designed with it.
                    </p>
                    <p class="h3">Ready-to-use microcement</p>
                    <p>Efectto Quartz
                        <!-- (link) --> is our ready-to-use microcement for floors. Thanks to
                        its wide range of colours and its four granulometries, you can create any
                        microcement floor you can imagine.
                    </p>
                    <h2>Microcement flooring in bathrooms and kitchens</h2>
                    <p>Microcement can be used both as exterior flooring and interior flooring in
                        any room. Among its many qualities, its ability to be waterproof (with the
                        use of a special sealant for this purpose) and its excellent resistance to
                        heat, traffic and stains make this coating an ideal material especially for
                        kitchens
                        <!-- (link new news) --> and bathrooms
                        <!--  (link new news). -->
                    </p>
                    <p>Thanks to the large number of microcement colours that exist, it is possible
                        to create an infinite number of floor coverings. In our gallery  you will find multiple microcement floors in bathrooms and
                        kitchens.
                    </p>
                    <h2>Colours for microcement floors</h2>
                    <p>White microcement is undoubtedly one of the most demanded colours due to a
                        marked minimalist style that is increasingly present in the interior
                        decoration of private homes and commercial premises. But grey microcement
                        floors are not far behind either. The industrial style is gaining more and
                        more followers to its cause, a style that gives the feeling that it will not
                        go out of fashion no matter how many years go by as it fits perfectly with
                        offices, large surfaces and warehouses and also houses.
                    </p>
                    <p>Although white and grey are the microcement colours par excellence, there is
                        a wide range of possibilities. In Topciment we have multiple colours for
                        microcement floors depending on the character you want to give to the room
                        where the microcement floor is going to be applied. We have designed
                        specific colours according to the microcement systems we manufacture (link
                        to PRODUCTS).
                    </p>

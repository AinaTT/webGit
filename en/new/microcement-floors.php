                <h1> Microcement floors: application, types, advantages and tips</h1>
                <div class="titular  fadeInRight estiloNormal16justificado">

                    <p>Microcement is one of the most widespread continuous floorings in the world
                        due to the decorative opportunities it offers for both indoors and outdoors.
                        A coating that can be applied on kitchen floors, bathroom floors, terrace
                        floors, etc.
                    </p>
                    <p>A building material that, although it has years of history, has become much
                        more popular in recent years, especially for covering all types of flooring.
                        Here we show you everything you need to know about microcement flooring: how
                        it is applied, its main advantages, the types of microcement available for
                        floors, tips... and much more.
                    </p>
                    <h2> Benefits of microcement on floors</h2>
                    <p>Applying microcement on floors offers countless advantages. Here is a summary
                        of some of the most important benefits that this innovative decorative
                        coating can offer as flooring:
                    </p>
                    <ul>
                        <li> It does not raise the level of the floor too much thanks to its minimum
                            thickness of between 2 and 3 mm. </li>
                        <li> It is a continuous floor, which means that there are no expansion
                            joints or joints. The entire floor renovation is done in one single
                            piece. </li>
                        <li>It adheres to any type of material without the need to remove the
                            existing substrate. It avoids generating debris and heavy renovations.
                        </li>
                        <li>It is unalterable to UV rays and has very good thermal resistance. </li>
                        <li>It has enviable resistance to traffic, abrasion and domestic cleaning
                            products. </li>
                        <li>No two microcement floors are alike thanks to the variety of colours and
                            finishes offered by this decorative covering. </li>
                        <li>It can be used as interior flooring but also as exterior flooring, for
                            example on terraces. </li>
                    </ul>

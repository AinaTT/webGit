                                    <h2>3.Choose between shower and bathtub</h2>
                                    <p>When it comes to choosing with the environment in mind, the shower has gained ground over the bathtub because of its functionality and three times less water expenditure. This is not the case in large bathrooms, where you can opt for comfort: generously sized baths or hydromassage columns. If we are hesitant about space, from five square metres onwards a bathtub can be installed without any problems.</p>
                                    <p> </p>
                                    <img src="https://www.topciment.com/imagenes/banos-microcemento/revestimiento-cemento-pulido-banos.jpg"

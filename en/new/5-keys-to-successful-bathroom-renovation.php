                                    <p>A place of relaxation and wellbeing, the bathroom should be as easy to live in as it is to maintain. The presence of water in this room means that you cannot just think about comfort when renovating it, as it demands quality materials that are resistant to humidity. Whether you want to start from scratch, renovate or enlarge your bathroom, here are a few simple steps to achieve a great result.</p>

                                    <h2>1. Organise the space</h2>
                                    <p>The bathroom is often the smallest area in the house and therefore needs to be optimised in size. The first step is to have the measurements and to think about the light points and the installation of toilets. A word of advice: in order not to make the passage difficult, it is necessary to maintain a distance of 60 centimetres in front of the sink or toilet, and about 35 centimetres between elements. There are tricks for making the micro-cement bathroom look bigger: the continuity of the micro-cement gives a greater feeling of spaciousness, as does the use of light colours for the bathroom floor and walls.</p>
                                    <p> </p>

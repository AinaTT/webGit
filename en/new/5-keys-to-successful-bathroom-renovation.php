                                    <h2>5.Lighting: finding a balance</h2>
                                    <p>Essential for creating a relaxed atmosphere, lights should not be neglected and their placement should be well thought out. There are many alternatives: ceiling lamps, wall lights, halogen or around the mirror. If there is natural lighting, that bathroom has a clear advantage over the rest. Most of the time it is interesting to have a front focus on the mirror to illuminate the face, while in the rest of the room you can reduce the intensity and create a relaxed atmosphere. You can also play with colours: light ones reflect and multiply the light while dark ones absorb it and darken the microcement baths.</p>

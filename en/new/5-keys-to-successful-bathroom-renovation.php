                                    <h2>4.Choosing the furniture</h2>
                                    <p><br>Sanitaryware, furniture and accessories are as important in the decoration as the walls and floors of the bathroom. Inside the washbasins, for example, there are many typologies: floor-standing, recessed, on the worktop, compact... each in a multitude of materials: natural stone, resin, cement, porcelain, ceramic, glass... It all depends on the environment in which it will be placed: the classic sanitary ware will provide a romantic air, perfect for vintage decoration, while the wall-mounted ones will serve in those projects that seek a modern and contemporary touch.</p>
                                    <p> </p>
                                    <img src="https://www.topciment.com/imagenes/banos-microcemento/banos-de-cemento.jpg" width="100%"

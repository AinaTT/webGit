                                    <h2>2.Floors and walls: resistance and style</h2>
                                    <p>Bathroom coverings must satisfy decorative tastes, but also withstand humidity. Porcelain stoneware, marble or large format tiles are some of the options we find on the market. In this respect, microcement fulfils the desired functions: the absence of joints prevents the accumulation of dirt and thus facilitates the cleaning of the bathroom. In addition, it can be applied perfectly on horizontal and vertical surfaces, achieving a completely watertight room, and also on showers, baths or wash basin tops.</p>
                                    <p> </p>
                                    <img src="https://www.topciment.com/imagenes/banos-microcemento/banos-de-microcemento.jpg" width="100%"
                                        alt="baños de microcemento">

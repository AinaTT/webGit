                <h1>Polished concrete: qualities and differences with other coatings</h1>
                <p>
                    There is currently a lot of confusion, due to a lack of knowledge, about polished concrete. In fact,
                    most people associate it with the typical industrial style flooring with a strong grey tone and
                    mirror effect that is used in the interiors and exteriors of large surfaces such as offices, shops,
                    factories, warehouses and car parks. Even, although to a much lesser extent, in some private homes.

                </p>
                <p>
                    However, in most cases, what is assumed to be polished concrete is actually microcement. The
                    decorative coating that is currently a trend in interior design and decoration, both in houses and
                    in shops and premises. A coating that has also spread like wildfire on outdoor terraces, facades and
                    swimming pools.

                </p>
                <p>
                    In this article you will find everything you need to know and didn't know about polished concrete:
                    what it is, its main qualities, the different finishes it allows, the application process, the price
                    per square metre and, above all, the main differences with microcement or smoothed microcement.

                </p>
                <figure class="figure mt-4">
                    <img src="https://www.topciment.com/imagenes/news/empresa-suelo-cemento-pulido.jpg"
                        class="figure-img img-fluid " title="company with polished concrete floor."
                        alt="ground floor of a company with polished concrete floor." width="auto" height="auto">
                    <figcaption class="figure-caption">Image of the ground floor of an industrial company with polished
                        concrete floor. </figcaption>
                    <h2>What is polished concrete</h2>

                    <p>
                        Polished concrete, also called screed concrete, is actually a decorative finish, not a pure
                        cladding. This product is mainly used in outdoor areas of car parks, shopping centres or sports
                        fields and, to a much lesser extent, in some indoor rooms.

                    </p>
                    <p>
                        Polished concrete is a finish resulting from sprinkling a cementitious mortar with aggregates
                        and
                        then polishing the floor to a shine with a rotary polisher. This technique of polishing the
                        floor is
                        called trowelling.

                    </p>
                    <h2>
                        Properties and advantages of polished concrete

                    </h2>
                    <p>
                        Polished concrete is designed to cover surfaces where the functionality of the flooring is more
                        important than its aesthetics. Why? Because polished concrete is characterised by its great
                        durability and resistance, two elements that make it ideal for covering industrial surfaces that
                        are
                        exposed to heavy traffic and weight. In fact, they are widely used for loading and unloading
                        areas,
                        as polished concrete flooring does not crack or chip.

                    </p>
                    <p>
                        In addition, polished concrete floors are very resistant to tyre rolling. This is why we see
                        this
                        type of finish so often in car parks and car parks.

                    </p>
                    <p>
                        Another quality of polished concrete floors is that their smooth and shiny surface prevents the
                        accumulation of dust and dirt. A finish that also enhances the feeling of spaciousness.

                    </p>
                    <figure class="figure mt-4">
                        <img src="https://www.topciment.com/imagenes/news/pulidora-rotativa.jpg"
                            class="figure-img img-fluid " title=" rotary polisher."
                            alt=" operator polishing polished concrete pavement with a rotary polisher." width="auto"
                            height="auto">
                        <figcaption class="figure-caption"> Operator polishing and shining polished concrete pavement
                            with a rotary polisher.</figcaption>
                    </figure>
                    <h2>
                        Inconveniences or disadvantages of polished concrete

                    </h2>
                    <ul class="ml-3">
                        However, it is also important to know that polished concrete has a series of inconveniences and
                        disadvantages to be taken into account. The following are the main ones:

                        <li> Polished concrete can lose its shine over time due to very abrasive liquids such as
                            industrial
                            solvents. It will therefore have to be polished again. </li>

                        <li> Polished concrete can end up cracking if the expansion joints are not properly made. </li>

                        <li> This finish should be avoided in places with cold or very hot temperatures, as polished
                            concrete is not a good thermal insulator. </li>

                        <li> The application process is time-consuming, as it requires building work and the use of
                            machinery. In addition, it has joints. </li>

                        <li> Poor sound insulation. </li>

                        <li> Low porosity. </li>
                    </ul>
                    <h2>Polished concrete finishes</h2>

                    <p>
                        Although polished concrete is mainly associated with grey finishes due to the locations where it
                        is
                        usually applied, there is the possibility of choosing colours that are a priori more daring,
                        such as
                        black, red, green... Polished concrete is used in airports, car parks and garages, shopping
                        centres
                        and factories.

                    </p>

                    <h3>Polished concrete bathrooms</h3>

                    <p>
                        Polished concrete bathrooms do not exist. This type of finish is often confused with the use of
                        other decorative coatings such as burnished concrete or a microcement washbasin. In fact,
                        microcement is one of the most commonly used coatings to renovate this room due to the great
                        possibilities of use it offers.

                    </p>
                    <p>
                        Microcement in bathrooms is perfect for lining shower trays or bathtubs, thanks to its <a
                            class="links" href="../microcements/polyurethane-waterbased-acrylic-varnishes-topsealer#topsealer-proplus">waterproof</a>
                        and <a href="../microcements/polyurethane-waterbased-acrylic-varnishes-topsealer#topsealer-anti-slip">anti-slip </a> properties
                        thanks to the
                        appropriate use of sealants. But also to create shelves, bathroom furniture with microcement,
                        walls
                        and floors.

                    </p>
                    <figure class="figure mt-4">
                        <img src="https://www.topciment.com/imagenes/news/nave-suelo-cemento-pulido.jpg"
                            class="figure-img img-fluid " title="nave suelo cemento pulido."
                            alt="industrial building with polished concrete floor." width="auto" height="auto">
                        <figcaption class="figure-caption">Imposing industrial building with polished concrete floor
                        </figcaption>
                    </figure>
                    <h2> Differences between polished concrete and microcement</h2>

                    <p>
                        We are going to discuss the main differences between polished concrete and microcement.

                    </p>
                    <h3>1. Polished concrete is thicker than microcement</h3>

                    <p>
                        The most noticeable difference between the two finishes is their thickness. Microcement is
                        barely
                        between 2 and 3 millimetres thick, which means that it can be applied on horizontal surfaces
                        (floors, stairs, ceilings...) as well as on vertical surfaces such as walls. It is a thin
                        building
                        material so light that it can be applied anywhere without fear that the structure will be
                        affected.

                    </p>
                    <p>
                        This is not the case with polished concrete, which is therefore reserved only for floors and
                        pavements.

                    </p>
                    <h3>2. Polished concrete generates debris, microcement does not</h3>

                    <p>
                        Another disparity between microcement and polished concrete relates to debris. Smoothed
                        microcement
                        is laid on the existing material, whether floor or wall. An excellent adherence that avoids
                        building
                        work and the debris that this entails.

                    </p>
                    <p>
                        This is the complete opposite of polished concrete, a finish that requires building work to
                        completely lift the existing floor or paving and the use of machinery to do so.

                    </p>
                    <h3>3. The installation of polished concrete is much more time consuming</h3>

                    <p>
                        This point is closely related to the previous one. As microcement does not require any building
                        work, the application process is very fast. A procedure that in the case of polished concrete
                        takes
                        much longer.
                    </p>
                    <figure class="figure mt-4">
                        <img src="https://www.topciment.com/imagenes/news/pavimento-cemento-pulido-nave.jpg"
                            class="figure-img img-fluid " title="polished concrete floor."
                            alt="Bright industrial building with polished floor." width="auto" height="auto">
                        <figcaption class="figure-caption">One of the rooms of a large industrial building where
                            polished concrete floor has been installed. </figcaption>
                    </figure>
                    <h3>4. Compatibility vs. incompatibility with underfloor heating</h3>

                    <p>
                        Underfloor heating is a great alternative to radiators and central heating because of its
                        benefits.
                        However, this system is not compatible with any coating.

                    </p>
                    <p>
                        Microcement, which has a high thermal conductivity, is perfectly compatible with underfloor
                        heating.
                        In fact, it is one of the most recommended construction materials. <a class="links"
                            href="micrcocement-over-underfloor-heating">Here</a>
                        you will find everything you need to know. A quality that polished concrete cannot boast.

                    </p>
                    <h3>5. Only polished concrete needs expansion joints</h3>

                    <p>
                        Polished concrete requires expansion joints, approximately every 4 to 5 metres. Microcement, on
                        the
                        other hand, is a continuous covering and, therefore, no expansion joints need to be made. This
                        is
                        why the use of smoothed microcement has increased in interiors to the detriment of polished
                        concrete.

                    </p>
                    <h3> 6. Microcement is more versatile than polished concrete</h3>

                    <p>
                        Microcement is not only a continuous coating that does not require building work or expansion
                        joints, but it is also quicker and easier to apply.

                    </p>
                    <p>
                        But not only for this reason, but because smoothed microcement also provides the security of
                        being a
                        coating with excellent durability and resistance not only to traffic like polished concrete, but
                        also to knocks, chemical products and scratches.

                    </p>
                    <p>
                        And, above all, because microcement offers more decorative solutions than polished concrete as
                        it
                        can be applied on any surface (horizontal or vertical) and material (concrete, tiles, tiles,
                        ceramic...). A wider range that makes it easier for microcement to be used in interiors and
                        exteriors, both in private homes and in premises, businesses and other companies.

                    </p>
                    <p>
                        Definitely, for all these reasons and more, microcement is much more versatile than polished
                        cement.

                    </p>
